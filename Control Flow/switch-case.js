/* 
   switch-case এর মাধ্যমে আমরা একটি নির্দিষ্ট ভ্যালুর ওপর ভিত্তি করে 
   একাধিক কন্ডিশন বা 'কেস' চেক করতে পারি। এটি অনেকগুলো else-if এর একটি সহজ বিকল্প।
*/

/* 
   এখানে switch এর ব্র্যাকেটে (২) দেওয়া হয়েছে। 
   এর মানে হলো, নিচের কেসগুলোর মধ্যে যেটির মান ২ হবে, সেটিই কার্যকর হবে। 
*/
switch (2) {
  /* 
     যদি switch এর মান ১ হতো, তবে এই কেসটি রান করতো। 
     যেহেতু আমাদের মান ২, তাই এটি স্কিপ হবে। 
  */
  case 1:
    console.log("Case 1");
    /* break ব্যবহার করা হয় যাতে সঠিক কেসটি পাওয়ার পর কোডটি switch ব্লক থেকে বের হয়ে যায়। */
    break;

  /* 
     এখানে মান ২ মিলে যাচ্ছে, তাই এই ব্লকটি রান করবে এবং কনসোলে "Case 2" প্রিন্ট হবে। 
  */
  case 2:
    console.log("Case 2");
    /* 
       break দেওয়া অত্যন্ত জরুরি। যদি এখানে break না দেওয়া হতো, 
       তবে এর পরের কেসগুলোও (যেমন: Case 3) অটোমেটিক রান হয়ে যেত। 
    */
    break;

  /* 
     যদি switch এর মান ৩ হতো, তবে এটি রান করতো। 
  */
  case 3:
    console.log("Case 3");
    break;

  /* 
     default ব্লকটি তখনই কাজ করে যখন উপরের কোনো কেস-ই (Case 1, 2, 3) সত্য হয় না। 
     এটি অনেকটা if-else ল্যাডারের শেষের 'else' এর মতো কাজ করে। 
  */
  default: {
    console.log("No cases matched.");
    /* 
       ডিফল্ট ব্লকের শেষে break দেওয়া বাধ্যতামূলক নয় কারণ এটিই ব্লকের শেষ অংশ, 
       তবে কোডের ধারাবাহিকতা বজায় রাখতে এটি ব্যবহার করা ভালো প্র্যাকটিস। 
    */
    break;
  }
}
