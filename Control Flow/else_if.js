/* 
   else-if ল্যাডার (ladder) ব্যবহার করে আমরা একাধিক কন্ডিশন বা শর্ত চেক করতে পারি। 
   এটি তখনই কার্যকর হয় যখন প্রথম 'if' মিথ্যা হয়, তখন পরের 'else if' চেক করা হয়। 
*/

/* 
   এখানে চেক করা হচ্ছে ইউজার কি লগইন করা (loggedIn) এবং সে কি অ্যাডমিন (admin)? 
   যদি দুটি শর্তই সত্য হয়, তবেই এই ব্লকটি কাজ করবে। 
*/
if (loggedIn && admin) {
  console.log("Welcome Admin");
} 
/* 
   যদি উপরের শর্তটি মিথ্যা হয় (অর্থাৎ সে অ্যাডমিন নয়), তবে চেক করা হবে সে কি অন্তত লগইন করা? 
   যদি loggedIn সত্য হয়, তবে এই ব্লকটি রান করবে। 
*/
else if (loggedIn) {
  console.log("Welcome User");
} 
/* 
   যদি উপরের কোনো শর্তই সত্য না হয় (অর্থাৎ ইউজার লগইন করা নেই), তবে ডিফল্ট হিসেবে এই else ব্লকটি রান করবে। 
*/
else {
  console.log("Please login first");
}

/* 
   এখানে সংখ্যা তুলনার মাধ্যমে একাধিক else-if এর ব্যবহার দেখানো হয়েছে। 
   প্রথমে চেক করা হচ্ছে ১২ কি ১৩ এর চেয়ে বড়? 
*/
if (12 > 13) {
  console.log("12 is greater than 13");
} 
/* 
   যেহেতু ১২ > ১৩ মিথ্যা, তাই কোডটি এখানে আসবে এবং চেক করবে ১২ কি ১৪ এর চেয়ে বড়? 
*/
else if (12 > 14) {
  console.log("12 is greater than 14");
} 
/* 
   এটিও মিথ্যা, তাই পরের শর্তটি চেক করা হবে: ১২ কি ১৬ এর চেয়ে বড়? 
*/
else if (12 > 16) {
  console.log("12 is greater than 16");
} 
/* 
   যেহেতু উপরের কোনো শর্তই সত্য হয়নি, তাই সবশেষে এই else ব্লকটি কার্যকর হবে। 
   এর মানে হলো ১২ উপরের কোনো সংখ্যার চেয়েই বড় নয়। 
*/
else {
  console.log("12 is smaller.");
}
