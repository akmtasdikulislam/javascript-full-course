/* এখানে কন্ডিশনটি সরাসরি true দেওয়া হয়েছে, তাই if ব্লকটি রান করবে। */
if (true) {
  /* যেহেতু কন্ডিশন সত্য, তাই কনসোলে true প্রিন্ট হবে। */
  console.log(true);
} else {
  /* কন্ডিশন সত্য হওয়ায় এই অংশটি স্কিপ করা হবে। */
  console.log(false);
}

/* এখানে কন্ডিশনটি সরাসরি false দেওয়া হয়েছে, তাই if ব্লকটি বাদ দিয়ে else ব্লকটি রান করবে। */
if (false) {
  /* কন্ডিশন মিথ্যা হওয়ায় এই অংশটি রান করবে না। */
  console.log(true);
} else {
  /* কন্ডিশন মিথ্যা হওয়ার কারণে এই else ব্লকটি কার্যকর হবে এবং false প্রিন্ট করবে। */
  console.log(false);
}

/* এখানে একটি গাণিতিক তুলনা করা হয়েছে: ১২ কি ১৩ এর চেয়ে বড়? 
   যেহেতু ১২ ছোট, তাই এই কন্ডিশনটি false রিটার্ন করবে। */
if (12 > 13) {
  /* কন্ডিশন মিথ্যা হওয়ায় এটি প্রিন্ট হবে না। */
  console.log(true);
} else {
  /* ১২ > ১৩ মিথ্যা, তাই else ব্লকটি রান করবে। */
  console.log(false);
}

/* এখানে চেক করা হচ্ছে ১১২ কি ১৩ এর চেয়ে বড়? 
   হ্যাঁ, ১১২ বড়, তাই কন্ডিশনটি true হবে। */
if (112 > 13) {
  /* কন্ডিশন সত্য হওয়ায় true প্রিন্ট হবে। */
  console.log(true);
} else {
  /* কন্ডিশন সত্য হওয়ায় এই অংশটি কার্যকর হবে না। */
  console.log(false);
}

