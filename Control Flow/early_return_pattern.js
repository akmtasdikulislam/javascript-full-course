/* 
   Early Return Pattern হলো এমন একটি কোডিং স্টাইল যেখানে ফাংশনের কাঙ্ক্ষিত শর্ত 
   মিলে গেলেই 'return' কিওয়ার্ড ব্যবহার করে ফাংশন থেকে বের হয়ে আসা হয়। 
   এর ফলে কোড অনেক বেশি পরিষ্কার থাকে এবং অতিরিক্ত else-if ব্লকের জটিলতা কমে যায়। 
*/

function getGrade(val) {
  /* 
     এখানে চেক করা হচ্ছে ভ্যালু কি ২৫ এর কম? 
     যদি সত্য হয়, তবে ফাংশনটি এখানেই "D" রিটার্ন করে শেষ হয়ে যাবে। 
     নিচের আর কোনো লাইন এক্সিকিউট হবে না। 
  */
  if (val < 25) {
    return "D";
  }

  /* 
     যদি উপরের শর্তটি মিথ্যা হয়, তবে কোড এখানে আসবে। 
     এখানে চেক করা হচ্ছে ভ্যালু কি ৫০ এর কম? 
     সত্য হলে "C" রিটার্ন করবে এবং ফাংশন থেকে বের হয়ে যাবে। 
  */
  else if (val < 50) {
    return "C";
  }

  /* 
     যদি আগের সব শর্ত মিথ্যা হয়, তবে চেক করা হবে ভ্যালু কি ৭৫ এর কম? 
     সত্য হলে "B" রিটার্ন করবে। 
  */
  else if (val < 75) {
    return "B";
  }

  /* 
     যদি উপরের কোনো শর্তই (২৫, ৫০ বা ৭৫ এর কম) সত্য না হয়, 
     তার মানে ভ্যালুটি অবশ্যই ৭৫ বা তার বেশি। 
     সেক্ষেত্রে ডিফল্ট হিসেবে "A" রিটার্ন করা হচ্ছে। 
  */
  else {
    return "A";
  }
}

/* 
   এখানে getGrade ফাংশনটিকে ৮৭ ভ্যালু দিয়ে কল করা হয়েছে। 
   যেহেতু ৮৭ সংখ্যাটি ২৫, ৫০ বা ৭৫ এর চেয়ে ছোট নয়, 
   তাই ফাংশনটি শেষ পর্যন্ত "A" রিটার্ন করবে। 
*/
console.log("Grade: ", getGrade(87));

