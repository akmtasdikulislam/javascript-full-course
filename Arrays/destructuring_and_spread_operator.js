/* 
   এখানে 'arr' নামে একটি অ্যারে তৈরি করা হয়েছে যাতে ১ থেকে ৫ পর্যন্ত সংখ্যা রয়েছে।
   এই অ্যারেটি আমরা ডিস্ট্রাকচারিং (Destructuring) এবং স্প্রেড অপারেটর (Spread Operator) 
   বোঝার জন্য ব্যবহার করবো।
*/
let arr = [1, 2, 3, 4, 5];

/* 
   অ্যারে ডিস্ট্রাকচারিং (Array Destructuring):
   এটি জাভাস্ক্রিপ্টের একটি শক্তিশালী ফিচার যা অ্যারের উপাদানগুলোকে আলাদা আলাদা ভেরিয়েবলে 
   খুব সহজে অ্যাসাইন করতে সাহায্য করে।
   
   এখানে 'let [a, b, , c] = arr;' এর মাধ্যমে যা ঘটছে:
   ১. 'a' ভেরিয়েবলটি অ্যারের প্রথম উপাদান (index 0) অর্থাৎ '1' গ্রহণ করছে।
   ২. 'b' ভেরিয়েবলটি দ্বিতীয় উপাদান (index 1) অর্থাৎ '2' গ্রহণ করছে।
   ৩. এখানে একটি কমা (,) খালি রাখা হয়েছে। এর মানে হলো আমরা ৩য় উপাদানটি (index 2) স্কিপ বা বাদ দিচ্ছি।
   ৪. 'c' ভেরিয়েবলটি চতুর্থ উপাদান (index 3) অর্থাৎ '4' গ্রহণ করছে।
   
   এই পদ্ধতির সুবিধা হলো আমাদের আলাদাভাবে arr[0], arr[1] এভাবে লিখতে হয় না।
*/
let [a, b, , c] = arr;

/* 
   console.log() এর মাধ্যমে ভেরিয়েবলগুলোর মান প্রিন্ট করা হচ্ছে।
   আউটপুট হবে: 1 2 4
   লক্ষ্য করুন, ৩য় উপাদান '3' বাদ পড়েছে কারণ আমরা ডিস্ট্রাকচারিং এর সময় সেখানে স্পেস রেখেছিলাম।
*/
console.log(a, b, c);

/* 
   সামগ্রিকভাবে এই কোডটি শেখায় কীভাবে একটি অ্যারে থেকে নির্দিষ্ট কিছু মান 
   সহজেই আলাদা ভেরিয়েবলে নিয়ে আসা যায় এবং অপ্রয়োজনীয় মানগুলো স্কিপ করা যায়।
*/
let arr2 = [1, 2, 3, 4, 5, 6, 7, 8, 9, 0];

/* 
   এখানে 'arr3' ভেরিয়েবলে 'arr2' কে সরাসরি অ্যাসাইন করা হয়েছে।
   জাভাস্ক্রিপ্টে অ্যারে হলো রেফারেন্স টাইপ। এর মানে হলো 'arr3' এবং 'arr2' 
   মেমোরিতে একই জায়গাকে নির্দেশ করছে। 
   যদি আমরা 'arr3' এর কোনো মান পরিবর্তন করি, তবে 'arr2' এর মানও বদলে যাবে। 
   একে বলা হয় 'Shallow Copy' বা রেফারেন্স শেয়ারিং।
*/
let arr3 = arr2;

/* 
   স্প্রেড অপারেটর (Spread Operator - '...'):
   এখানে 'arr4' তৈরি করার সময় স্প্রেড অপারেটর ব্যবহার করা হয়েছে। 
   এটি 'arr2' এর ভেতরের প্রতিটি উপাদানকে আলাদা আলাদা করে বের করে এনে 
   একটি নতুন থার্ড ব্র্যাকেটের ভেতরে বসিয়ে দেয়।
   
   এর ফলে 'arr4' একটি সম্পূর্ণ নতুন অ্যারে হিসেবে তৈরি হয় যা 'arr2' এর 
   একটি ক্লোন বা কপি। এখন যদি আমরা 'arr4' পরিবর্তন করি, তবে 'arr2' তে 
   তার কোনো প্রভাব পড়বে না। এটি ডেটা ইমিউটেবিলিটি (Immutability) বজায় রাখতে সাহায্য করে।
*/
let arr4 = [...arr2];

/* 
   রেস্ট প্যারামিটার (Rest Parameter) এবং ডিস্ট্রাকচারিং:
   নিচে একটি উদাহরণ দেওয়া হলো যেখানে প্রথম দুটি উপাদান আলাদা ভেরিয়েবলে নেওয়া হচ্ছে 
   এবং বাকি সব উপাদানকে একটি নতুন অ্যারেতে জমা করা হচ্ছে।
*/
let [first, second, ...rest] = arr2;

/* 
   ব্যাখ্যা:
   ১. 'first' পাবে ১ (index 0)।
   ২. 'second' পাবে ২ (index 1)।
   ৩. '...rest' অংশটি বাকি সব উপাদান (৩ থেকে ০ পর্যন্ত) নিয়ে একটি নতুন অ্যারে তৈরি করবে।
   
   এটি মূলত বড় কোনো অ্যারে থেকে নির্দিষ্ট কিছু অংশ আলাদা করার জন্য খুবই কার্যকর।
*/
console.log(first);  // আউটপুট: 1
console.log(second); // আউটপুট: 2
console.log(rest);   // আউটপুট: [3, 4, 5, 6, 7, 8, 9, 0]
