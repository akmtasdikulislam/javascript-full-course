/* 
   জাভাস্ক্রিপ্টে অ্যারে (Array) হলো একটি বিশেষ ধরণের অবজেক্ট যা একাধিক মানকে একটি তালিকার মতো সংরক্ষণ করতে পারে।
   এটি মূলত একটি কন্টেইনার যেখানে আমরা বিভিন্ন ধরণের ডেটা (যেমন: সংখ্যা, স্ট্রিং, অবজেক্ট, এমনকি অন্য অ্যারে) 
   একসাথে সাজিয়ে রাখতে পারি।
*/

/* 
   নিচে আমরা শিখবো কীভাবে জাভাস্ক্রিপ্টে অ্যারে তৈরি করতে হয় এবং এর নমনীয়তা (flexibility) কতটুকু।
   প্রতিটি ধাপের ব্যাখ্যা নিচে দেওয়া হলো:
*/
/* 
   এখানে 'a' নামে একটি ভেরিয়েবল ঘোষণা করা হয়েছে এবং এতে একটি খালি অ্যারে (Empty Array) রাখা হয়েছে।
   অ্যারে তৈরি করার জন্য থার্ড ব্র্যাকেট '[]' ব্যবহার করা হয়। এটি মূলত একটি ফাঁকা তালিকা।
*/
let a = [];

/* 
   এখানে 'b' ভেরিয়েবলে একটি অ্যারে তৈরি করা হয়েছে যাতে ১ থেকে ৫ পর্যন্ত পাঁচটি সংখ্যা রয়েছে।
   এটি একটি সমজাতীয় (Homogeneous) অ্যারে যেখানে সব উপাদানই সংখ্যা (Numbers)।
*/
let b = [1, 2, 3, 4, 5];

/* 
   এখানে 'c' ভেরিয়েবলে একটি অ্যারে তৈরি করা হয়েছে যাতে সংখ্যা এবং স্ট্রিং (String) উভয়ই আছে।
   জাভাস্ক্রিপ্ট অ্যারেতে একই সাথে বিভিন্ন ধরণের ডেটা টাইপ রাখা সম্ভব।
*/
let c = [1, "a", 2, "b"];

/* 
   এখানে 'num' নামে একটি ভেরিয়েবল ঘোষণা করা হয়েছে কিন্তু কোনো মান দেওয়া হয়নি।
   ডিফল্টভাবে এর মান হবে 'undefined'। এটি পরে অ্যারের উপাদান হিসেবে ব্যবহার করা হবে।
*/
let num;

/* 
   এখানে 'obj' নামে একটি খালি অবজেক্ট (Empty Object) তৈরি করা হয়েছে।
   কার্লি ব্রেসেস '{}' ব্যবহার করে অবজেক্ট ডিক্লেয়ার করা হয়।
*/
let obj = {};

/* 
   এখানে 'd' ভেরিয়েবলে একটি জটিল অ্যারে তৈরি করা হয়েছে যা জাভাস্ক্রিপ্টের নমনীয়তা প্রকাশ করে।
   এই অ্যারেটিতে নিচের উপাদানগুলো রয়েছে:
   ১. সংখ্যা (1)
   ২. আগে তৈরি করা একটি অ্যারে (a)
   ৩. একটি স্ট্রিং ("a")
   ৪. একটি আনডিফাইন্ড ভেরিয়েবল (num)
   ৫. সরাসরি একটি খালি অবজেক্ট ({})
   ৬. আগে তৈরি করা একটি অবজেক্ট ভেরিয়েবল (obj)
   এর মাধ্যমে বোঝা যায় যে, একটি অ্যারের ভেতরে অন্য যেকোনো ডেটা টাইপ বা স্ট্রাকচার রাখা সম্ভব।
*/
let d = [1, a, "a", num, {}, obj];

/* 
   এখানে 'new Array()' কনস্ট্রাক্টর (Constructor) ব্যবহার করে একটি নতুন অ্যারে তৈরি করা হয়েছে।
   এটি অ্যারে তৈরি করার একটি বিকল্প পদ্ধতি। 
   এখানে ব্র্যাকেটের ভেতরে ১, ২, ৩ প্যারামিটার হিসেবে দেওয়া হয়েছে, 
   যার ফলে 'arr' ভেরিয়েবলটিতে [1, 2, 3] উপাদানগুলো নিয়ে একটি অ্যারে তৈরি হবে।
   সাধারণত থার্ড ব্র্যাকেট '[]' ব্যবহার করা বেশি জনপ্রিয় এবং দ্রুত, 
   তবে বিশেষ প্রয়োজনে এই কনস্ট্রাক্টর পদ্ধতিও ব্যবহার করা হয়।
*/
let arr = new Array(1, 2, 3);

/* 
   সামগ্রিকভাবে এই ফাইলটির উদ্দেশ্য হলো অ্যারে তৈরির বিভিন্ন পদ্ধতি শেখা।
   ১. খালি অ্যারে তৈরি করা।
   ২. বিভিন্ন ধরণের ডেটা (Mixed Data Types) দিয়ে অ্যারে তৈরি করা।
*/
