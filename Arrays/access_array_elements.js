/* 
   এখানে 'arr' নামে একটি ভেরিয়েবল ঘোষণা করা হয়েছে যা একটি অ্যারে (Array) ধারণ করে।
   অ্যারে হলো একটি বিশেষ ধরণের অবজেক্ট যা একাধিক মানকে একটি তালিকার মতো সাজিয়ে রাখতে পারে।
   এখানে ১ থেকে ৫ পর্যন্ত সংখ্যাগুলো এই অ্যারের উপাদান (elements) হিসেবে আছে।
*/
let arr = [1, 2, 3, 4, 5];

/* 
   console.log() ব্যবহার করে অ্যারের একটি নির্দিষ্ট উপাদান কনসোলে প্রিন্ট করা হচ্ছে।
   জাভাস্ক্রিপ্টে অ্যারের ইনডেক্সিং (indexing) শুরু হয় ০ থেকে। 
   সেই হিসেবে: arr[0]=1, arr[1]=2, এবং arr[2]=3।
   এখানে arr[2] ব্যবহার করার অর্থ হলো আমরা অ্যারের তৃতীয় উপাদানটি অ্যাক্সেস করছি।
   ফলাফল হিসেবে আউটপুটে '3' দেখা যাবে।
*/
console.log(arr[2]);

/* 
   এখানে 'arr[15]' ব্যবহার করে অ্যারের ১৬ নম্বর ইনডেক্সের মান অ্যাক্সেস করার চেষ্টা করা হচ্ছে।
   যেহেতু আমাদের 'arr' অ্যারেটিতে মাত্র ৫টি উপাদান আছে (ইনডেক্স ০ থেকে ৪ পর্যন্ত), 
   তাই ১৫ নম্বর ইনডেক্সে কোনো মান বিদ্যমান নেই।
   জাভাস্ক্রিপ্টে যদি এমন কোনো ইনডেক্স অ্যাক্সেস করার চেষ্টা করা হয় যা অ্যারেতে নেই, 
   তবে সেটি 'undefined' রিটার্ন করে। 
   এর মাধ্যমে বোঝা যায় যে মেমোরিতে ওই নির্দিষ্ট পজিশনে কোনো ভ্যালু সেট করা হয়নি।
*/
console.log(arr[15]);

