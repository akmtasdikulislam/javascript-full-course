// Q7. Use .find() to get the first number less than 10:

/* 
   এখানে 'arr' নামে একটি অ্যারে ঘোষণা করা হয়েছে যাতে ৫টি সংখ্যা রয়েছে।
   আমাদের লক্ষ্য হলো এই অ্যারে থেকে ১০-এর চেয়ে ছোট "প্রথম" সংখ্যাটি খুঁজে বের করা।
*/
let arr = [12, 15, 3, 8, 20];

/* 
   find() মেথড ব্যবহার করে অ্যারের ভেতর নির্দিষ্ট শর্ত অনুযায়ী উপাদান খোঁজা হচ্ছে:
   
   ১. find() মেথড অ্যারের প্রতিটি উপাদানের ওপর কলব্যাক ফাংশনটি চালায় যতক্ষণ না সে শর্ত পূরণকারী কোনো মান পায়।
   ২. 'val' প্যারামিটারটি বর্তমান উপাদানটিকে রিপ্রেজেন্ট করে।
   ৩. 'return val < 10' শর্তটি চেক করে। যখনই কোনো উপাদান ১০-এর চেয়ে ছোট হয়, 
      find() সাথে সাথে সেই মানটি রিটার্ন করে এবং লুপ থামিয়ে দেয়।
   
   এখানে ১২ এবং ১৫ শর্ত পূরণ করে না, কিন্তু ৩ শর্তটি পূরণ করে (৩ < ১০)। 
   যদিও ৮-ও ১০-এর চেয়ে ছোট, কিন্তু find() শুধুমাত্র প্রথম পাওয়া মানটিই (৩) রিটার্ন করবে।
   
   মনে রাখতে হবে, যদি কোনো উপাদানই শর্ত পূরণ না করে, তবে এটি 'undefined' রিটার্ন করে।
   এটি মূল অ্যারে পরিবর্তন করে না।
*/
let ans = arr.find(function (val) {
  return val < 10;
});

/* 
   console.log() এর মাধ্যমে আমরা 'ans' ভেরিয়েবলটি আউটপুট হিসেবে দেখছি।
   এটি আমাদের নিশ্চিত করবে যে ১০-এর ছোট প্রথম সংখ্যাটি (৩) সঠিকভাবে পাওয়া গেছে কি না।
*/
console.log(ans);

