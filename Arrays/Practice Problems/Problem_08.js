// Q8. Use .some() to check if any student has scored below 35:

/* 
   এখানে 'arr' নামে একটি অ্যারে ঘোষণা করা হয়েছে যাতে ৪ জন শিক্ষার্থীর প্রাপ্ত নম্বর রয়েছে।
   আমাদের লক্ষ্য হলো—অ্যারেতে এমন কোনো শিক্ষার্থী আছে কি না, যে ৩৫-এর কম নম্বর পেয়েছে তা যাচাই করা।
*/
let arr = [45, 60, 28, 90];

/* 
   some() মেথড ব্যবহার করে অ্যারের ভেতর নির্দিষ্ট শর্ত অন্তত একজন পূরণ করে কি না তা চেক করা হচ্ছে:
   
   ১. some() মেথড অ্যারের প্রতিটি উপাদানের ওপর কলব্যাক ফাংশনটি চালায়।
   ২. এটি ততক্ষণ পর্যন্ত চলে যতক্ষণ না সে শর্ত পূরণকারী (true) কোনো মান পায়। 
   ৩. যদি অন্তত একটি উপাদান শর্ত পূরণ করে, তবে এটি সাথে সাথে 'true' রিটার্ন করে। 
      যদি কোনো উপাদানই শর্ত পূরণ না করে, তবে এটি 'false' রিটার্ন করে।
   ৪. 'val' প্যারামিটারটি বর্তমান স্কোর বা উপাদানটিকে রিপ্রেজেন্ট করে।
   
   এখানে:
   - ৪৫ < ৩৫ (False)
   - ৬০ < ৩৫ (False)
   - ২৮ < ৩৫ (True) -> যেহেতু একটি মান শর্ত পূরণ করেছে, তাই some() এখানেই থেমে যাবে এবং true রিটার্ন করবে।
   
   বি.দ্র.: কলব্যাক ফাংশনের ভেতর অবশ্যই 'return' কীওয়ার্ড ব্যবহার করতে হবে মানটি যাচাই করার জন্য।
*/
let ans = arr.some(function (val) {
  return val < 35;
});

/* 
   console.log() এর মাধ্যমে আমরা 'ans' ভেরিয়েবলটি আউটপুট হিসেবে দেখছি।
   এটি আমাদের 'true' অথবা 'false' এর মাধ্যমে জানাবে যে ৩৫-এর নিচে কেউ আছে কি না।
*/
console.log(ans);

