// Q1. Insert 'Red' and 'Blue' at index 1 in the given array;

/* 
   এখানে 'colors' নামে একটি অ্যারে তৈরি করা হয়েছে যাতে প্রাথমিক অবস্থায় দুটি স্ট্রিং উপাদান 
   'Green' এবং 'Yellow' রয়েছে। 
   ইনডেক্স অনুযায়ী: Green আছে ০ নম্বর ইনডেক্সে এবং Yellow আছে ১ নম্বর ইনডেক্সে।
*/
let colors = ["Green", "Yellow"];

/* 
   splice() মেথড ব্যবহার করে অ্যারের নির্দিষ্ট অবস্থানে নতুন উপাদান যোগ করা হচ্ছে:
   ১. প্রথম প্যারামিটার (1): এটি নির্দেশ করে যে পরিবর্তনটি ১ নম্বর ইনডেক্স থেকে শুরু হবে।
   ২. দ্বিতীয় প্যারামিটার (0): এর মানে হলো আমরা ওই পজিশন থেকে কোনো বিদ্যমান উপাদান মুছে ফেলতে চাই না।
   ৩. পরবর্তী প্যারামিটারসমূহ ("Red", "Blue"): এগুলো হলো সেই নতুন মান যা আমরা ১ নম্বর ইনডেক্সে বসাতে চাই।
   
   এর ফলে 'Yellow' উপাদানটি ডানে সরে যাবে এবং তার জায়গায় 'Red' ও 'Blue' বসে যাবে।
   বর্তমান অ্যারেটি হবে: ["Green", "Red", "Blue", "Yellow"]
*/
colors.splice(1, 0, "Red", "Blue");

/* আউটপুট যাচাই করার জন্য কনসোলে প্রিন্ট করা যেতে পারে */
console.log(colors);
