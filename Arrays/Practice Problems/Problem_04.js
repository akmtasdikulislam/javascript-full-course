// Q4. Use .map() to square each number

/* 
   এখানে 'arr' নামে একটি অ্যারে ঘোষণা করা হয়েছে যাতে ১ থেকে ৪ পর্যন্ত চারটি সংখ্যা রয়েছে।
   আমাদের লক্ষ্য হলো এই অ্যারের প্রতিটি সংখ্যাকে বর্গ (square) করে একটি নতুন অ্যারে তৈরি করা।
*/
let arr = [1, 2, 3, 4];

/* 
   map() মেথড ব্যবহার করে অ্যারের প্রতিটি উপাদানের ওপর নির্দিষ্ট একটি কাজ চালানো হচ্ছে:
   
   ১. map() মেথড মূল অ্যারের প্রতিটি উপাদানের জন্য একবার করে কলব্যাক ফাংশনটি চালায়।
   ২. 'val' প্যারামিটারটি বর্তমান উপাদানটিকে (যেমন: প্রথমে ১, তারপর ২...) রিপ্রেজেন্ট করে।
   ৩. 'return val * val' এর মাধ্যমে প্রতিটি সংখ্যাকে তার নিজের সাথে গুণ করে বর্গফল বের করা হচ্ছে।
   
   ফলাফলস্বরূপ, map() একটি নতুন অ্যারে রিটার্ন করবে যেখানে মানগুলো হবে: [1, 4, 9, 16]।
   মনে রাখতে হবে, map() মূল অ্যারে (arr) পরিবর্তন করে না, বরং একটি নতুন অ্যারে তৈরি করে।
*/
let newArr = arr.map(function (val) {
  return val * val;
});

/* 
   console.log() এর মাধ্যমে আমরা 'newArr' ভেরিয়েবলটি আউটপুট হিসেবে দেখছি।
   এটি আমাদের নিশ্চিত করবে যে প্রতিটি সংখ্যা সঠিকভাবে বর্গ হয়েছে কি না।
*/
console.log(newArr);

/* 
   অতিরিক্ত নোট: 
   মূল অ্যারে 'arr' অপরিবর্তিত আছে: [1, 2, 3, 4]
   নতুন অ্যারে 'newArr' হয়েছে: [1, 4, 9, 16]
*/
