// Q13. Clone this array properly (not by reference):

/* 
   এখানে 'arr' নামে একটি প্রাথমিক অ্যারে ঘোষণা করা হয়েছে যাতে তিনটি সংখ্যা রয়েছে।
   আমাদের লক্ষ্য হলো এই অ্যারের একটি কপি বা ক্লোন তৈরি করা।
*/
let arr = [1, 2, 3];

/* 
   স্প্রেড অপারেটর (...) ব্যবহার করে অ্যারেটি ক্লোন (Clone) করা হচ্ছে:
   
   ১. জাভাস্ক্রিপ্টে যদি আমরা সরাসরি 'let arr2 = arr' লিখতাম, তবে সেটি রেফারেন্স কপি হতো। 
      অর্থাৎ, 'arr2' পরিবর্তন করলে মূল 'arr' ও পরিবর্তন হয়ে যেত।
   ২. এই সমস্যা এড়াতে আমরা স্প্রেড অপারেটর (...) ব্যবহার করছি। এটি 'arr' এর ভেতরের 
      উপাদানগুলোকে (১, ২, ৩) বের করে আনে।
   ৩. এরপর সেগুলোকে একটি নতুন থার্ড ব্র্যাকেট [] এর ভেতর রাখায় একটি সম্পূর্ণ নতুন অ্যারে তৈরি হয়।
   ৪. একে বলা হয় 'Shallow Copy'। এর ফলে 'arr2' এখন একটি স্বতন্ত্র অ্যারে, যার সাথে 
      মূল 'arr' এর মেমোরি অ্যাড্রেসের কোনো সরাসরি সংযোগ নেই।
*/
let arr2 = [...arr];

/* 
   console.log() এর মাধ্যমে আমরা নতুন তৈরি হওয়া 'arr2' অ্যারেটি আউটপুট হিসেবে দেখছি।
   এটি আমাদের নিশ্চিত করবে যে মূল অ্যারের সব উপাদান নতুন অ্যারেতে কপি হয়েছে।
*/
console.log(arr2);

