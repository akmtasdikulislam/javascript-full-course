// Use .reduce() to find the sum of this array:

/* 
   এখানে 'arr' নামে একটি অ্যারে ঘোষণা করা হয়েছে যাতে ১০, ২০ এবং ৩০ এই তিনটি সংখ্যা রয়েছে।
   আমাদের লক্ষ্য হলো এই অ্যারের সবকটি সংখ্যার যোগফল বের করা।
*/
let arr = [10, 20, 30];

/* 
   reduce() মেথড ব্যবহার করে অ্যারের সব উপাদানকে একত্রিত করে একটি একক মান (এক্ষেত্রে যোগফল) বের করা হচ্ছে:
   
   ১. reduce() মেথড দুটি প্রধান প্যারামিটার নেয়: একটি কলব্যাক ফাংশন এবং একটি প্রাথমিক মান (Initial Value)।
   ২. কলব্যাক ফাংশনের ভেতরে 'acc' (Accumulator) হলো জমানো ফলাফল এবং 'val' (Current Value) হলো বর্তমান উপাদান।
   ৩. দ্বিতীয় প্যারামিটার হিসেবে '0' দেয়া হয়েছে, যার মানে হলো যোগফল গণনা ০ থেকে শুরু হবে।
   
   ধাপে ধাপে কাজ করার প্রক্রিয়া:
   - প্রথম ধাপে: acc = 0, val = 10; যোগফল হয় ১০।
   - দ্বিতীয় ধাপে: acc = 10, val = 20; যোগফল হয় ৩০।
   - তৃতীয় ধাপে: acc = 30, val = 30; যোগফল হয় ৬০।
   
   অবশেষে, reduce() মেথড চূড়ান্ত ফলাফল ৬০ রিটার্ন করে।
*/
let sum = arr.reduce(function (acc, val) {
  return acc + val;
}, 0);

/* 
   console.log() এর মাধ্যমে আমরা 'sum' ভেরিয়েবলটি আউটপুট হিসেবে দেখছি।
   এটি আমাদের নিশ্চিত করবে যে অ্যারের সব সংখ্যার যোগফল সঠিকভাবে বের হয়েছে কি না।
*/
console.log(sum);

/* অতিরিক্ত নোট: মূল অ্যারে 'arr' অপরিবর্তিত থাকে। */
