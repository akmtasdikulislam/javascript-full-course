// Use .every() to check if all numbers are even:

/* 
   এখানে 'arr' নামে একটি অ্যারে ঘোষণা করা হয়েছে যাতে ৫টি জোড় সংখ্যা রয়েছে।
   আমাদের লক্ষ্য হলো—অ্যারের "সবগুলো" উপাদান জোড় সংখ্যা কি না তা যাচাই করা।
*/
let arr = [2, 4, 6, 8, 10];

/* 
   every() মেথড ব্যবহার করে অ্যারের প্রতিটি উপাদান একটি নির্দিষ্ট শর্ত পূরণ করে কি না তা চেক করা হচ্ছে:
   
   ১. every() মেথড অ্যারের প্রতিটি উপাদানের ওপর কলব্যাক ফাংশনটি চালায়।
   ২. এটি 'some()' মেথডের ঠিক উল্টো। এটি তখনই 'true' রিটার্ন করে যখন অ্যারের "প্রতিটি" উপাদান শর্ত পূরণ করে।
   ৩. যদি একটি উপাদানও শর্ত পূরণ না করে (false হয়), তবে every() সাথে সাথে থেমে যায় এবং 'false' রিটার্ন করে।
   ৪. 'val' প্যারামিটারটি বর্তমান সংখ্যাটিকে রিপ্রেজেন্ট করে।
   
   এখানে শর্তটি হলো: val % 2 === 0 (অর্থাৎ সংখ্যাটিকে ২ দিয়ে ভাগ করলে ভাগশেষ ০ হবে কি না)।
   - ২ % ২ === ০ (True)
   - ৪ % ২ === ০ (True)
   - ৬ % ২ === ০ (True)
   - ৮ % ২ === ০ (True)
   - ১০ % ২ === ০ (True)
   
   যেহেতু সবকটি মান শর্ত পূরণ করেছে, তাই 'ans' ভেরিয়েবলে 'true' জমা হবে।
*/
let ans = arr.every(function (val) {
  return val % 2 === 0;
});

/* 
   console.log() এর মাধ্যমে আমরা 'ans' ভেরিয়েবলটি আউটপুট হিসেবে দেখছি।
   এটি আমাদের 'true' অথবা 'false' এর মাধ্যমে জানাবে যে সবাই জোড় সংখ্যা কি না।
*/
console.log(ans);

