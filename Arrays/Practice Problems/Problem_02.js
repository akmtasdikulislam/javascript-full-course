// Q2. Extract only the middle 3 elements from this array

/* 
   এখানে 'items' নামে একটি অ্যারে ঘোষণা করা হয়েছে যাতে ১ থেকে ৬ পর্যন্ত মোট ৬টি সংখ্যা রয়েছে।
   আমাদের লক্ষ্য হলো এই অ্যারের মাঝখান থেকে ৩টি উপাদান (২, ৩, ৪) আলাদা করে নেওয়া।
*/
let items = [1, 2, 3, 4, 5, 6];

/* 
   slice() মেথড ব্যবহার করে অ্যারের একটি নির্দিষ্ট অংশ কপি করে নতুন একটি অ্যারে তৈরি করা হচ্ছে:
   ১. প্রথম প্যারামিটার (1): এটি নির্দেশ করে যে স্লাইসিং বা কাটা শুরু হবে ১ নম্বর ইনডেক্স থেকে (অর্থাৎ ২ থেকে)।
   ২. দ্বিতীয় প্যারামিটার (4): এটি হলো 'end index'। slice মেথড এই ইনডেক্সের ঠিক আগের উপাদান পর্যন্ত নেয়। 
      অর্থাৎ, এটি ১, ২ এবং ৩ নম্বর ইনডেক্সের মানগুলো নেবে কিন্তু ৪ নম্বর ইনডেক্সের মানটি নেবে না।
   
   ফলাফলস্বরূপ, আমরা ইনডেক্স ১ থেকে ৩ পর্যন্ত উপাদানগুলো পাবো: [2, 3, 4]।
   মনে রাখতে হবে, slice() মূল অ্যারে (items) পরিবর্তন করে না, বরং একটি নতুন অ্যারে রিটার্ন করে।
*/
let newArr = items.slice(1, 4);

/* 
   console.log() এর মাধ্যমে আমরা 'newArr' ভেরিয়েবলটি আউটপুট হিসেবে দেখছি।
   এটি আমাদের নিশ্চিত করবে যে আমরা সঠিকভাবে মাঝখানের ৩টি উপাদান আলাদা করতে পেরেছি কি না।
*/
console.log(newArr);

