// Q11. Merge two arrays using spread operator:

/* 
   এখানে 'a' এবং 'b' নামে দুটি আলাদা অ্যারে ঘোষণা করা হয়েছে।
   আমাদের লক্ষ্য হলো এই দুটি অ্যারের উপাদানগুলোকে একত্রিত করে একটি নতুন অ্যারে তৈরি করা।
*/
let a = [1, 2];
let b = [3, 4];

/* 
   স্প্রেড অপারেটর (...) ব্যবহার করে দুটি অ্যারেকে মার্জ বা একত্রিত করা হচ্ছে:
   
   ১. স্প্রেড অপারেটর (...) কোনো অ্যারের ভেতরের উপাদানগুলোকে "ছড়িয়ে" দেয় বা বের করে আনে।
   ২. '...a' লেখার মানে হলো অ্যারে 'a' এর উপাদানগুলো (১, ২) এখানে বসে যাবে।
   ৩. '...b' লেখার মানে হলো অ্যারে 'b' এর উপাদানগুলো (৩, ৪) এখানে বসে যাবে।
   ৪. থার্ড ব্র্যাকেট [] এর ভেতরে এদের রাখায় তারা সবাই মিলে একটি নতুন অ্যারে 'c' তৈরি করছে।
   
   এটি করার ফলে 'c' এর মান হবে: [1, 2, 3, 4]
*/
let c = [...a, ...b];

/* 
   console.log() এর মাধ্যমে আমরা নতুন তৈরি হওয়া 'c' অ্যারেটি আউটপুট হিসেবে দেখছি।
   এটি আমাদের নিশ্চিত করবে যে দুটি অ্যারের মান সঠিকভাবে একত্রিত হয়েছে কি না।
*/
console.log(c);

