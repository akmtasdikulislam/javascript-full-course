// Q12. Add 'Bangladesh' to the start of this array using spread operator

/* 
   এখানে 'countries' নামে একটি অ্যারে ঘোষণা করা হয়েছে যাতে দুটি দেশের নাম রয়েছে।
   আমাদের লক্ষ্য হলো স্প্রেড অপারেটর ব্যবহার করে এই অ্যারের শুরুতে 'Bangladesh' যোগ করা।
*/
let countries = ["USA", "UK"];

/* 
   স্প্রেড অপারেটর (...) ব্যবহার করে নতুন একটি অ্যারে তৈরি করে সেটি আবার 'countries' ভেরিয়েবলে রাখা হচ্ছে:
   
   ১. আমরা একটি নতুন অ্যারে ব্র্যাকেট [] নিচ্ছি।
   ২. প্রথমে "Bangladesh" স্ট্রিংটি লিখছি, কারণ আমরা এটিকে অ্যারের শুরুতে রাখতে চাই।
   ৩. এরপর '...countries' ব্যবহার করছি। স্প্রেড অপারেটর (...) এখানে 'countries' অ্যারের 
      ভেতরের উপাদানগুলোকে (USA, UK) বের করে এনে "Bangladesh"-এর পরে বসিয়ে দিচ্ছে।
   ৪. এর ফলে একটি নতুন অ্যারে তৈরি হচ্ছে: ["Bangladesh", "USA", "UK"]।
   
   যেহেতু আমরা 'let' দিয়ে ভেরিয়েবলটি ঘোষণা করেছি, তাই আমরা নতুন এই অ্যারেটি 
   পুরানো 'countries' ভেরিয়েবলের ওপর পুনরায় অ্যাসাইন (Re-assign) করতে পারছি।
*/
countries = ["Bangladesh", ...countries];

/* 
   console.log() এর মাধ্যমে আমরা পরিবর্তিত 'countries' অ্যারেটি আউটপুট হিসেবে দেখছি।
   এটি আমাদের নিশ্চিত করবে যে 'Bangladesh' সঠিকভাবে শুরুতে যুক্ত হয়েছে কি না।
*/
console.log(countries);

/* 
   অতিরিক্ত নোট: 
   - সাধারণত অ্যারের শুরুতে কিছু যোগ করতে unshift() মেথড ব্যবহার করা হয়, 
     কিন্তু স্প্রেড অপারেটর ব্যবহার করা আরও আধুনিক এবং এটি মূল অ্যারেকে সরাসরি পরিবর্তন না করে 
     নতুন অ্যারে তৈরির সুবিধা দেয় (Immutability)।
   - যদি আমরা শেষে যোগ করতে চাইতাম, তবে লিখতাম: [...countries, "Bangladesh"]
   
   আউটপুট হবে: ["Bangladesh", "USA", "UK"]
*/
