// Q5. Use .filter() to keep numbers greater than 10

/* 
   এখানে 'arr' নামে একটি অ্যারে ঘোষণা করা হয়েছে যাতে ৫টি সংখ্যা রয়েছে।
   আমাদের লক্ষ্য হলো এই অ্যারে থেকে শুধুমাত্র ১০-এর চেয়ে বড় সংখ্যাগুলোকে খুঁজে বের করে একটি নতুন অ্যারে তৈরি করা।
*/
let arr = [5, 12, 8, 20, 3];

/* 
   filter() মেথড ব্যবহার করে অ্যারের উপাদানগুলোকে যাচাই করা হচ্ছে:
   
   ১. filter() মেথড মূল অ্যারের প্রতিটি উপাদানের জন্য একবার করে কলব্যাক ফাংশনটি চালায়।
   ২. 'val' প্যারামিটারটি বর্তমান উপাদানটিকে রিপ্রেজেন্ট করে।
   ৩. 'return val > 10' শর্তটি চেক করে। যদি শর্তটি সত্য (true) হয়, তবে সেই উপাদানটি নতুন অ্যারেতে যুক্ত হয়।
   ৪. এখানে ১২ এবং ২০ শর্তটি পূরণ করে (১০-এর চেয়ে বড়), তাই তারা নতুন অ্যারেতে থাকবে।
   
   মনে রাখতে হবে, filter() মূল অ্যারে (arr) পরিবর্তন করে না, বরং একটি নতুন অ্যারে রিটার্ন করে।
*/
let newArr = arr.filter((val) => {
  return val > 10;
});

/* 
   console.log() এর মাধ্যমে আমরা 'newArr' ভেরিয়েবলটি আউটপুট হিসেবে দেখছি।
   এটি আমাদের নিশ্চিত করবে যে শুধুমাত্র ১০-এর বড় সংখ্যাগুলোই ফিল্টার হয়েছে কি না।
*/
console.log(newArr);

/* 
   অতিরিক্ত নোট: 
   মূল অ্যারে 'arr' অপরিবর্তিত আছে: [5, 12, 8, 20, 3]
   নতুন অ্যারে 'newArr' হয়েছে: [12, 20]
*/
