// Q10. Destructure this array to get firstName and lastName

/* 
   এখানে 'fullName' নামে একটি অ্যারে ঘোষণা করা হয়েছে যাতে দুটি স্ট্রিং উপাদান রয়েছে।
   প্রথম উপাদানটি হলো প্রথম নাম (First Name) এবং দ্বিতীয়টি হলো পদবি (Last Name)।
*/
let fullName = ["Harsh", "Sharma"];

/* 
   অ্যারে ডিস্ট্রাকচারিং (Array Destructuring) ব্যবহার করে অ্যারের উপাদানগুলোকে আলাদা ভেরিয়েবলে রাখা হচ্ছে:
   
   ১. বাম পাশে থার্ড ব্র্যাকেট [firstName, lastName] ব্যবহার করা হয়েছে। এটি জাভাস্ক্রিপ্টের একটি আধুনিক সিনট্যাক্স।
   ২. এটি 'fullName' অ্যারের ০ নম্বর ইনডেক্সের মানকে 'firstName' ভেরিয়েবলে এবং 
      ১ নম্বর ইনডেক্সের মানকে 'lastName' ভেরিয়েবলে স্বয়ংক্রিয়ভাবে অ্যাসাইন বা জমা করে দেয়।
   ৩. এর ফলে আমাদের আলাদাভাবে fullName[0] বা fullName[1] লিখতে হচ্ছে না।
*/
let [firstName, lastName] = fullName;

/* 
   console.log() এর মাধ্যমে আমরা 'firstName' এবং 'lastName' ভেরিয়েবল দুটির মান আউটপুট হিসেবে দেখছি।
   এটি আমাদের নিশ্চিত করবে যে ডিস্ট্রাকচারিং সঠিকভাবে কাজ করেছে।
*/
console.log(firstName, lastName);

