/* 
   এখানে 'arr' নামে একটি ভেরিয়েবল ঘোষণা করা হয়েছে এবং এতে ১ থেকে ৫ পর্যন্ত পাঁচটি সংখ্যা নিয়ে একটি অ্যারে তৈরি করা হয়েছে।
   জাভাস্ক্রিপ্টে অ্যারে হলো মিউটেবল (Mutable), যার অর্থ হলো অ্যারে তৈরি করার পর এর ভেতরের উপাদানগুলো পরিবর্তন করা সম্ভব।
*/
let arr = [1, 2, 3, 4, 5];

/* 
   এখানে অ্যারের একটি নির্দিষ্ট ইনডেক্সের মান পরিবর্তন করা হচ্ছে।
   আমরা জানি জাভাস্ক্রিপ্টে ইনডেক্সিং ০ থেকে শুরু হয়। 
   সেই হিসেবে:
   arr[0] = 1
   arr[1] = 2
   arr[2] = 3
   এখানে 'arr[2] = 12' লেখার মাধ্যমে আমরা অ্যারের ৩য় অবস্থানে থাকা মান '3' কে পরিবর্তন করে '12' দিয়ে রিপ্লেস করছি।
   এটিই হলো অ্যারের এলিমেন্ট মডিফাই বা আপডেট করার পদ্ধতি।
*/
arr[2] = 12;

/* 
   console.log() ব্যবহার করে সম্পূর্ণ অ্যারেটি কনসোলে প্রিন্ট করা হচ্ছে।
   যেহেতু আমরা ৩য় উপাদানটি পরিবর্তন করেছি, তাই আউটপুটে আমরা [1, 2, 12, 4, 5] দেখতে পাবো।
   এর মাধ্যমে নিশ্চিত হওয়া যায় যে অ্যারের মূল ডেটা সফলভাবে পরিবর্তিত হয়েছে।
*/
console.log(arr);

