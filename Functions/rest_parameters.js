/* 
   জাভাস্ক্রিপ্টে রেস্ট প্যারামিটার (Rest Parameters) আমাদের একটি ফাংশনে অনির্দিষ্ট সংখ্যক আর্গুমেন্টকে একটি অ্যারে (Array) হিসেবে গ্রহণ করতে সাহায্য করে।
   এটি তিনটি ডট (...) দিয়ে শুরু হয়।
*/

/* 
   ১. এখানে 'abcd' নামে একটি ফাংশন তৈরি করা হয়েছে।
   ২. প্যারামিটার হিসেবে '...val' ব্যবহার করা হয়েছে। একেই রেস্ট প্যারামিটার বলে।
   ৩. এর কাজ হলো ফাংশন কল করার সময় যতগুলোই আর্গুমেন্ট দেওয়া হোক না কেন, সেগুলোকে একটি 'val' নামক অ্যারেতে জমা করা।
*/
function abcd(...val) {
  /* 
     ৪. console.log(val) দিলে আমরা দেখতে পাবো যে সবকটি ইনপুট একটি অ্যারে হিসেবে দেখাচ্ছে।
  */
  console.log(val);
}

/* 
   ৫. এখানে ফাংশনটিকে ১০টি আলাদা আলাদা সংখ্যা দিয়ে কল করা হয়েছে।
   ৬. রেস্ট প্যারামিটারের কারণে এই ১০টি সংখ্যাই 'val' অ্যারের ভেতরে ঢুকে যাবে।
   আউটপুট: [1, 2, 3, 4, 5, 6, 7, 8, 9, 0]
*/
abcd(1, 2, 3, 4, 5, 6, 7, 8, 9, 0);

/* 
   ৭. এখানে একই নামের ফাংশনটি আবার লেখা হয়েছে (জাভাস্ক্রিপ্টে পরেরটি আগেরটিকে ওভাররাইড করে)।
   ৮. এখানে 'a, b, c' হলো সাধারণ প্যারামিটার এবং '...val' হলো রেস্ট প্যারামিটার।
   ৯. নিয়ম হলো: রেস্ট প্যারামিটার সবসময় সবার শেষে থাকতে হবে।
*/
function abcd(a, b, c, ...val) {
  /* 
     ১০. এখানে 'a' পাবে প্রথম আর্গুমেন্ট, 'b' পাবে দ্বিতীয়টি এবং 'c' পাবে তৃতীয়টি।
     ১১. বাকি যতগুলো আর্গুমেন্ট থাকবে, সব 'val' নামক অ্যারেতে জমা হবে।
  */
  console.log(a, b, c, val);
}

/* 
   ১২. এখানে ফাংশন কল করার সময়:
       - 1 যাবে a তে
       - 2 যাবে b তে
       - 3 যাবে c তে
       - বাকি (4 থেকে 0) সব যাবে val অ্যারেতে।
   আউটপুট: 1 2 3 [4, 5, 6, 7, 8, 9, 0]
*/
abcd(1, 2, 3, 4, 5, 6, 7, 8, 9, 0);

/* 
   সারসংক্ষেপ (Learning Note):
   - রেস্ট প্যারামিটার (...) ব্যবহার করে অনেকগুলো মানকে একটি সিঙ্গেল ভেরিয়েবলে (অ্যারে হিসেবে) ধরা যায়।
   - এটি ফাংশনের প্যারামিটার লিস্টের একদম শেষে বসাতে হয়।
   - যখন আমরা জানি না ইউজার ঠিক কতগুলো ইনপুট দিবে, তখন এটি খুবই কার্যকর।
*/
