/* 
   জাভাস্ক্রিপ্টে একটি ফাংশনকে তখনই হায়ার অর্ডার ফাংশন বলা হয় যখন সেটি:
   ১. অন্য একটি ফাংশনকে আর্গুমেন্ট (Argument) হিসেবে গ্রহণ করে।
   অথবা,
   ২. একটি ফাংশনকে রিটার্ন (Return) করে।
*/

/* 
   উদাহরণ ১: ফাংশনকে আর্গুমেন্ট হিসেবে গ্রহণ করা
*/

/* 
   ১. এখানে 'example_1' নামে একটি ফাংশন তৈরি করা হয়েছে।
   ২. এটি 'val' নামক একটি প্যারামিটার গ্রহণ করে। 
*/
function example_1(val) {
  /* 
     ৩. এখানে 'val()' লিখে প্যারামিটারটিকে কল করা হয়েছে। 
     ৪. এর মানে হলো 'example_1' আশা করছে যে 'val' এর ভেতরে একটি ফাংশন আসবে।
  */
  val();
}

/* 
   ৫. এখন 'example_1' ফাংশনটিকে কল করা হচ্ছে।
   ৬. আর্গুমেন্ট হিসেবে আমরা সরাসরি একটি অ্যানোনিমাস (নামহীন) ফাংশন পাঠিয়ে দিচ্ছি।
   ৭. এই পাঠানো ফাংশনটি 'val' এ গিয়ে জমা হবে এবং 'val()' হিসেবে রান হবে।
   ৮. যেহেতু 'example_1' অন্য একটি ফাংশনকে ইনপুট হিসেবে নিয়েছে, তাই এটি একটি হায়ার অর্ডার ফাংশন।
*/
example_1(function () {
  /* 
     ৯. এই মেসেজটি কনসোলে প্রিন্ট হবে যখন 'val()' এক্সিকিউট হবে।
  */
  console.log("Hello World");
});

/* 
   উদাহরণ ২: ফাংশন রিটার্ন করা
*/

/* 
   ১০. এখানে 'example_2' নামে একটি ফাংশন তৈরি করা হয়েছে।
   ১১. এই ফাংশনটি নিজে কোনো কাজ সরাসরি না করে একটি পুরো ফাংশনকে 'return' করছে।
*/
function example_2() {
  /* 
     ১২. এখানে একটি ফাংশন রিটার্ন করা হচ্ছে। 
     ১৩. যখন কেউ 'example_2()' কল করবে, সে ফলাফল হিসেবে এই নিচের ফাংশনটি ফেরত পাবে।
  */
  return function () {
    /* 
       ১৪. এই কোডটি তখনই চলবে যখন রিটার্ন করা ফাংশনটিকে আলাদাভাবে কল করা হবে।
    */
    console.log("Hello World");
  };
}

/* 
   ১৫. এখানে 'example_2()()' ব্যবহার করা হয়েছে।
   ১৬. প্রথম 'example_2()' কল করার ফলে আমরা ভেতর থেকে একটি ফাংশন ফেরত পাই।
   ১৭. দ্বিতীয় বন্ধনী '()' সেই ফেরত আসা ফাংশনটিকে সাথে সাথে কল বা এক্সিকিউট করে দেয়।
   ১৮. ফলে কনসোলে "Hello World" দেখা যায়।
*/
example_2()();
