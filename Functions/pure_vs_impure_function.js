/* 
   জাভাস্ক্রিপ্টে পিওর (Pure) এবং ইমপিওর (Impure) ফাংশন বোঝা খুবই গুরুত্বপূর্ণ। 
   এটি কোডের প্রেডিক্টেবিলিটি এবং বাগ (Bug) কমাতে সাহায্য করে।
*/

/* 
   ১. এখানে 'a' নামে একটি গ্লোবাল ভেরিয়েবল ঘোষণা করা হয়েছে এবং এর মান ১২ দেওয়া হয়েছে।
   ২. এটি ফাংশনের বাইরে অবস্থিত, তাই যেকোনো ফাংশন একে অ্যাক্সেস করতে পারবে।
*/
let a = 12;

/* 
   উদাহরণ ১: ইমপিওর ফাংশন (Impure Function)
   
   ৩. 'abcd' ফাংশনটি একটি ইমপিওর ফাংশন। 
   ৪. কেন এটি ইমপিওর? কারণ এটি তার স্কোপের বাইরের একটি ভেরিয়েবল 'a' এর উপর নির্ভর করছে।
   ৫. যদি বাইরের 'a' এর মান পরিবর্তন হয়, তবে একই ফাংশন কল করলে ভিন্ন ভিন্ন আউটপুট আসতে পারে।
   ৬. এছাড়া console.log ব্যবহার করাও একটি 'Side Effect', যা ফাংশনটিকে ইমপিওর করে তোলে।
*/
function abcd() {
  /* 
     ৭. এখানে বাইরের ভেরিয়েবল 'a' কে কনসোলে প্রিন্ট করা হচ্ছে।
  */
  console.log(a);
}

/* 
   উদাহরণ ২: ইমপিওর ফাংশন (Side Effect সহ)
   
   ৮. 'abc' ফাংশনটিও একটি ইমপিওর ফাংশন।
   ৯. এটি বাইরের ভেরিয়েবল 'a' এর মান সরাসরি পরিবর্তন (Modify) করে দিচ্ছে।
   ১০. পিওর ফাংশনের নিয়ম হলো সে বাইরের কোনো কিছু পরিবর্তন করবে না এবং 
       একই ইনপুটের জন্য সবসময় একই আউটপুট দিবে। 
   ১১. যেহেতু এটি বাইরের স্টেট পরিবর্তন করছে, তাই এটি একটি 'Side Effect' তৈরি করছে।
*/
function abc() {
  /* 
     ১২. এখানে 'a++' এর মাধ্যমে গ্লোবাল ভেরিয়েবল 'a' এর মান ১ বাড়িয়ে দেওয়া হচ্ছে।
  */
  a++;
}

/* 
   পিওর ফাংশন (Pure Function) কেমন হওয়া উচিত? (অতিরিক্ত উদাহরণ):
   
   function add(x, y) {
     return x + y;
   }
   
   ১৩. উপরের 'add' ফাংশনটি একটি পিওর ফাংশন কারণ:
       - এটি শুধু তার প্যারামিটার (x, y) এর উপর নির্ভর করে।
       - এটি বাইরের কোনো ভেরিয়েবল পরিবর্তন করে না।
       - add(2, 3) কল করলে এটি সবসময় ৫-ই রিটার্ন করবে।
*/

/* 
   সারসংক্ষেপ (Learning Note):
   - পিওর ফাংশন: একই ইনপুটে সবসময় একই আউটপুট দেয় এবং কোনো সাইড ইফেক্ট থাকে না।
   - ইমপিওর ফাংশন: বাইরের ভেরিয়েবলের ওপর নির্ভর করে বা বাইরের ভ্যালু পরিবর্তন করে।
   - গ্লোবাল ভেরিয়েবল ব্যবহার বা পরিবর্তন করা ফাংশনকে ইমপিওর করে তোলে।
   - রিয়্যাক্ট (React) বা ফাংশনাল প্রোগ্রামিংয়ে পিওর ফাংশন ব্যবহার করাকে উৎসাহিত করা হয়।
*/

abcd(); // ১২ প্রিন্ট করবে
abc(); // 'a' এর মান ১৩ করে দিবে
