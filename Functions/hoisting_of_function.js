/* 
   ৫. এখানে একটি অ্যানোনিমাস ফাংশনকে 'greetWithExpression' নামক ভেরিয়েবলে রাখা হয়েছে। 
   এটি একটি ফাংশন এক্সপ্রেশন।
*/
let greetWithExpression = function () {
  /* 
     ৬. এই মেসেজটি তখনই প্রিন্ট হবে যখন ফাংশনটি ডিক্লেয়ার করার পর কল করা হবে।
  */
  console.log("Hello World");
};

/* 
   ৭. এখন ফাংশনটি ডিক্লেয়ার করা শেষ, তাই এখন কল করলে এটি সঠিকভাবে কাজ করবে।
*/
greetWithExpression();

/* 
   সারসংক্ষেপ (Learning Note):
   - ফাংশন ডিক্লারেশন (Function Declaration) পুরোপুরি হোইস্ট হয়, তাই একে 
     তৈরি করার আগেও কল করা যায়।
   - ফাংশন এক্সপ্রেশন (Function Expression) ভেরিয়েবলের মতো আচরণ করে। 
     যদি 'var' দিয়ে করা হয় তবে সেটি 'undefined' থাকে, আর 'let/const' দিয়ে 
     করলে সেটি ডিক্লেয়ার করার আগে ব্যবহার করা যায় না (Error দেয়)।
   - কোড ক্লিন রাখতে এবং বাগ এড়াতে সবসময় ফাংশন ডিক্লেয়ার করার পর কল করা ভালো।
*/
