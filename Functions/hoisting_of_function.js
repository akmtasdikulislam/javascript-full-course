/* 
   জাভাস্ক্রিপ্টে হোইস্টিং (Hoisting) হলো এমন একটি প্রক্রিয়া যেখানে ডিক্লারেশনগুলোকে 
   কোড এক্সিকিউট হওয়ার আগে মেমোরির ওপরের দিকে নিয়ে যাওয়া হয়। 
   ফাংশন ডিক্লারেশন এবং ফাংশন এক্সপ্রেশনের ক্ষেত্রে হোইস্টিং ভিন্নভাবে কাজ করে।
*/

/* 
   ১. ফাংশন ডিক্লারেশনের হোইস্টিং (Hoisting of Function Declaration):
   এখানে 'greetWithDeclaration' ফাংশনটি তৈরি করার আগেই কল করা হয়েছে। 
   জাভাস্ক্রিপ্ট ইঞ্জিন পুরো ফাংশন ডিক্লারেশনটিকে হোইস্ট করে উপরে নিয়ে যায়, 
   তাই এটি কোনো এরর ছাড়াই "Hello World" প্রিন্ট করবে।
*/
greetWithDeclaration();

/* 
   ২. এখানে একটি সাধারণ ফাংশন ডিক্লারেশন করা হয়েছে। 
   যেহেতু এটি একটি ডিক্লারেশন, তাই এটি কোডের যেকোনো জায়গা থেকে কল করা সম্ভব।
*/
function greetWithDeclaration() {
  /* 
     ৩. ফাংশনটি সফলভাবে চললে কনসোলে এই মেসেজটি দেখাবে।
  */
  console.log("Hello World");
}

/* 
   ৪. ফাংশন এক্সপ্রেশনের হোইস্টিং (Hoisting of Function Expression):
   এখানে 'greetWithExpression' ফাংশনটি কল করার চেষ্টা করা হচ্ছে। 
   কিন্তু এটি একটি 'let' ভেরিয়েবলে রাখা ফাংশন এক্সপ্রেশন। 
   জাভাস্ক্রিপ্টে 'let' বা 'const' দিয়ে তৈরি ভেরিয়েবল হোইস্ট হলেও সেগুলো 
   'Temporal Dead Zone' এ থাকে, তাই ডিক্লেয়ার করার আগে এদের এক্সেস করা যায় না।
   
   নোট: নিচের লাইনটি আনকমেন্ট করলে "ReferenceError" দিবে।
*/
