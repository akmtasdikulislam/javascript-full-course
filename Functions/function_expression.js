/* 
   জাভাস্ক্রিপ্টে ফাংশন এক্সপ্রেশন (Function Expression) সম্পর্কে শিখি।
   ফাংশন এক্সপ্রেশন হলো এমন একটি পদ্ধতি যেখানে একটি ফাংশনকে একটি ভেরিয়েবলের (Variable) মান হিসেবে রাখা হয়।
*/

/* 
   ১. এখানে 'let' কিওয়ার্ড ব্যবহার করে 'fnc' নামে একটি ভেরিয়েবল ঘোষণা করা হয়েছে।
   ২. সমান চিহ্নের (=) ডান পাশে একটি 'function' তৈরি করা হয়েছে যার কোনো নাম নেই। একে 'Anonymous Function' বলা হয়।
   ৩. এই পুরো ফাংশনটিকে 'fnc' ভেরিয়েবলের ভেতরে স্টোর বা জমা করে রাখা হয়েছে।
   ৪. ফাংশন এক্সপ্রেশনের সুবিধা হলো এটি কোডের লজিককে ভেরিয়েবলের মতো পাস করতে সাহায্য করে।
*/
let fnc = function () {
  /* 
     ৫. ফাংশনের ভেতরে console.log ব্যবহার করা হয়েছে। 
     যখনই এই ভেরিয়েবলটিকে ফাংশন হিসেবে কল করা হবে, তখন এটি "Hello world" প্রিন্ট করবে।
  */
  console.log("Hello world");
  /* 
     ৬. কার্লি ব্র্যাকেট } এবং সেমিকোলন ; দিয়ে ফাংশন এক্সপ্রেশনটি শেষ করা হয়েছে।
  */
};

/* 
   ৭. যেহেতু ফাংশনটি 'fnc' ভেরিয়েবলে আছে, তাই একে চালানোর জন্য ভেরিয়েবলের নামের সাথে প্রথম বন্ধনী 'fnc()' ব্যবহার করতে হয়।
   ৮. একেই বলা হয় ফাংশন কল করা বা ইনভোক করা।
*/
fnc();

