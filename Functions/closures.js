/* 
   জাভাস্ক্রিপ্টে ক্লোজার (Closure) একটি অত্যন্ত শক্তিশালী এবং গুরুত্বপূর্ণ কনসেপ্ট। 
   যখন একটি ফাংশন অন্য একটি ফাংশনের ভেতর থেকে রিটার্ন হয়, তখন সে তার বাইরের ফাংশনের 
   ভেরিয়েবলগুলোর এক্সেস বা রেফারেন্স সাথে নিয়ে বের হয়—একেকই ক্লোজার বলে।
*/

/* 
   ১. এখানে 'abcd' নামে একটি প্যারেন্ট ফাংশন তৈরি করা হয়েছে।
*/
function abcd() {
  /* 
     ২. ফাংশনের ভেতরে 'a' নামে একটি লোকাল ভেরিয়েবল ঘোষণা করা হয়েছে এবং মান ১২ দেওয়া হয়েছে।
     ৩. সাধারণ নিয়ম অনুযায়ী, ফাংশনটি শেষ হয়ে গেলে এই ভেরিয়েবলটি মেমোরি থেকে মুছে যাওয়ার কথা।
  */
  let a = 12;

  /* 
     ৪. এখানে একটি অ্যানোনিমাস (নামহীন) ফাংশন 'return' করা হচ্ছে।
     ৫. এই রিটার্ন হওয়া ফাংশনটি তার বাইরের স্কোপের ভেরিয়েবল 'a' কে ব্যবহার করছে।
     ৬. ক্লোজারের কারণে, এই ফাংশনটি যখন রিটার্ন হয়ে বাইরে যাবে, সে 'a' এর মানটি মনে রাখবে।
  */
  return function () {
    /* 
       ৭. এখানে বাইরের ভেরিয়েবল 'a' কে কনসোলে প্রিন্ট করা হচ্ছে।
    */
    console.log(a);
  };
}

/* 
   ৮. 'abcd()' কল করার ফলে আমরা ভেতর থেকে একটি ফাংশন ফেরত পাই, যা 'ans' ভেরিয়েবলে রাখা হলো।
   ৯. এখন 'ans()' কল করলে সেটি ১২ প্রিন্ট করবে, যদিও 'abcd' ফাংশনটি অনেক আগেই শেষ হয়ে গেছে।
*/
let ans = abcd();
ans();

