/* 
   ১. 'function' কিওয়ার্ডটি ব্যবহার করা হয়েছে জাভাস্ক্রিপ্টকে নির্দেশ দেওয়ার জন্য যে আমরা একটি নতুন ফাংশন তৈরি করতে চাই।
   ২. 'abcd' হলো এই ফাংশনটির নাম। এই নাম ব্যবহার করেই আমরা ভবিষ্যতে কোডের যেকোনো জায়গা থেকে এই ফাংশনটিকে কল করতে পারবো।
   ৩. ফাংশন নামের ঠিক পরেই প্রথম বন্ধনী () ব্যবহার করা হয়েছে। এর ভেতরে আমরা চাইলে প্যারামিটার (ইনপুট) নিতে পারি, তবে এখানে এটি খালি রাখা হয়েছে।
   ৪. এরপর দ্বিতীয় বন্ধনী {} বা কার্লি ব্র্যাকেট শুরু করা হয়েছে। এর ভেতরেই ফাংশনের সমস্ত কাজ বা লজিক লেখা হয়, যাকে 'ফাংশন বডি' বলা হয়।
*/
function abcd() {
  /* 
     ৫. ফাংশনের ভেতরে console.log ব্যবহার করা হয়েছে। 
     এর উদ্দেশ্য হলো যখনই এই ফাংশনটি চালানো হবে, তখন আউটপুট হিসেবে কনসোলে "Hello World" লেখাটি দেখাবে।
     এটিই এই ফাংশনটির মূল কাজ।
  */
  console.log("Hello World");
  /* 
     ৬. এখানে দ্বিতীয় বন্ধনী } দিয়ে ফাংশনটির বডি শেষ করা হয়েছে। 
     এর মানে হলো ফাংশনটির কাজের পরিধি এখানেই শেষ।
  */
}

/* 
   ৭. উপরে আমরা শুধু ফাংশনটি তৈরি (Declare) করেছি। কিন্তু জাভাস্ক্রিপ্টে কোনো ফাংশন নিজে থেকে কাজ শুরু করে না।
   ৮. ফাংশনটিকে কার্যকর করার জন্য বা এর ভেতরের কোডগুলো চালানোর জন্য তাকে 'কল' (Call) বা 'ইনভোক' (Invoke) করতে হয়।
   ৯. তাই 'abcd()' লিখে ফাংশনটিকে ডাকা হয়েছে, যাতে এটি তার ভেতরের কাজগুলো সম্পন্ন করে।
*/
abcd();

/* 
   সারসংক্ষেপ (Learning Note):
   - প্রথমে 'function' কিওয়ার্ড দিয়ে শুরু করতে হয়।
   - তারপর একটি অর্থবহ নাম দিতে হয়।
   - বন্ধনীর ভেতর কোড বা ইনস্ট্রাকশন লিখতে হয়।
   - সবশেষে নামের সাথে প্রথম বন্ধনী দিয়ে ফাংশনটিকে কল করতে হয় যাতে কোডটি রান হয়।
*/
