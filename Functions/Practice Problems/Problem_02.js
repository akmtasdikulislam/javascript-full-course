// Q2. Write a BMI calculator

/* 
   এখানে একটি BMI (Body Mass Index) ক্যালকুলেটর তৈরি করা হচ্ছে। 
   BMI এর মাধ্যমে বোঝা যায় একজন মানুষের ওজন তার উচ্চতা অনুযায়ী ঠিক আছে কি না।
*/

function bmi(weight, height) {
  /* 
     bmi ফাংশনটি দুটি প্যারামিটার গ্রহণ করে: weight (ওজন - কেজিতে) এবং height (উচ্চতা - মিটারে)।
     BMI বের করার সূত্র হলো: ওজন / (উচ্চতা * উচ্চতা)।
     এখানে return কীওয়ার্ড ব্যবহার করা হয়েছে যাতে ক্যালকুলেশন শেষে ফলাফলটি ফাংশনের বাইরে পাঠানো যায়।
  */
  return weight / (height * height);
}

/* 
   console.log এর মাধ্যমে আমরা ফাংশনটিকে কল করছি এবং আউটপুট দেখছি।
   এখানে weight হিসেবে 69 এবং height হিসেবে 1.7 পাঠানো হয়েছে।
*/
console.log(bmi(69, 1.7).toFixed(2));

