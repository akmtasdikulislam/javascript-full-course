// Q3. Create a reusable discount calculator (using HOF)

/* 
   এখানে একটি Higher Order Function (HOF) তৈরি করা হয়েছে। 
   HOF এমন একটি ফাংশন যা অন্য একটি ফাংশনকে রিটার্ন করে অথবা প্যারামিটার হিসেবে গ্রহণ করে।
*/
function discountCalculator(discount) {
  /* 
     এই ফাংশনটি 'discount' (শতাংশ) প্যারামিটার হিসেবে নেয়। 
     এটি একটি নতুন অ্যানোনিমাস ফাংশন রিটার্ন করে যা পরবর্তীতে যেকোনো প্রাইসের ওপর 
     এই নির্দিষ্ট ডিসকাউন্টটি প্রয়োগ করতে পারবে। একে 'Closure' ও বলা হয়।
  */
  return function (price) {
    /* 
       ভিতরের এই ফাংশনটি 'price' গ্রহণ করে এবং ডিসকাউন্ট বাদ দিয়ে চূড়ান্ত দাম হিসাব করে।
       সূত্র: আসল দাম - (আসল দাম * ডিসকাউন্ট হার / ১০০)
    */
    return price - price * (discount / 100);
  };
}

/* 
   এখানে discountCalculator(10) কল করার মাধ্যমে আমরা ১০% ডিসকাউন্টের জন্য 
   একটি নির্দিষ্ট ফাংশন তৈরি করে 'discounter' ভেরিয়েবলে রাখছি। 
   এখন 'discounter' নিজেই একটি ফাংশন হিসেবে কাজ করবে।
*/
let discounter = discountCalculator(10);

/* 
   এখন আমরা 'discounter' ফাংশনটি ব্যবহার করে ২০০ টাকার ওপর ১০% ডিসকাউন্ট হিসাব করছি।
   ফলাফল আসবে: ২০০ - (২০০ * ০.১) = ১৮০।
*/
console.log(discounter(200));

