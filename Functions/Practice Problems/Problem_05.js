/* 
   Q5. Create a pure function to transform a value
   
   পিওর ফাংশন (Pure Function) হলো এমন একটি ফাংশন যা:
   ১. একই ইনপুটের জন্য সবসময় একই আউটপুট দেয়।
   ২. এর বাইরে কোনো সাইড ইফেক্ট (Side Effect) থাকে না (যেমন বাইরের কোনো ভেরিয়েবল পরিবর্তন করে না)।
*/

function double(val) {
  /* 
     এই ফাংশনটি 'val' নামে একটি প্যারামিটার গ্রহণ করে। 
     এটি একটি পিওর ফাংশন কারণ এটি শুধুমাত্র তার ইনপুটের ওপর নির্ভর করে কাজ করছে।
  */

  /* 
     এখানে ইনপুট ভ্যালুটিকে ২ দিয়ে গুণ করে রিটার্ন করা হচ্ছে। 
     এটি ডাটা ট্রান্সফরমেশনের একটি সহজ উদাহরণ যেখানে আমরা ইনপুটকে দ্বিগুণ করছি।
  */
  return val * 2;
}

/* 
   console.log এর মাধ্যমে আমরা double ফাংশনটিকে ১০ আর্গুমেন্ট দিয়ে কল করছি। 
   যেহেতু ১০ দেওয়া হয়েছে, তাই এটি ১০ * ২ = ২০ আউটপুট দিবে।
*/
console.log(double(10));

/* 
   সারসংক্ষেপ: 
   ১. পিওর ফাংশন কোডকে প্রেডিক্টেবল (Predictable) করে তোলে, অর্থাৎ আপনি নিশ্চিতভাবে জানেন কি আউটপুট আসবে।
   ২. এটি গ্লোবাল স্টেট বা বাইরের কোনো ভেরিয়েবলকে স্পর্শ করে না, তাই বাগ (Bug) হওয়ার সম্ভাবনা কম থাকে।
   ৩. ফাংশনাল প্রোগ্রামিংয়ে পিওর ফাংশন অত্যন্ত গুরুত্বপূর্ণ।
*/

