// Q6. Use IIFE to isolate variables (ভেরিয়েবল আলাদা রাখার জন্য IIFE এর ব্যবহার)

/* 
   IIFE এর পূর্ণরূপ হলো Immediately Invoked Function Expression। 
   এটি এমন একটি ফাংশন যা তৈরি করার সাথে সাথেই নিজে নিজে কল বা এক্সিকিউট হয়ে যায়।
   এর প্রধান উদ্দেশ্য হলো একটি লোকাল স্কোপ তৈরি করা যাতে এর ভিতরের ভেরিয়েবলগুলো 
   বাইরের কোডের সাথে মিশে না যায় বা গ্লোবাল স্কোপকে দূষিত না করে।
*/

(function () {
  /* 
     এখানে একটি অ্যানোনিমাস (নামহীন) ফাংশনকে প্রথম বন্ধনী () দিয়ে ঘিরে দেওয়া হয়েছে, 
     যা একে একটি এক্সপ্রেশনে রূপান্তর করে। শেষে আরেকটি () দিয়ে একে তাৎক্ষণিকভাবে কল করা হয়েছে।
  */

  /* 
     'password' ভেরিয়েবলটি এই IIFE ফাংশনের ভেতরে ডিক্লেয়ার করা হয়েছে। 
     এটি একটি লোকাল ভেরিয়েবল, যার ফলে এটি শুধুমাত্র এই ফাংশনের ভেতরেই দৃশ্যমান।
     একে 'Data Privacy' বা 'Encapsulation' বলা হয়।
  */
  const password = "secret_password";

  /* 
     যেহেতু আমরা ফাংশনের ভেতরেই আছি, তাই এখানে 'password' ভেরিয়েবলটি অ্যাক্সেস করা সম্ভব।
  */
  console.log(password);
})();

/* 
   নিচের লাইনটি একটি এরর (Error) তৈরি করবে। 
   কারণ 'password' ভেরিয়েবলটি IIFE-এর ভেতরে 'const' দিয়ে ডিফাইন করা হয়েছিল, 
   তাই এটি ফাংশনের বাইরের স্কোপ থেকে পাওয়া সম্ভব নয়। 
   এটি প্রমাণ করে যে IIFE সফলভাবে ভেরিয়েবলটিকে আইসোলেট বা আলাদা করে রেখেছে।
*/
console.log(password);

/* 
   সারসংক্ষেপ: 
   ১. IIFE ব্যবহার করলে গ্লোবাল নেমস্পেস (Global Namespace) পরিষ্কার থাকে।
   ২. বড় প্রজেক্টে বিভিন্ন স্ক্রিপ্টের মধ্যে ভেরিয়েবলের নামের সংঘর্ষ (Naming Collision) এড়াতে এটি ব্যবহৃত হয়।
   ৩. এটি কোডের সিকিউরিটি এবং প্রাইভেসি বজায় রাখতে সাহায্য করে।
*/

