/* 
   Q4. Create a Counter function using Closure.
   
   এখানে ক্লোজার (Closure) এর একটি চমৎকার উদাহরণ দেখানো হয়েছে। 
   ক্লোজার হলো এমন একটি বৈশিষ্ট্য যেখানে একটি ফাংশন তার বাইরের স্কোপের ভেরিয়েবলকে মনে রাখতে পারে।
*/

function counter() {
  /* 
     'count' ভেরিয়েবলটি counter ফাংশনের লোকাল স্কোপে আছে। 
     বাইরে থেকে সরাসরি একে অ্যাক্সেস বা পরিবর্তন করা সম্ভব নয় (Data Privacy)।
  */
  let count = 0;

  /* 
     counter ফাংশনটি একটি অ্যানোনিমাস ফাংশন রিটার্ন করছে। 
     এই ভিতরের ফাংশনটি 'count' ভেরিয়েবলটিকে অ্যাক্সেস করতে পারে কারণ এটি তার প্যারেন্ট স্কোপে আছে।
  */
  return function () {
    /* 
       যতবার এই রিটার্ন করা ফাংশনটি কল করা হবে, ততবার এটি 'count' এর মান ১ করে বাড়িয়ে দিবে।
    */
    count++;
    /* 
       আপডেট হওয়া বর্তমান কাউন্টের মানটি রিটার্ন করা হচ্ছে।
    */
    return count;
  };
}

/* 
   এখানে counter() কল করার মাধ্যমে আমরা একটি নতুন ফাংশন এবং তার নিজস্ব 'count' ভেরিয়েবল মেমরিতে তৈরি করছি।
   একে 'c' ভেরিয়েবলে স্টোর করা হয়েছে। এখন 'c' একটি ফাংশন হিসেবে কাজ করবে।
*/
let c = counter();

/* 
   'c' ফাংশনটি যতবার কল করা হচ্ছে, এটি তার নিজস্ব মেমরিতে থাকা 'count' এর মান মনে রাখছে এবং বাড়াচ্ছে।
*/
console.log(c()); // আউটপুট: 1
console.log(c()); // আউটপুট: 2
console.log(c()); // আউটপুট: 3
console.log(c()); // আউটপুট: 4
console.log(c()); // আউটপুট: 5

/* 
   এখানে 'cnt' নামে আরেকটি নতুন ভেরিয়েবলে counter() কল করা হয়েছে। 
   এটি সম্পূর্ণ নতুন একটি ক্লোজার তৈরি করবে এবং এর 'count' ভেরিয়েবলটি 'c' এর থেকে আলাদা হবে।
*/
let cnt = counter();

/* 
   যেহেতু 'cnt' একটি নতুন ইনস্ট্যান্স, তাই এটি আবার ১ থেকে গণনা শুরু করবে। 
   এটি প্রমাণ করে যে ক্লোজার প্রতিটি কলের জন্য আলাদা আলাদা স্টেট (State) বজায় রাখতে পারে।
*/
console.log(cnt()); // আউটপুট: 1
console.log(cnt()); // আউটপুট: 2
console.log(cnt()); // আউটপুট: 3
console.log(cnt()); // আউটপুট: 4
console.log(cnt()); // আউটপুট: 5

/* 
   সারসংক্ষেপ: 
   ১. counter() ফাংশনটি একটি ফাংশন রিটার্ন করে।
   ২. রিটার্ন করা ফাংশনটি 'count' ভেরিয়েবলটিকে "মনে রাখে" (Closure)।
   ৩. আলাদা আলাদা ভেরিয়েবলে (c, cnt) রাখলে তারা আলাদা আলাদা ভাবে মান গণনা করে।
*/
