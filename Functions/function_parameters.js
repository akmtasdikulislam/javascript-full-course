/* 
   জাভাস্ক্রিপ্টে ফাংশন প্যারামিটার (Function Parameters) সম্পর্কে শিখি।
   প্যারামিটার হলো এমন কিছু ভেরিয়েবল যা ফাংশন তৈরি করার সময় ব্র্যাকেটের ভেতর লেখা হয়, 
   যাতে ফাংশনটি কল করার সময় বাইরে থেকে ডেটা বা মান গ্রহণ করতে পারে।
*/

/* 
   ১. এখানে 'welcome' নামে একটি ফাংশন ডিক্লেয়ার করা হয়েছে।
   ২. প্রথম বন্ধনীর ভেতর 'name' হলো একটি প্যারামিটার। এটি একটি খালি জায়গার মতো কাজ করে, 
      যেখানে ফাংশন কল করার সময় পাঠানো মানটি এসে জমা হবে।
   ৩. ফাংশন বডির ভেতরে ব্যাকটিক (``) এবং টেম্পলেট লিটারেল (${name}) ব্যবহার করা হয়েছে 
      যাতে ডাইনামিকভাবে যেকোনো নাম প্রিন্ট করা যায়।
*/
function welcome(name) {
  /* 
     ৪. console.log এর মাধ্যমে আউটপুট দেখানো হচ্ছে। 
     যদি আমরা welcome("Rahim") কল করি, তবে 'name' এর জায়গায় "Rahim" বসে যাবে।
  */
  console.log(`Welcome, ${name}`);
}

/* 
   ৫. এখানে 'add' নামে আরেকটি ফাংশন তৈরি করা হয়েছে যা দুটি প্যারামিটার (num1, num2) গ্রহণ করে।
   ৬. একাধিক প্যারামিটার থাকলে তাদের কমা (,) দিয়ে আলাদা করতে হয়।
   ৭. এই ফাংশনটির উদ্দেশ্য হলো দুটি সংখ্যার যোগফল বের করা।
*/
function add(num1, num2) {
  /* 
     ৮. এখানে num1 এবং num2 এর যোগফল কনসোলে প্রিন্ট করা হচ্ছে।
     ৯. যখন এই ফাংশনটি কল করা হবে, তখন প্রথম মানটি num1 এ এবং দ্বিতীয় মানটি num2 এ যাবে।
  */
  console.log(num1 + num2);
}

/* 
   ১০. ফাংশন কল করার উদাহরণ:
   welcome("Alice"); // আউটপুট: Welcome, Alice
   add(10, 20);      // আউটপুট: 30
*/

/* 
   সারসংক্ষেপ (Learning Note):
   - প্যারামিটার (Parameter) হলো ফাংশনের সংজ্ঞায় ব্যবহৃত ভেরিয়েবল।
   - আর্গুমেন্ট (Argument) হলো সেই প্রকৃত মান যা ফাংশন কল করার সময় পাঠানো হয়।
   - প্যারামিটার ব্যবহারের ফলে একটি ফাংশন অনেক বেশি ডাইনামিক এবং পুনরায় ব্যবহারযোগ্য (Reusable) হয়।
   - যদি কোনো প্যারামিটারের মান না পাঠানো হয়, তবে সেটি ডিফল্টভাবে 'undefined' থাকে।
*/
