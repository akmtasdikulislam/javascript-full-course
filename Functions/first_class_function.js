/* 
   জাভাস্ক্রিপ্টে 'ফার্স্ট ক্লাস ফাংশন' (First-Class Function) একটি অত্যন্ত শক্তিশালী ধারণা। 
   এর মানে হলো জাভাস্ক্রিপ্টে ফাংশনকে একটি সাধারণ ভেরিয়েবলের মতোই ব্যবহার করা যায়। 
   অর্থাৎ, একটি ফাংশনকে ভেরিয়েবলে রাখা যায়, অন্য ফাংশনের আর্গুমেন্ট হিসেবে পাঠানো যায় 
   এবং অন্য ফাংশন থেকে রিটার্নও করা যায়।
*/

/* 
   উদাহরণ ১: ফাংশনকে ভেরিয়েবলে রাখা (Assigning a function to a variable)
*/

/* 
   ১. এখানে 'abc' নামে একটি ভেরিয়েবল ঘোষণা করা হয়েছে।
   ২. একটি অ্যানোনিমাস ফাংশন (নামহীন ফাংশন) তৈরি করে সেটি 'abc' ভেরিয়েবলের মান হিসেবে রাখা হয়েছে।
   ৩. এটি প্রমাণ করে যে জাভাস্ক্রিপ্টে ফাংশন একটি ভ্যালু বা ডেটার মতো কাজ করতে পারে।
*/
let abc = function () {
  /* 
     ৪. ফাংশনটি কল করা হলে এটি কনসোলে "Hello World" প্রিন্ট করবে।
  */
  console.log("Hello World");
};

/* 
   উদাহরণ ২: ফাংশনকে আর্গুমেন্ট হিসেবে পাঠানো (Passing a function as an argument)
*/

/* 
   ৫. এখানে 'abcd' নামে একটি ফাংশন তৈরি করা হয়েছে যা 'val' নামক একটি প্যারামিটার গ্রহণ করে।
   ৬. জাভাস্ক্রিপ্টে আমরা জানি না 'val' এর ভেতরে কী আসবে (নাম্বার, স্ট্রিং নাকি ফাংশন)।
*/
function abcd(val) {
  /* 
     ৭. এখানে 'val()' লিখে প্যারামিটারটিকে একটি ফাংশন হিসেবে কল করা হয়েছে। 
     ৮. এর মানে হলো 'abcd' ফাংশনটি আশা করছে যে 'val' এর ভেতরে একটি ফাংশন পাঠানো হবে।
  */
  val();
}

/* 
   ৯. এখন 'abcd' ফাংশনটিকে কল করা হচ্ছে।
   ১০. আর্গুমেন্ট হিসেবে আমরা কোনো সাধারণ সংখ্যা বা স্ট্রিং না পাঠিয়ে সরাসরি একটি পুরো ফাংশন পাঠিয়ে দিচ্ছি।
   ১১. এই পাঠানো ফাংশনটি 'abcd' এর ভেতরে 'val' প্যারামিটারে গিয়ে জমা হবে এবং সেখানে 'val()' হিসেবে রান হবে।
*/
abcd(function () {
  /* 
     ১২. এই কোডটি 'abcd' ফাংশনের ভেতর থেকে এক্সিকিউট হবে।
  */
  console.log("Hello World");
});

/* 
   সারসংক্ষেপ (Learning Note):
   - ফার্স্ট ক্লাস ফাংশন হওয়ার কারণে আমরা ফাংশনকে ভেরিয়েবলে স্টোর করতে পারি (যেমন: উদাহরণ ১)।
   - আমরা একটি ফাংশনকে অন্য ফাংশনের ইনপুট বা আর্গুমেন্ট হিসেবে পাঠাতে পারি (যেমন: উদাহরণ ২)।
   - যখন একটি ফাংশন অন্য একটি ফাংশনকে আর্গুমেন্ট হিসেবে গ্রহণ করে বা ফাংশন রিটার্ন করে, 
     তখন তাকে 'হায়ার অর্ডার ফাংশন' (Higher-Order Function) বলা হয়।
   - এই বৈশিষ্ট্যের কারণেই জাভাস্ক্রিপ্টে কলব্যাক ফাংশন (Callback Function) ব্যবহার করা সম্ভব হয়।
*/