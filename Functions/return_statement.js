/* 
   একটি ফাংশন যখন তার কাজ শেষ করে কোনো ফলাফল বা মান (Value) ফাংশন কলের জায়গায় ফেরত পাঠাতে চায়, 
   তখন 'return' ব্যবহার করা হয়।
*/

/* 
   ১. এখানে 'add' নামে একটি ফাংশন তৈরি করা হয়েছে যা দুটি প্যারামিটার (num1, num2) গ্রহণ করে।
   ২. ফাংশনের ভেতরে 'return' কিওয়ার্ড ব্যবহার করা হয়েছে। 
   ৩. 'return num1 + num2;' এর মানে হলো, এই ফাংশনটি num1 এবং num2 যোগ করবে এবং 
      সেই যোগফলটি ফাংশনের বাইরে পাঠিয়ে দিবে। 
   ৪. মনে রাখতে হবে, 'return' করার পর ফাংশনের ভেতরে আর কোনো কোড কাজ করে না।
*/
function add(num1, num2) {
  return num1 + num2;
}

/* 
   ৫. এখানে 'add(1, 3)' লিখে ফাংশনটিকে কল করা হয়েছে। 
   ৬. ফাংশনটি ১ এবং ৩ যোগ করে ফলাফল হিসেবে ৪ 'return' করেছে। 
   ৭. সেই ফেরত আসা মানটি (৪) আমরা 'sum' নামক একটি ভেরিয়েবলের মধ্যে স্টোর বা জমা করে রেখেছি। 
   ৮. যদি আমরা 'return' না করতাম, তবে 'sum' এর মান 'undefined' হতো।
*/
let sum = add(1, 3);

/* 
   ৯. সবশেষে console.log এর মাধ্যমে 'sum' ভেরিয়েবলে জমা থাকা মানটি প্রিন্ট করা হচ্ছে। 
   আউটপুট: 4
*/
console.log(sum);

/* 
   সারসংক্ষেপ (Learning Note):
   - 'return' কিওয়ার্ড ফাংশনের এক্সিকিউশন বন্ধ করে দেয় এবং একটি নির্দিষ্ট মান ফেরত পাঠায়।
   - ফাংশন থেকে কোনো ডেটা বা রেজাল্ট নিয়ে পরবর্তীতে অন্য কোথাও ব্যবহার করতে চাইলে 'return' করা জরুরি।
   - যদি কোনো ফাংশনে 'return' না থাকে, তবে সেটি ডিফল্টভাবে 'undefined' রিটার্ন করে।
   - এটি ফাংশনকে আরও বেশি ডাইনামিক এবং কার্যকর করে তোলে।
*/
