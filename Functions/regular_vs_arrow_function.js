/* 
   এই ফাইলটিতে জাভাস্ক্রিপ্টের একটি অত্যন্ত গুরুত্বপূর্ণ বিষয়—রেগুলার ফাংশন বনাম অ্যারো ফাংশনের 
   'this' কীওয়ার্ডের ব্যবহারের পার্থক্য দেখানো হয়েছে।
*/

const obj = {
  value: 42,

  /* 
     ১. রেগুলার ফাংশন (Regular Function):
     রেগুলার ফাংশনের ক্ষেত্রে 'this' এর মান নির্ভর করে ফাংশনটি কীভাবে কল করা হচ্ছে তার ওপর। 
     যখন আমরা 'obj.regular()' কল করি, তখন 'this' সরাসরি 'obj' অবজেক্টকে নির্দেশ করে। 
     তাই এটি 'obj.value' অর্থাৎ ৪২ রিটার্ন করতে পারে।
  */
  regular: function () {
    return this.value;
  },

  /* 
     ২. অ্যারো ফাংশন (Arrow Function):
     অ্যারো ফাংশনের নিজস্ব কোনো 'this' নেই। এটি তার চারপাশের (Lexical Scope) 'this' কে ধার করে। 
     এখানে অ্যারো ফাংশনটি 'obj' অবজেক্টের ভেতরে থাকলেও, অবজেক্ট নিজে কোনো স্কোপ তৈরি করে না। 
     ফলে এই অ্যারো ফাংশনটি গ্লোবাল স্কোপের (বা উইন্ডো অবজেক্টের) 'this' কে ব্যবহার করে। 
     গ্লোবাল স্কোপে 'value' নামে কিছু না থাকায় এটি 'undefined' রিটার্ন করে।
  */
  arrow: () => {
    return this.value;
  },
};

/* 
   এখানে 'obj.regular()' কল করা হয়েছে। 
   যেহেতু এটি একটি মেথড হিসেবে কল করা হয়েছে, তাই এর 'this' হলো 'obj'। 
   আউটপুট আসবে: 42
*/
console.log(obj.regular());

/* 
   এখানে 'obj.arrow()' কল করা হয়েছে। 
   অ্যারো ফাংশন হওয়ার কারণে এটি 'obj' কে 'this' হিসেবে পায় না, বরং বাইরের গ্লোবাল স্কোপকে পায়। 
   আউটপুট আসবে: undefined
*/
console.log(obj.arrow());

/* 
   সারসংক্ষেপ ও শেখার বিষয়:
   ১. অবজেক্টের মেথড তৈরির জন্য রেগুলার ফাংশন ব্যবহার করা নিরাপদ যদি আপনি 'this' ব্যবহার করতে চান।
   ২. অ্যারো ফাংশন সাধারণত কলব্যাক ফাংশন হিসেবে ভালো কাজ করে, কিন্তু অবজেক্ট মেথড হিসেবে 
      ব্যবহার করলে 'this' নিয়ে সমস্যা হতে পারে।
   ৩. মনে রাখবেন: Regular functions have their own 'this', Arrow functions inherit 'this'.
*/

