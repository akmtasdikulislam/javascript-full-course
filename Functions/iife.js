/* 
   জাভাস্ক্রিপ্টে IIFE (Immediately Invoked Function Expression) এমন একটি ফাংশন যা তৈরি করার সাথে সাথেই নিজে নিজে কল বা এক্সিকিউট হয়ে যায়।
*/

/* 
   ১. এখানে একটি অ্যানোনিমাস (নামহীন) ফাংশনকে প্রথম বন্ধনী () এর ভেতরে রাখা হয়েছে।
   ২. ফাংশনটিকে বন্ধনী দিয়ে ঘেরাও করার কারণ হলো জাভাস্ক্রিপ্টকে বোঝানো যে এটি একটি 'Expression', 
      কোনো সাধারণ ফাংশন ডিক্লারেশন নয়।
   ৩. IIFE ব্যবহার করার প্রধান কারণ হলো একটি 'Private Scope' তৈরি করা, যাতে এর ভেতরের 
      ভেরিয়েবলগুলো বাইরের গ্লোবাল স্কোপের সাথে মিশে না যায় (Global Namespace Pollution রোধ করা)।
*/
(function () {
  /* 
     ৪. ফাংশন বডির ভেতরে console.log ব্যবহার করা হয়েছে। 
     ৫. যখনই এই ফাইলটি রান হবে, এই মেসেজটি সরাসরি আউটপুটে দেখা যাবে।
  */
  console.log("Hello World");

  /* 
     ৬. এখানে ফাংশন বডি শেষ হয়েছে এবং বাইরের বন্ধনীটি ক্লোজ করা হয়েছে।
     ৭. সবশেষে যে অতিরিক্ত এক জোড়া প্রথম বন্ধনী '()' দেওয়া হয়েছে, সেটিই মূলত ফাংশনটিকে 
        সাথে সাথে কল বা ইনভোক (Invoke) করে দেয়।
  */
})();

/* 
   IIFE-এর আরেকটি বাস্তব উদাহরণ: প্রাইভেট স্কোপিং (Private Scoping) এবং ক্লোজার (Closure)।
   এখানে 'score' ভেরিয়েবলটি ফাংশনের বাইরে থেকে সরাসরি অ্যাক্সেস বা পরিবর্তন করা সম্ভব নয়।
*/
let shery = (function () {
  /* 
     ৭. 'score' একটি লোকাল ভেরিয়েবল। এটি ফাংশনের ভেতরেই সীমাবদ্ধ (Private)।
     বাইরে থেকে কেউ 'shery.score' লিখে এটি দেখতে পাবে না।
  */
  let score = 0;

  /* 
     ৮. এই IIFE একটি অবজেক্ট রিটার্ন করছে। 
     এই অবজেক্টের ভেতরে থাকা মেথডগুলো ক্লোজারের মাধ্যমে 'score' ভেরিয়েবলটিকে মনে রাখবে।
  */
  return {
    /* 
       ৯. 'getScore' মেথডটি ব্যবহার করে আমরা প্রাইভেট 'score' এর মান দেখতে পারবো।
    */
    getScore: function () {
      console.log(score);
    },
    /* 
       ১০. 'setScore' মেথডটি ব্যবহার করে আমরা নিয়ন্ত্রিতভাবে 'score' এর মান পরিবর্তন করতে পারবো।
    */
    setScore: function (val) {
      score = val;
    },
  };
  /* 
     ১১. এখানে '()' দিয়ে ফাংশনটিকে সাথে সাথে কল করা হয়েছে, ফলে 'shery' ভেরিয়েবলে 
     রিটার্ন করা অবজেক্টটি জমা হবে।
  */
})();

/* 
   ব্যবহার বিধি (Usage):
   shery.getScore(); // আউটপুট: 0
   shery.setScore(100); 
   shery.getScore(); // আউটপুট: 100
*/

/* 
   সারসংক্ষেপ (Learning Note):
   - IIFE-এর পূর্ণরূপ হলো Immediately Invoked Function Expression।
   - এটি ডিফাইন করার সাথে সাথেই একবার রান হয় এবং পুনরায় কল করার জন্য এর কোনো নাম থাকে না।
   - এটি গ্লোবাল ভেরিয়েবলের ভিড় (Pollution) কমাতে সাহায্য করে।
   - মডিউল প্যাটার্ন বা লাইব্রেরি তৈরিতে ডেটা হাইড (Data Privacy) করার জন্য এটি অত্যন্ত কার্যকর।
*/

/* 
   অতিরিক্ত নোট: কেন আমরা IIFE ব্যবহার করবো?
   ১. ভেরিয়েবল কলিশন এড়াতে: যদি আপনার প্রজেক্টে অনেকগুলো জাভাস্ক্রিপ্ট ফাইল থাকে, 
      তবে এক ফাইলের ভেরিয়েবল অন্য ফাইলের সাথে মিলে গিয়ে সমস্যা তৈরি করতে পারে। 
      IIFE ব্যবহার করলে ভেরিয়েবলগুলো ওই ফাংশনের ভেতরেই সীমাবদ্ধ থাকে।
   
   ২. মেমোরি ম্যানেজমেন্ট: IIFE এর ভেতরের কাজ শেষ হয়ে গেলে এর লোকাল ভেরিয়েবলগুলো 
      মেমোরি থেকে মুছে যায় (যদি না ক্লোজার হিসেবে ব্যবহৃত হয়), যা পারফরম্যান্স ভালো রাখে।
*/

/* 
   প্যারামিটার সহ IIFE (IIFE with Parameters):
   আমরা চাইলে IIFE তে আর্গুমেন্টও পাঠাতে পারি।
*/
(function (name) {
  /* 
     ১২. এখানে 'name' প্যারামিটারটি বাইরের আর্গুমেন্ট গ্রহণ করবে।
  */
  console.log(`Hello, ${name}! This is an IIFE with parameters.`);
  
  /* 
     ১৩. নিচের বন্ধনীতে "Tasdikul" আর্গুমেন্ট হিসেবে পাঠানো হয়েছে।
  */
})("Tasdikul");
/* 
   এটি সাথে সাথে "Hello, Tasdikul! This is an IIFE with parameters." প্রিন্ট করবে।
*/