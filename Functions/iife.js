/* 
   জাভাস্ক্রিপ্টে IIFE (Immediately Invoked Function Expression) এমন একটি ফাংশন যা তৈরি করার সাথে সাথেই নিজে নিজে কল বা এক্সিকিউট হয়ে যায়।
*/

/* 
   ১. এখানে একটি অ্যানোনিমাস (নামহীন) ফাংশনকে প্রথম বন্ধনী () এর ভেতরে রাখা হয়েছে।
   ২. ফাংশনটিকে বন্ধনী দিয়ে ঘেরাও করার কারণ হলো জাভাস্ক্রিপ্টকে বোঝানো যে এটি একটি 'Expression', 
      কোনো সাধারণ ফাংশন ডিক্লারেশন নয়।
   ৩. IIFE ব্যবহার করার প্রধান কারণ হলো একটি 'Private Scope' তৈরি করা, যাতে এর ভেতরের 
      ভেরিয়েবলগুলো বাইরের গ্লোবাল স্কোপের সাথে মিশে না যায় (Global Namespace Pollution রোধ করা)।
*/
(function () {
  /* 
     ৪. ফাংশন বডির ভেতরে console.log ব্যবহার করা হয়েছে। 
     ৫. যখনই এই ফাইলটি রান হবে, এই মেসেজটি সরাসরি আউটপুটে দেখা যাবে।
  */
  console.log("Hello World");

  /* 
     ৬. এখানে ফাংশন বডি শেষ হয়েছে এবং বাইরের বন্ধনীটি ক্লোজ করা হয়েছে।
     ৭. সবশেষে যে অতিরিক্ত এক জোড়া প্রথম বন্ধনী '()' দেওয়া হয়েছে, সেটিই মূলত ফাংশনটিকে 
        সাথে সাথে কল বা ইনভোক (Invoke) করে দেয়।
  */
})();

/* 
   সারসংক্ষেপ (Learning Note):
   - IIFE-এর পূর্ণরূপ হলো Immediately Invoked Function Expression।
   - এটি একবারই চলে এবং পুনরায় কল করার জন্য কোনো নাম থাকে না।
   - লাইব্রেরি বা মডিউল তৈরিতে এটি ডেটা হাইড করার জন্য প্রচুর ব্যবহৃত হয়।
*/
