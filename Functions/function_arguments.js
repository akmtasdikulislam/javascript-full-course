/* 
   জাভাস্ক্রিপ্টে ফাংশন আর্গুমেন্ট (Function Arguments) সম্পর্কে শিখি।
   আর্গুমেন্ট হলো সেই প্রকৃত মান (Actual Value) যা ফাংশন কল করার সময় পাঠানো হয়।
*/

/* 
   ১. এখানে 'welcome' নামে একটি ফাংশন ডিক্লেয়ার করা হয়েছে।
   ২. 'name' হলো একটি প্যারামিটার, যা ফাংশনটি কল করার সময় পাঠানো আর্গুমেন্ট গ্রহণ করবে।
*/
function welcome(name) {
  /* 
     ৩. console.log এর মাধ্যমে একটি স্বাগতম বার্তা প্রিন্ট করা হচ্ছে।
     ৪. টেম্পলেট লিটারেল (``) ব্যবহার করা হয়েছে যাতে 'name' ভেরিয়েবলের মানটি ডাইনামিকভাবে বসানো যায়।
  */
  console.log(`Welcome, ${name}`);
}

/* 
   ৫. এখানে 'add' নামে একটি ফাংশন তৈরি করা হয়েছে যা দুটি প্যারামিটার (num1, num2) নেয়।
   ৬. এর কাজ হলো আর্গুমেন্ট হিসেবে আসা দুটি সংখ্যার যোগফল বের করা।
*/
function add(num1, num2) {
  /* 
     ৭. num1 এবং num2 এর যোগফল কনসোলে দেখানো হচ্ছে।
  */
  console.log(num1 + num2);
}

/* 
   ৮. ফাংশন কল করার সময় প্রথম বন্ধনীর ভেতর যে মানটি দেওয়া হয়, তাকেই 'আর্গুমেন্ট' বলে।
   ৯. এখানে "Tasdikul" হলো একটি স্ট্রিং আর্গুমেন্ট। 
   ১০. এটি ফাংশনের 'name' প্যারামিটারে গিয়ে জমা হবে এবং আউটপুট দিবে: Welcome, Tasdikul
*/
welcome("Tasdikul");

/* 
   ১১. এখানে 'add' ফাংশনটিকে দুটি আর্গুমেন্ট (1 এবং 2) দিয়ে কল করা হয়েছে।
   ১২. প্রথম আর্গুমেন্ট '1' যাবে 'num1' এ এবং দ্বিতীয় আর্গুমেন্ট '2' যাবে 'num2' এ।
   ১৩. আউটপুট আসবে: 3
*/
add(1, 2);

/* 
   সারসংক্ষেপ (Learning Note):
   - প্যারামিটার (Parameter) থাকে ফাংশন তৈরির সময় (Definition)।
   - আর্গুমেন্ট (Argument) থাকে ফাংশন চালানোর সময় (Call)।
   - আর্গুমেন্ট হিসেবে আমরা স্ট্রিং, নাম্বার, অবজেক্ট বা অন্য ফাংশনও পাঠাতে পারি।
   - যদি ফাংশন কল করার সময় আর্গুমেন্ট কম দেওয়া হয়, তবে বাকি প্যারামিটারগুলো 'undefined' থাকে।
   - যদি আর্গুমেন্ট বেশি দেওয়া হয়, তবে জাভাস্ক্রিপ্ট অতিরিক্ত আর্গুমেন্টগুলো ইগনোর করে 
     (যদিও 'arguments' অবজেক্টের মাধ্যমে সেগুলো পাওয়া সম্ভব)।
*/
