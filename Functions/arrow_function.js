/* 
   জাভাস্ক্রিপ্টে অ্যারো ফাংশন (Arrow Function) সম্পর্কে শিখি।
   এটি ES6 ভার্সনে আসা ফাংশন লেখার একটি আধুনিক এবং সংক্ষিপ্ত পদ্ধতি।
*/

/* 
   ১. এখানে 'let' কিওয়ার্ড ব্যবহার করে 'fnc' নামে একটি ভেরিয়েবল ঘোষণা করা হয়েছে।
   ২. সমান চিহ্নের (=) ডান পাশে '() =>' ব্যবহার করা হয়েছে। 
      - '()' হলো প্যারামিটার লিস্ট (এখানে কোনো ইনপুট নেই)।
      - '=>' (Fat Arrow) চিহ্নটি নির্দেশ করে যে এটি একটি অ্যারো ফাংশন।
   ৩. অ্যারো ফাংশনে 'function' কিওয়ার্ডটি লেখার প্রয়োজন হয় না, যা কোডকে আরও ছোট করে।
   ৪. এই পুরো ফাংশনটিকে 'fnc' ভেরিয়েবলের ভেতরে স্টোর বা জমা করে রাখা হয়েছে।
*/
let fnc = () => {
  /* 
     ৫. দ্বিতীয় বন্ধনী {} বা কার্লি ব্র্যাকেটের ভেতরে ফাংশনের মূল কাজ বা 'body' লেখা হয়েছে।
     ৬. console.log ব্যবহার করা হয়েছে যাতে ফাংশনটি চললে আউটপুটে "Hello World" দেখায়।
  */
  console.log("Hello World");
  /* 
     ৭. এখানে কার্লি ব্র্যাকেট } দিয়ে ফাংশন বডি শেষ করা হয়েছে।
  */
};

/* 
   ৮. যেহেতু ফাংশনটি 'fnc' ভেরিয়েবলে জমা আছে, তাই একে কার্যকর করার জন্য 
      ভেরিয়েবলের নামের সাথে প্রথম বন্ধনী 'fnc()' ব্যবহার করে কল করা হয়েছে।
*/
fnc();

/* 
   সারসংক্ষেপ (Learning Note):
   - অ্যারো ফাংশন হলো ফাংশন এক্সপ্রেশনের একটি সংক্ষিপ্ত রূপ।
   - এতে 'function' কিওয়ার্ড লিখতে হয় না, তার বদলে '=>' ব্যবহার করা হয়।
   - যদি ফাংশনের ভেতরে মাত্র একটি লাইন থাকে, তবে কার্লি ব্র্যাকেট {} এবং 
     'return' কিওয়ার্ড ছাড়াও এটি লেখা সম্ভব (Implicit Return)।
   - এটি সাধারণত কলব্যাক ফাংশন হিসেবে এবং কোড ক্লিন রাখতে বেশি ব্যবহৃত হয়।
   - ফাংশন এক্সপ্রেশনের মতো এটিকেও ডিক্লেয়ার করার আগে কল করা যায় না।
*/
