/* 
   জাভাস্ক্রিপ্টে লেক্সিক্যাল স্কোপিং (Lexical Scoping) একটি অত্যন্ত গুরুত্বপূর্ণ কনসেপ্ট। 
   এর সহজ অর্থ হলো—একটি নেস্টেড ফাংশন (ফাংশনের ভেতরে ফাংশন) সবসময় তার বাইরের 
   প্যারেন্ট ফাংশনের ভেরিয়েবলগুলোকে অ্যাক্সেস করতে পারে। 
   কিন্তু প্যারেন্ট ফাংশন তার চাইল্ড ফাংশনের ভেতরের ভেরিয়েবলগুলো দেখতে পায় না।
*/

/* 
   ১. এখানে 'grandParentFunction' নামে একটি টপ-লেভেল ফাংশন তৈরি করা হয়েছে।
   ২. এটি হলো সবার বাইরের স্কোপ।
*/
function grandParentFunction() {
  /* 
     ৩. 'grandParentVar' নামে একটি ভেরিয়েবল ঘোষণা করা হয়েছে। 
     ৪. লেক্সিক্যাল স্কোপিং অনুযায়ী, এই ভেরিয়েবলটি এর ভেতরে থাকা সব ফাংশন ব্যবহার করতে পারবে।
  */
  let grandParentVar = 12;

  /* 
     ৫. 'parentFunction' নামে একটি ফাংশন 'grandParentFunction' এর ভেতরে তৈরি করা হয়েছে।
  */
  function parentFunction() {
    /* 
       ৬. 'parentVar' নামে একটি লোকাল ভেরিয়েবল ঘোষণা করা হয়েছে। 
       ৭. এই ফাংশনটি তার নিজের ভেরিয়েবল এবং তার প্যারেন্টের 'grandParentVar'—উভয়ই অ্যাক্সেস করতে পারবে।
    */
    let parentVar = 13;

    /* 
       ৮. 'childFunction' হলো একদম ভেতরের ফাংশন।
    */
    function childFunction() {
      /* 
         ৯. 'childVar' নামে একটি লোকাল ভেরিয়েবল ঘোষণা করা হয়েছে।
      */
      let childVar = 14;

      /* 
         ১০. লেক্সিক্যাল স্কোপিংয়ের আসল জাদু এখানে:
         - এটি নিজের 'childVar' অ্যাক্সেস করতে পারছে।
         - এটি তার ঠিক উপরের 'parentVar' অ্যাক্সেস করতে পারছে।
         - এটি একদম বাইরের 'grandParentVar' ও অ্যাক্সেস করতে পারছে।
         চাইল্ড ফাংশন তার চেইন ধরে উপরের দিকে সব ভেরিয়েবল খুঁজে পায়।
      */
      console.log(grandParentVar, parentVar, childVar);
    }

    /* 
       ১১. চাইল্ড ফাংশনটিকে কল করা হলো যাতে এটি কাজ শুরু করে।
    */
    childFunction();
  }

  /* 
     ১২. প্যারেন্ট ফাংশনটিকে কল করা হলো।
  */
  parentFunction();
}

/* 
   ১৩. গ্র্যান্ড প্যারেন্ট ফাংশনটিকে কল করার মাধ্যমে পুরো চেইনটি শুরু করা হলো।
*/
grandParentFunction();

/* 
   সারসংক্ষেপ (Learning Note):
   - লেক্সিক্যাল স্কোপিং নির্ধারণ করে যে কোডের কোথায় কোন ভেরিয়েবল পাওয়া যাবে।
   - এটি কোড লেখার পজিশন বা স্ট্রাকচারের ওপর নির্ভর করে (Static Scoping)।
   - ইনার ফাংশন (Inner Function) আউটার ফাংশনের (Outer Function) স্কোপে অ্যাক্সেস পায়।
   - একে 'Scope Chain' ও বলা হয়, কারণ জাভাস্ক্রিপ্ট কোনো ভেরিয়েবল না পেলে 
     ধাপে ধাপে বাইরের স্কোপে খুঁজতে থাকে।
   - কিন্তু উল্টোটা সম্ভব নয়; অর্থাৎ 'grandParentFunction' কখনোই 'childVar' কে অ্যাক্সেস করতে পারবে না।
*/
