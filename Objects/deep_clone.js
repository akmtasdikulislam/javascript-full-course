/* 
   এখানে একটি 'obj' তৈরি করা হয়েছে যাতে সাধারণ প্রপার্টির পাশাপাশি 
   'address' নামে একটি নেস্টেড অবজেক্ট (Nested Object) রয়েছে।
*/
let obj = {
  name: "harsh",
  age: 26,
  email: "test@test.com",
  address: {
    city: "Bhopal", // এটি একটি নেস্টেড প্রপার্টি
  },
};

/* 
   স্প্রেড অপারেটর (...) ব্যবহার করে কপি করা:
   এটি একটি 'Shallow Copy' তৈরি করে। এর মানে হলো এটি প্রথম লেভেলের প্রপার্টিগুলো 
   নতুন করে তৈরি করলেও ভেতরের নেস্টেড অবজেক্টগুলোর (যেমন: address) শুধু রেফারেন্স কপি করে।
*/
let obj2 = { ...obj };

/* 
   শ্যালো কপির সীমাবদ্ধতা (Limitations):
   যেহেতু 'address' অবজেক্টটি মেমোরিতে একই জায়গায় পয়েন্ট করে আছে, 
   তাই 'obj2' এর মাধ্যমে সিটি পরিবর্তন করলে মূল 'obj' এর সিটিও বদলে যাবে।
*/
obj2.address.city = "Indore";

/* 
   এখানে দেখা যাবে obj2 এবং obj উভয়েরই সিটি "Indore" হয়ে গেছে। 
   এটিই শ্যালো কপির সমস্যা।
*/
console.log(obj2);
console.log(obj);

/* 
   ডিপ ক্লোন (Deep Clone) এর মাধ্যমে সমাধান:
   JSON.stringify(obj): এটি পুরো অবজেক্টটিকে একটি স্ট্রিং-এ রূপান্তর করে। 
   এর ফলে মেমোরির সাথে সব সম্পর্ক ছিন্ন হয়ে যায়।
   
   JSON.parse(...): সেই স্ট্রিংটিকে আবার নতুন একটি অবজেক্টে রূপান্তর করে। 
   এর ফলে সম্পূর্ণ নতুন একটি মেমোরি লোকেশনে অবজেক্টটি তৈরি হয়, যার সাথে মূল অবজেক্টের কোনো যোগসূত্র থাকে না।
*/
let obj3 = JSON.parse(JSON.stringify(obj));

/* 
   এখন 'obj3' এর নেস্টেড প্রপার্টি পরিবর্তন করলে সেটি শুধুমাত্র 'obj3' তেই কার্যকর হবে।
   মূল 'obj' এর ডাটা একদম সুরক্ষিত থাকবে।
*/
obj3.address.city = "Kolkata";

/* 
   আউটপুটে দেখা যাবে obj3 এর সিটি "Kolkata" কিন্তু obj এর সিটি আগের মতোই "Indore" রয়ে গেছে।
*/
console.log(obj3);
console.log(obj);

/* 
   বিশেষ দ্রষ্টব্য: 
   ১. JSON.parse(JSON.stringify()) পদ্ধতিটি সহজ হলেও এর কিছু সীমাবদ্ধতা আছে। 
      এটি অবজেক্টের ভেতরে থাকা ফাংশন (Functions) বা আনডিফাইন্ড (undefined) ভ্যালু কপি করতে পারে না।
   
   ২. আধুনিক জাভাস্ক্রিপ্টে 'structuredClone(obj)' নামে একটি ইন-বিল্ট মেথড আছে 
      যা আরও নিখুঁতভাবে ডিপ ক্লোন করতে পারে।
*/
