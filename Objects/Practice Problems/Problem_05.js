/* 
   প্রশ্ন ৫: নিচের অবজেক্টটি থেকে ল্যাটিটিউড (latitude) প্রিন্ট করুন।
   উদ্দেশ্য: নেস্টেড অবজেক্ট (Nested Object) বা একটি অবজেক্টের ভেতরে থাকা অন্য অবজেক্টের ডাটা অ্যাক্সেস করা শেখা।
*/

const locations = {
  /* 
     'city' একটি সাধারণ প্রপার্টি যা স্ট্রিং ভ্যালু ধারণ করছে।
  */
  city: "Bhopal",

  /* 
     'coordinates' হলো একটি নেস্টেড অবজেক্ট। 
     অর্থাৎ, এই কি (key)-এর ভ্যালু হিসেবে আরেকটি অবজেক্ট ব্যবহার করা হয়েছে।
     এর ভেতরে 'lat' এবং 'lng' নামে আরও দুটি প্রপার্টি আছে।
  */
  coordinates: {
    lat: 23.2,
    lng: 77.4,
  },
};

/* 
   অ্যাক্সেস করার নিয়ম:
   ১. প্রথমে আমাদের মূল অবজেক্ট 'locations' লিখতে হবে।
   ২. এরপর ডট (.) দিয়ে তার ভেতরের অবজেক্ট 'coordinates' এ ঢুকতে হবে।
   ৩. সবশেষে আবার ডট (.) দিয়ে 'lat' প্রপার্টিটি অ্যাক্সেস করতে হবে।
   
   চেইন সিস্টেম: locations -> coordinates -> lat
*/
console.log(locations.coordinates.lat);

