// Q2. Can an object key be a number or boolean?

/* 
   প্রশ্ন ২: একটি অবজেক্টের 'কি' (key) কি নম্বর বা বুলিয়ান হতে পারে?
   উদ্দেশ্য: জাভাস্ক্রিপ্ট অবজেক্টের কি (key) কিভাবে কাজ করে এবং ডাটা টাইপ কনভার্সন বোঝা।
*/

const obj = {
  /* 
     এখানে 'true' একটি বুলিয়ান ভ্যালু হলেও জাভাস্ক্রিপ্ট এটিকে ইন্টারনালি 
     একটি স্ট্রিং "true" হিসেবে রূপান্তর করে নেয়। 
     অবজেক্টের কি (key) সবসময় স্ট্রিং বা সিম্বল হয়।
  */
  true: "yes",

  /* 
     একইভাবে '42' একটি নম্বর হলেও এটি অবজেক্টের কি হিসেবে 
     স্ট্রিং "42" তে রূপান্তরিত হয়ে সেভ হয়।
  */
  42: "answer",
};

/* 
   obj[true] ব্যবহার করে আমরা "yes" ভ্যালুটি পাচ্ছি। 
   এখানে ব্র্যাকেট নোটেশনের ভেতরে 'true' লিখলে জাভাস্ক্রিপ্ট 
   সেটিকে স্ট্রিং "true" হিসেবে খুঁজে বের করে।
*/
console.log(obj[true]);

/* 
   obj[42] ব্যবহার করে আমরা "answer" ভ্যালুটি পাচ্ছি। 
   নম্বর কি-এর ক্ষেত্রে ডট নোটেশন (obj.42) কাজ করে না, 
   তাই ব্র্যাকেট নোটেশন ব্যবহার করা বাধ্যতামূলক।
*/
console.log(obj[42]);

/* 
   সারসংক্ষেপ: হ্যাঁ, আমরা নম্বর বা বুলিয়ান কি হিসেবে লিখতে পারি, 
   কিন্তু জাভাস্ক্রিপ্ট সেগুলোকে পর্দার আড়ালে স্ট্রিং হিসেবেই ট্রিট করে।
*/
