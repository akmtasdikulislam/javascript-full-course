/* 
   প্রশ্ন ১০: Object.entries() ব্যবহার করে অবজেক্টের সকল 'কি' (key) এবং 'ভ্যালু' (value) নিচের ফরম্যাটে প্রিন্ট করুন:
   title: JavaScript
   duration: 4 weeks
   উদ্দেশ্য: একটি অবজেক্টকে অ্যারেতে রূপান্তর করে তার প্রতিটি উপাদান নিয়ে কাজ করার আধুনিক পদ্ধতি শেখা।
*/

/* 
   এখানে 'obj' নামে একটি অবজেক্ট ডিফাইন করা হয়েছে। 
   এতে দুটি প্রপার্টি আছে: 'title' এবং 'duration'।
*/
const obj = {
  title: "JavaScript",
  duration: "4 week",
};

/* 
   Object.entries(obj) এর কাজ:
   ১. এটি 'obj' অবজেক্টটিকে একটি টু-ডাইমেনশনাল অ্যারেতে (Array of Arrays) রূপান্তর করে।
   ২. আমাদের এই অবজেক্টের জন্য এটি রিটার্ন করবে: [ ["title", "JavaScript"], ["duration", "4 week"] ]
   ৩. অর্থাৎ প্রতিটি ছোট অ্যারের প্রথম উপাদান (ইন্ডেক্স ০) হলো 'key' এবং দ্বিতীয় উপাদান (ইন্ডেক্স ১) হলো 'value'।

   .forEach((val) => { ... }) এর কাজ:
   ১. forEach লুপটি Object.entries থেকে পাওয়া অ্যারের প্রতিটি উপাদানের ওপর দিয়ে যায়।
   ২. প্রতিবার লুপ ঘোরার সময় 'val' নামক প্যারামিটারে একটি করে ছোট অ্যারে (যেমন: ["title", "JavaScript"]) চলে আসে।
*/
Object.entries(obj).forEach((val) => {
  /* 
     আউটপুট প্রিন্ট করা:
     ১. val[0] দিয়ে আমরা অ্যারের প্রথম উপাদান অর্থাৎ 'key' (যেমন: title) পাচ্ছি।
     ২. val[1] দিয়ে আমরা অ্যারের দ্বিতীয় উপাদান অর্থাৎ 'value' (যেমন: JavaScript) পাচ্ছি।
     ৩. মাঝখানে ": " যোগ করে আমরা কাঙ্ক্ষিত ফরম্যাটে ডাটাটি কনসোলে দেখাচ্ছি।
  */
  console.log(val[0] + ": " + val[1]);
});

/* 
   বিকল্প পদ্ধতি (Destructuring):
   আমরা চাইলে (val) এর বদলে সরাসরি ([key, value]) ব্যবহার করে কোডটি আরও সহজ করতে পারতাম:
   Object.entries(obj).forEach(([key, value]) => {
     console.log(`${key}: ${value}`);
   });
*/

/* 
   সারসংক্ষেপ: 
   Object.entries() ব্যবহার করলে অবজেক্টের কি এবং ভ্যালু উভয়ই একসাথে অ্যারে হিসেবে পাওয়া যায়, যা লুপ চালানোর জন্য খুব সুবিধাজনক।
*/
Object.entries(obj).forEach((val) => {
  console.log(val[0] + ": " + val[1]);
});
