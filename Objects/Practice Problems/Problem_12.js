// Q12. Deep clone the obj1 safely

/* 
   প্রশ্ন ১২: obj1-কে 'Deep Clone' বা গভীরভাবে কপি করুন যাতে একটি পরিবর্তন করলে অন্যটি প্রভাবিত না হয়।
   উদ্দেশ্য: নেস্টেড অবজেক্টের ক্ষেত্রে সাধারণ কপি (Shallow Copy) এবং ডিপ কপির (Deep Copy) পার্থক্য বোঝা।
*/

/* 
   এখানে 'obj1' একটি নেস্টেড অবজেক্ট। 
   এর ভেতরে 'info' নামে আরেকটি অবজেক্ট আছে যার ভেতরে 'score' প্রপার্টি রয়েছে।
   সাধারণ স্প্রেড অপারেটর (...) দিয়ে কপি করলে ভেতরের 'info' অবজেক্টটি রেফারেন্স হিসেবেই থেকে যায়।
*/
const obj1 = { info: { score: 80 } };

/* 
   ডিপ ক্লোনিং পদ্ধতি (JSON.parse & JSON.stringify):
   
   ১. JSON.stringify(obj1): এটি পুরো অবজেক্টটিকে একটি স্ট্রিং-এ রূপান্তর করে ফেলে। 
      এর ফলে অবজেক্টের সাথে মেমোরি অ্যাড্রেসের সব কানেকশন বিচ্ছিন্ন হয়ে যায়।
   
   ২. JSON.parse(...): স্ট্রিং হয়ে যাওয়া ডাটাটিকে পুনরায় একটি নতুন জাভাস্ক্রিপ্ট অবজেক্টে রূপান্তর করে।
   
   ৩. ফলাফল: 'obj2' এখন 'obj1' এর একটি সম্পূর্ণ স্বাধীন কপি। এর ভেতরের নেস্টেড অবজেক্টগুলোও 
      সম্পূর্ণ আলাদা মেমোরি লোকেশনে তৈরি হয়েছে।
*/
let obj2 = JSON.parse(JSON.stringify(obj1));

/* 
   পরিবর্তন পরীক্ষা করা:
   আমরা 'obj2' এর ভেতরের স্কোর পরিবর্তন করে ১০০ করছি। 
   যেহেতু এটি একটি ডিপ কপি, তাই এই পরিবর্তনের প্রভাব 'obj1' এর ওপর পড়বে না।
*/
obj2.info.score = 100;

/* 
   আউটপুট চেক করা:
   obj2 প্রিন্ট করলে আমরা { info: { score: 100 } } দেখতে পাবো।
*/
console.log(obj2);

/* 
   obj1 প্রিন্ট করলে আমরা আগের ভ্যালু { info: { score: 80 } } দেখতে পাবো। 
   এটিই প্রমাণ করে যে কপিটি সফলভাবে এবং নিরাপদে (safely) হয়েছে।
*/
console.log(obj1);

/* 
   সতর্কতা: 
   JSON.parse(JSON.stringify()) পদ্ধতিটি সহজ হলেও এর কিছু সীমাবদ্ধতা আছে। 
   যদি অবজেক্টের ভেতরে কোনো ফাংশন (Function) বা আনডিফাইন্ড (undefined) ভ্যালু থাকে, 
   তবে এই পদ্ধতিতে সেগুলো হারিয়ে যায়। তবে সাধারণ ডাটার জন্য এটি অত্যন্ত কার্যকর।
*/

/* 
   সারসংক্ষেপ: 
   - Shallow Copy (স্প্রেড অপারেটর) শুধুমাত্র উপরের লেভেল কপি করে, নেস্টেড অবজেক্টের রেফারেন্স রেখে দেয়।
   - Deep Copy (JSON পদ্ধতি) অবজেক্টের প্রতিটি লেভেলকে আলাদাভাবে কপি করে সম্পূর্ণ নতুন অবজেক্ট তৈরি করে।
*/
