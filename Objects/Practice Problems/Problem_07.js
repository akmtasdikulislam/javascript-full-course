// Q7. Destructure the city and lat from the location object below:

/* 
   প্রশ্ন ৭: নিচের 'locations' অবজেক্টটি থেকে 'city' এবং 'lat' প্রপার্টি দুটি ডিস্ট্রাকচার (Destructure) করুন।
   উদ্দেশ্য: অবজেক্ট ডিস্ট্রাকচারিং শেখা, যার মাধ্যমে অবজেক্টের ভেতর থেকে নির্দিষ্ট ডাটা বের করে সরাসরি ভেরিয়েবলে রাখা যায়।
*/

const locations = {
  /* 
     'city' একটি সাধারণ প্রপার্টি।
  */
  city: "Bhopal",

  /* 
     'coordinates' একটি নেস্টেড অবজেক্ট যার ভেতরে 'lat' এবং 'lng' আছে।
  */
  coordinates: {
    lat: 23.2,
    lng: 77.4,
  },
};

/* 
   ১. সাধারণ ডিস্ট্রাকচারিং:
   এখানে 'locations' অবজেক্ট থেকে 'city' প্রপার্টিটি বের করে আনা হয়েছে।
   let { city } = locations; এর মানে হলো— 'locations' অবজেক্টে 'city' নামে যে কি (key) আছে, 
   তার ভ্যালু নিয়ে 'city' নামেই একটি নতুন ভেরিয়েবল তৈরি করো। 
   এর ফলে আমাদের বারবার 'locations.city' লিখতে হবে না।
*/
let { city } = locations;

/* 
   ২. নেস্টেড অবজেক্ট থেকে ডিস্ট্রাকচারিং:
   যেহেতু 'lat' সরাসরি 'locations' এর ভেতরে নেই, বরং 'coordinates' এর ভেতরে আছে, 
   তাই আমরা 'locations.coordinates' থেকে 'lat' কে ডিস্ট্রাকচার করছি।
   এটি 'lat' নামে একটি আলাদা ভেরিয়েবল তৈরি করবে যার মান হবে ২৩.২।
*/
let { lat } = locations.coordinates;

/* 
   আউটপুট চেক করা:
*/
console.log(city); // আউটপুট: Bhopal
console.log(lat); // আউটপুট: 23.2

