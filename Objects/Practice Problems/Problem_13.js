// Q13. Use a variable to dynamically assign a property

/* 
   প্রশ্ন ১৩: একটি ভেরিয়েবল ব্যবহার করে কীভাবে অবজেক্টের ভেতরে ডাইনামিকভাবে (Dynamic) প্রপার্টি বা কি (key) অ্যাসাইন করবেন?
   উদ্দেশ্য: Computed Property Names-এর ব্যবহার শেখা, যার মাধ্যমে রানটাইমে কোনো ভেরিয়েবলের মানকে অবজেক্টের 'কি' হিসেবে ব্যবহার করা যায়।
*/

/* 
   এখানে 'key' নামে একটি ভেরিয়েবল নেওয়া হয়েছে যার ভ্যালু "role"। 
   আমরা চাচ্ছি এই ভেরিয়েবলের ভেতরে থাকা মানটিই যেন অবজেক্টের একটি প্রপার্টি বা কি (key) হয়।
*/
const key = "role";

let obj = {
  /* 
     'name' একটি সাধারণ স্ট্যাটিক প্রপার্টি।
  */
  name: "Harsh",

  /* 
     ডাইনামিক কি (Computed Property):
     ১. যদি আমরা সরাসরি role: "admin" লিখতাম, তবে সেটি সবসময় 'role' ই থাকতো।
     ২. কিন্তু আমরা যদি কোনো ভেরিয়েবলের মানকে 'কি' হিসেবে ব্যবহার করতে চাই, 
        তবে তাকে থার্ড ব্র্যাকেট [ ] এর ভেতরে লিখতে হয়। একে বলা হয় Computed Property Syntax।
     
     ৩. এখানে [key]: "admin" লেখার ফলে জাভাস্ক্রিপ্ট দেখবে 'key' ভেরিয়েবলের মান কী। 
        যেহেতু key = "role", তাই অবজেক্টটি তৈরি হবে { name: "Harsh", role: "admin" } হিসেবে।
  */
  [key]: "admin",
};

/* 
   আউটপুট চেক করা:
   console.log(obj) দিলে আমরা { name: "Harsh", role: "admin" } দেখতে পাবো।
*/
console.log(obj);

