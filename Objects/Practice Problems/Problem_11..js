/* 
   প্রশ্ন ১১: স্প্রেড অপারেটর (Spread Operator) ব্যবহার করে নিচের অবজেক্টটি কপি করুন।
   উদ্দেশ্য: অবজেক্ট ক্লোনিং বা কপি করার আধুনিক এবং সহজ পদ্ধতি শেখা।
*/

/* 
   এখানে 'original' নামে একটি সোর্স অবজেক্ট তৈরি করা হয়েছে। 
   এতে 'a' এবং 'b' নামে দুটি প্রপার্টি আছে।
*/
const original = { a: 1, b: 2 };

/* 
   স্প্রেড অপারেটর (...) এর ব্যবহার:
   ১. তিনটি ডট (...) কে জাভাস্ক্রিপ্টে স্প্রেড অপারেটর বলা হয়। 
   ২. এটি একটি অবজেক্টের ভেতরের সব উপাদানকে (properties) ভেঙে আলাদা করে ফেলে এবং 
      নতুন একটি অবজেক্টের ভেতরে সেগুলোকে বসিয়ে দেয়।
   
   ৩. { ...original } এর মানে হলো— 'original' অবজেক্টের সব কি (key) এবং ভ্যালু (value) 
      বের করে এনে এই নতুন কার্লি ব্রেস {} এর ভেতরে রাখো।
   
   ৪. এটি একটি 'Shallow Copy' তৈরি করে। অর্থাৎ, 'copy' এবং 'original' এখন দুটি আলাদা অবজেক্ট, 
      কিন্তু তাদের ভেতরের ডাটা একই।
*/
const copy = { ...original };

/* 
   আউটপুট চেক করা:
   কনসোলে আমরা 'copy' অবজেক্টটি প্রিন্ট করলে { a: 1, b: 2 } দেখতে পাবো।
*/
console.log(copy);

/* 
   কেন এটি গুরুত্বপূর্ণ?
   জাভাস্ক্রিপ্টে যদি আমরা সরাসরি let copy = original; লিখতাম, তবে সেটি নতুন কপি তৈরি না করে 
   একই মেমোরি অ্যাড্রেসকে রেফার করতো। ফলে 'copy' পরিবর্তন করলে 'original' ও বদলে যেত। 
   স্প্রেড অপারেটর ব্যবহার করলে তারা সম্পূর্ণ আলাদা হয়ে যায়।
*/

