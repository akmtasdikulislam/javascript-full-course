// Q8. Destructure the key 'first-name' as a varaible called firstName.

/* 
   প্রশ্ন ৮: 'first-name' কি-টিকে ডিস্ট্রাকচার করে 'firstName' নামক একটি ভেরিয়েবলে রাখুন।
   উদ্দেশ্য: হাইফেন যুক্ত কি (key) ডিস্ট্রাকচার করার সময় কীভাবে রিনেম (Rename) করতে হয় তা শেখা।
*/

const user = {
  /* 
     এখানে কি (key) হিসেবে 'first-name' ব্যবহার করা হয়েছে। 
     যেহেতু এতে হাইফেন আছে, তাই এটি একটি ভ্যালিড ভেরিয়েবল নাম হতে পারে না।
  */
  "first-name": "Harsh",
};

/* 
   ডিস্ট্রাকচারিং এবং রিনেম করার নিয়ম:
   
   ১. সাধারণত আমরা লিখি: let { name } = user; কিন্তু এখানে কি-এর নাম 'first-name'।
   ২. আমরা যদি সরাসরি let { first-name } = user; লিখি, তবে জাভাস্ক্রিপ্ট এরর দিবে, 
      কারণ ভেরিয়েবল নামে হাইফেন (-) অনুমোদিত নয়।
   
   ৩. সমাধান: 
      প্রথমে কোটেশনের ভেতরে কি-এর নাম লিখতে হয় "first-name", 
      তারপর কোলন (:) দিয়ে নতুন একটি ভেরিয়েবল নাম দিতে হয় (এখানে firstName)।
   
   ৪. এর ফলে জাভাস্ক্রিপ্ট 'user' অবজেক্ট থেকে 'first-name' এর ভ্যালু খুঁজে বের করবে 
      এবং সেটি 'firstName' নামক নতুন ভেরিয়েবলে স্টোর করবে।
*/
let { "first-name": firstName } = user;

/* 
   আউটপুট চেক করা:
   এখন আমরা 'firstName' ভেরিয়েবলটি ব্যবহার করতে পারবো।
*/
console.log(firstName); // আউটপুট: Harsh

