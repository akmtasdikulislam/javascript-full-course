// Q4. Given a dynamic key let key = "age", how will you access user[key]?

/* 
   প্রশ্ন ৪: যদি একটি ডাইনামিক কি (dynamic key) ভেরিয়েবলে থাকে, তবে সেটি ব্যবহার করে অবজেক্টের ভ্যালু কীভাবে অ্যাক্সেস করবেন?
   উদ্দেশ্য: ভেরিয়েবলের মাধ্যমে অবজেক্ট প্রপার্টি অ্যাক্সেস করার পদ্ধতি (Bracket Notation) শেখা।
*/

/* 
   এখানে 'key' নামে একটি ভেরিয়েবল নেওয়া হয়েছে যার ভ্যালু "age"। 
   আমরা যখন জানি না ভবিষ্যতে কোন কি (key) অ্যাক্সেস করতে হবে, তখন এভাবে ভেরিয়েবল ব্যবহার করি।
*/
let key = "age";

/* 
   'user' একটি অবজেক্ট যেখানে 'name' এবং 'age' প্রপার্টি আছে।
*/
const user = {
  name: "Harsh",
  age: 26,
};

/* 
   অ্যাক্সেস করার নিয়ম:
   যখন আমরা user[key] লিখি, জাভাস্ক্রিপ্ট দেখে 'key' ভেরিয়েবলের ভেতর কী ভ্যালু আছে। 
   যেহেতু key = "age", তাই এটি user["age"] হিসেবে কাজ করে এবং ২৬ রিটার্ন করে।
   এখানে ডট নোটেশন (user.key) কাজ করবে না, কারণ ডট নোটেশন সরাসরি 'key' নামে কোনো প্রপার্টি খুঁজবে।
*/
console.log(user[key]);

/* 
   এখন আমরা 'key' ভেরিয়েবলের ভ্যালু পরিবর্তন করে "name" করে দিচ্ছি। 
   এটিই হলো ডাইনামিক অ্যাক্সেসের সুবিধা—একই কোড ব্যবহার করে ভিন্ন ভিন্ন প্রপার্টি দেখা সম্ভব।
*/
key = "name";

/* 
   এখন user[key] লিখলে জাভাস্ক্রিপ্ট 'key' এর বর্তমান ভ্যালু "name" ব্যবহার করবে। 
   ফলে আউটপুট আসবে "Harsh"।
*/
console.log(user[key]);

/* 
   সারসংক্ষেপ: 
   ১. যদি কি (key) কোনো ভেরিয়েবলের মধ্যে থাকে, তবে অবশ্যই ব্র্যাকেট নোটেশন [] ব্যবহার করতে হবে।
   ২. user.key লিখলে সেটি অবজেক্টের ভেতরে সরাসরি 'key' নামের প্রপার্টি খুঁজবে (যা এখানে নেই)।
   ৩. user[key] লিখলে সেটি ভেরিয়েবলের মান (যেমন: "age" বা "name") অনুযায়ী প্রপার্টি খুঁজবে।
*/
