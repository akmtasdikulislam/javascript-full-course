// Q9. Use for-in to log all keys in this object:

/* 
   প্রশ্ন ৯: for-in লুপ ব্যবহার করে নিচের অবজেক্টের সকল 'কি' (key) প্রিন্ট করুন।
   উদ্দেশ্য: অবজেক্টের ভেতর দিয়ে লুপ চালিয়ে তার প্রপার্টিগুলো খুঁজে বের করার পদ্ধতি শেখা।
*/

/* 
   এখানে 'obj' নামে একটি অবজেক্ট তৈরি করা হয়েছে। 
   এতে 'title' এবং 'duration' নামে দুটি প্রপার্টি বা কি (key) আছে।
*/
const obj = {
  title: "JavaScript",
  duration: "4 week",
};

/* 
   for-in লুপের ব্যবহার:
   ১. জাভাস্ক্রিপ্টে অবজেক্টের প্রপার্টিগুলোর ওপর ইটারেট (একটির পর একটি চেক করা) করার জন্য for-in লুপ ব্যবহার করা হয়।
   
   ২. 'let key in obj' এর মানে হলো— 'obj' অবজেক্টের ভেতরে যতগুলো কি (key) আছে, 
      লুপটি প্রতিবার ঘোরার সময় একটি করে কি-এর নাম 'key' নামক ভেরিয়েবলে রাখবে।
   
   ৩. প্রথমবার লুপটি ঘুরলে 'key' এর মান হবে "title", দ্বিতীয়বার হবে "duration"।
*/
for (let key in obj) {
  /* 
     এখানে console.log(key) ব্যবহার করে আমরা অবজেক্টের কি-গুলোর নাম আউটপুট হিসেবে দেখছি।
     যদি আমরা কি-এর ভ্যালু দেখতে চাইতাম, তবে obj[key] ব্যবহার করতে হতো।
  */
  console.log(key);
}

/* 
   সারসংক্ষেপ: 
   - for-in লুপ মূলত অবজেক্টের কি (key) গুলো বের করার জন্য ডিজাইন করা হয়েছে।
   - এটি অবজেক্টের প্রতিটি প্রপার্টির ওপর দিয়ে ক্রমান্বয়ে যায় যতক্ষণ না সব শেষ হয়।
*/
