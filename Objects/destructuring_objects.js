/* 
   অবজেক্ট ডিস্ট্রাকচারিং (Object Destructuring): 
   এটি জাভাস্ক্রিপ্টের একটি আধুনিক সিনট্যাক্স যার মাধ্যমে অবজেক্টের ভেতর থেকে 
   নির্দিষ্ট প্রপার্টিগুলোকে বের করে সরাসরি ভ্যারিয়েবল হিসেবে ব্যবহার করা যায়। 
   এর ফলে বারবার 'object.property' লিখতে হয় না।
*/

/* এখানে একটি নেস্টেড অবজেক্ট 'user' তৈরি করা হয়েছে যার ভেতরে ঠিকানা ও লোকেশন আছে। */
const user = {
  name: "Harsh",
  address: {
    city: "Bhopal",
    pin: 462001,
    location: {
      lat: 23.2,
      lng: 77.4,
    },
  },
};

/* 
   ডিস্ট্রাকচারিং করার প্রক্রিয়া:
   ১. এখানে 'user.address.location' অবজেক্টের ভেতরে থাকা 'lat' এবং 'lng' প্রপার্টি দুটিকে টার্গেট করা হয়েছে।
   ২. বাম পাশে কার্লি ব্রেসেস { lat, lng } ব্যবহার করে ওই একই নামের দুটি নতুন ভ্যারিয়েবল তৈরি করা হয়েছে।
   ৩. জাভাস্ক্রিপ্ট স্বয়ংক্রিয়ভাবে 'user.address.location.lat' এর মান 'lat' ভ্যারিয়েবলে 
      এবং 'user.address.location.lng' এর মান 'lng' ভ্যারিয়েবলে অ্যাসাইন করে দিবে।
   
   কেন এটি ব্যবহার করা হয়? 
   কোডকে আরও পরিষ্কার (clean) এবং পঠনযোগ্য (readable) করার জন্য এটি ব্যবহার করা হয়, 
   যাতে বারবার লম্বা চেইন (user.address.location...) লিখতে না হয়।
*/
let { lat, lng } = user.address.location;
