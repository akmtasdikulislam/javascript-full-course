/* 
   এখানে 'person' নামে একটি অবজেক্ট তৈরি করা হয়েছে। 
   এতে 'name' এবং 'age' নামে দুটি প্রপার্টি বা কি (key) আছে।
*/
let person = {
  name: "Harsh",
  age: 26,
};

/* 
   ডট নোটেশন (Dot Notation): 
   অবজেক্টের প্রপার্টি অ্যাক্সেস করার সবচেয়ে সহজ উপায় হলো ডট (.) ব্যবহার করা। 
   এখানে 'person.name' দিয়ে অবজেক্টের ভেতর থেকে 'name' এর ভ্যালু বের করে আনা হয়েছে।
*/
console.log(person.name);

/* 
   একইভাবে 'person.age' ব্যবহার করে বয়স (age) প্রিন্ট করা হয়েছে।
*/
console.log(person.age);

/* 
   ব্র্যাকেট নোটেশন (Bracket Notation): 
   থার্ড ব্র্যাকেট [] এবং স্ট্রিং ব্যবহার করেও প্রপার্টি অ্যাক্সেস করা যায়। 
   এটি তখন বেশি কার্যকর যখন কি (key) এর নাম কোনো ভ্যারিয়েবলে থাকে বা স্পেস থাকে।
*/
console.log(person["name"]);

/* 
   এখানে 'age' কি-টিকে স্ট্রিং হিসেবে ব্র্যাকেটের ভেতর লিখে ভ্যালু রিড করা হয়েছে।
*/
console.log(person["age"]);

/* 
   'newKey' নামে একটি ভ্যারিয়েবল নেওয়া হয়েছে যার ভ্যালু হলো "gender"। 
   আমরা চাই এই ভ্যারিয়েবলের ভ্যালুটিকে অবজেক্টের একটি 'key' হিসেবে ব্যবহার করতে।
*/
let newKey = "gender";

/* 
   ডট নোটেশন দিয়ে ডাইনামিক কি (Dynamic Key) যোগ করার চেষ্টা:
   যদি আমরা 'person.newKey' লিখি, তবে জাভাস্ক্রিপ্ট অবজেক্টে সরাসরি 'newKey' নামেই একটি প্রপার্টি খুঁজবে। 
   এটি ভ্যারিয়েবলের ভেতর থাকা "gender" ভ্যালুটিকে কি (key) হিসেবে নিবে না।
*/
person.newKey = "male";

/* 
   আউটপুট দেখলে বোঝা যাবে যে অবজেক্টে 'newKey: "male"' যোগ হয়েছে, 'gender: "male"' নয়।
*/
console.log(person);

/* 
   ব্র্যাকেট নোটেশন দিয়ে ডাইনামিক কি (Dynamic Key) যোগ করা:
   যখন আমরা 'person[newKey]' লিখি, তখন জাভাস্ক্রিপ্ট প্রথমে 'newKey' ভ্যারিয়েবলের মান (যা হলো "gender") দেখে। 
   এরপর সেই মানটিকে অবজেক্টের কি (key) হিসেবে সেট করে। 
   ডাইনামিক প্রপার্টি অ্যাড করার জন্য এটিই সঠিক পদ্ধতি।
*/
person[newKey] = "male";

/* 
   এখন আউটপুট দেখলে দেখা যাবে অবজেক্টে 'gender: "male"' সফলভাবে যোগ হয়েছে। 
   নোট: এখানে console.log(obj) এর বদলে console.log(person) ব্যবহার করা হয়েছে কারণ 
   আমাদের অবজেক্টের নাম 'person'।
*/
console.log(person);
