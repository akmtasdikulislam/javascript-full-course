/* 
   জাভাস্ক্রিপ্টে অবজেক্ট কপি করার প্রয়োজনীয়তা অনেক। 
   সরাসরি 'let obj2 = obj' লিখলে সেটি শুধু রেফারেন্স কপি করে, নতুন অবজেক্ট তৈরি করে না। 
   নিচে অবজেক্ট কপি করার আধুনিক এবং কার্যকর পদ্ধতিগুলো দেখানো হলো।
*/

let obj = {
  name: "harsh",
  age: 26,
  email: "test@test.com",
};

/* 
   স্প্রেড অপারেটর (Spread Operator - ...) ব্যবহার করে কপি:
   এটি ES6 এর একটি জনপ্রিয় ফিচার। তিনটি ডট (...) দিয়ে একটি অবজেক্টের সব প্রপার্টি 
   খুলে অন্য একটি নতুন অবজেক্টের ভেতরে বসিয়ে দেওয়া হয়। 
   একে 'Shallow Copy' বলা হয়, কারণ এটি একটি নতুন মেমোরি লোকেশনে অবজেক্টটি তৈরি করে।
*/
let obj2 = { ...obj };

/* 
   এখানে 'obj2' প্রিন্ট করলে 'obj' এর হুবহু একটি কপি দেখা যাবে। 
   এখন যদি আমরা 'obj2' এর কোনো মান পরিবর্তন করি, তবে মূল 'obj' এ কোনো প্রভাব পড়বে না।
*/
console.log(obj2);

/* 
   Object.assign() মেথড ব্যবহার করে কপি:
   এটি একটি পুরনো কিন্তু শক্তিশালী পদ্ধতি। এটি প্রথম প্যারামিটার হিসেবে একটি 
   টার্গেট অবজেক্ট (এখানে খালি অবজেক্ট {}) নেয় এবং পরের প্যারামিটার (obj) থেকে 
   সব প্রপার্টি সেই টার্গেট অবজেক্টে কপি করে দেয়।
*/
let obj3 = Object.assign({}, obj);

/* 
   এখানে 'obj3' ও 'obj' এর একটি আলাদা কপি হিসেবে কাজ করবে। 
   এটিও মূলত 'Shallow Copy' তৈরি করার জন্য ব্যবহৃত হয়।
*/
console.log(obj3);

/* 
   Object.assign() এর মাধ্যমে নতুন প্রপার্টিসহ কপি:
   এই পদ্ধতিতে আমরা কপি করার সময় নতুন কোনো প্রপার্টিও যোগ করতে পারি। 
   এখানে প্রথম প্যারামিটারে একটি অবজেক্ট দেওয়া হয়েছে যাতে 'gender: "male"' আছে। 
   এরপর 'obj' এর সব ডাটা এর সাথে এসে যুক্ত হবে।
*/
let obj4 = Object.assign({ gender: "male" }, obj);

/* 
   আউটপুট হবে: { gender: "male", name: "harsh", age: 26, email: "test@test.com" }
   অর্থাৎ কপি হওয়ার পাশাপাশি নতুন তথ্যও যুক্ত হয়েছে।
*/
console.log(obj4);

/* 
   সতর্কতা: 
   উপরে দেখানো সবকটি পদ্ধতিই 'Shallow Copy' করে। 
   এর মানে হলো, যদি অবজেক্টের ভেতরে আরও একটি নেস্টেড অবজেক্ট থাকে, 
   তবে সেই ভেতরের অবজেক্টটি কিন্তু রেফারেন্স হিসেবেই থেকে যাবে। 
   পুরোপুরি আলাদা কপি (Deep Copy) করতে হলে JSON.parse(JSON.stringify(obj)) 
   অথবা আধুনিক structuredClone() ব্যবহার করতে হয়।
*/
