/* 
   জাভাস্ক্রিপ্ট অবজেক্ট (Objects) হলো এমন একটি ডাটা স্ট্রাকচার যা 'key-value pair' হিসেবে তথ্য জমা রাখে।
*/

/* 
   নিচের লাইনটিতে একটি খালি অবজেক্ট (Empty Object) তৈরি করা হয়েছে।
   'let obj = {}' এর মাধ্যমে মেমোরিতে একটি জায়গা তৈরি হয় যেখানে ভবিষ্যতে প্রপার্টি যোগ করা যাবে।
   এটি অবজেক্ট লিটারেল (Object Literal) সিনট্যাক্স।
*/
let obj = {};

/* 
   এখানে 'person' নামে একটি অবজেক্ট তৈরি করা হয়েছে যার ভেতরে কিছু তথ্য আছে।
   এটি বাস্তব জীবনের কোনো ব্যক্তি বা বস্তুর বৈশিষ্ট্য প্রকাশ করার জন্য ব্যবহৃত হয়।
*/
let person = {
  /* 
     'name' হলো একটি কি (key) এবং "Harsh" হলো তার ভ্যালু (value)।
     এটি ব্যক্তির নাম সংরক্ষণ করছে। স্ট্রিং হওয়ার কারণে এটি কোটেশনের ভেতরে রাখা হয়েছে।
  */
  name: "Harsh",

  /* 
     'age' হলো আরেকটি কি (key) যা ব্যক্তির বয়স নির্দেশ করে।
     যেহেতু ২৬ একটি সংখ্যা (Number), তাই এখানে কোনো কোটেশন ব্যবহার করা হয়নি।
     প্রতিটি প্রপার্টির পর কমা (,) দিয়ে আলাদা করতে হয়।
  */
  age: 26,
};
