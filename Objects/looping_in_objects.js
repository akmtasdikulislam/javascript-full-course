/* 
   জাভাস্ক্রিপ্টে অবজেক্টের ওপর লুপ চালানোর জন্য 'for...in' লুপ ব্যবহার করা হয়। 
   এটি অবজেক্টের প্রতিটি 'key' বা প্রপার্টির ওপর দিয়ে একে একে ঘুরে আসে।
*/
let obj = {
  name: "harsh",
  age: 26,
  email: "test@test.com",
};

/* 
   'for...in' লুপের গঠন:
   এখানে 'key' হলো একটি ভ্যারিয়েবল যা প্রতিবার লুপ ঘোরার সময় অবজেক্টের একটি করে কি (যেমন: name, age) ধারণ করবে।
   'obj' হলো সেই অবজেক্ট যার ওপর আমরা লুপ চালাচ্ছি।
*/
for (let key in obj) {
  /* 
     এখানে 'key' ভ্যারিয়েবলটি প্রিন্ট করলে আমরা অবজেক্টের কি-গুলোর নাম পাবো।
     আর 'obj[key]' ব্যবহার করে ওই কি-এর বিপরীতে থাকা ভ্যালু বা মানটি পাওয়া যাবে।
     
     কেন ব্র্যাকেট নোটেশন [key] ব্যবহার করা হয়েছে?
     কারণ 'key' এখানে একটি ভ্যারিয়েবল। লুপের ভেতর ডাইনামিক ভ্যালু পাওয়ার জন্য 
     সবসময় ব্র্যাকেট নোটেশন ব্যবহার করতে হয়।
  */
  console.log(key, obj[key]);

  /* 
     নিচের কমেন্ট করা লাইনটি লক্ষ্য করুন:
     console.log(key, obj.key);
     
     আমরা যদি 'obj.key' লিখি, তবে জাভাস্ক্রিপ্ট অবজেক্টের ভেতরে সরাসরি 'key' নামে কোনো প্রপার্টি খুঁজবে।
     যেহেতু আমাদের অবজেক্টে 'key' নামে কোনো প্রপার্টি নেই (আছে name, age, email), 
     তাই এটি 'undefined' রিটার্ন করবে। 
     
     মনে রাখতে হবে: ডট নোটেশন (.) ভ্যারিয়েবল সাপোর্ট করে না, এটি সরাসরি নাম খোঁজে। 
     তাই লুপের ভেতরে ভ্যালু পেতে হলে অবশ্যই ব্র্যাকেট নোটেশন ব্যবহার করতে হবে।
  */

  /* 
     আউটপুট হবে:
     name harsh
     age 26
     email test@test.com
  */
}

/* 
   'Object.keys()' মেথড ব্যবহার:
   এই মেথডটি একটি অবজেক্টকে ইনপুট হিসেবে নেয় এবং ওই অবজেক্টের সবকটি 'key' বা প্রপার্টির নাম নিয়ে 
   একটি অ্যারে (Array) তৈরি করে দেয়। এটি তখন কাজে লাগে যখন আমাদের শুধু কি-গুলোর তালিকা দরকার হয়।
*/
let keys = Object.keys(obj);

/* 
   এখানে 'keys' ভ্যারিয়েবলটি প্রিন্ট করলে আমরা ['name', 'age', 'email'] এই অ্যারেটি দেখতে পাবো।
*/
console.log(keys);

/* 
   'Object.entries()' মেথড ব্যবহার:
   এই মেথডটি অবজেক্টের প্রতিটি কি-ভ্যালু পেয়ারকে (key-value pair) একেকটি ছোট অ্যারেতে রূপান্তর করে 
   এবং সবশেষে একটি বড় অ্যারের ভেতরে সেগুলো সাজিয়ে রাখে (Array of Arrays)। 
   এটি ডাটা ম্যানিপুলেশন বা ম্যাপ করার জন্য খুবই শক্তিশালী একটি টুল।
*/
let entries = Object.entries(obj);

/* 
   এখানে 'entries' প্রিন্ট করলে আউটপুট হবে: [ ['name', 'harsh'], ['age', 26], ['email', 'test@test.com'] ]
   অর্থাৎ প্রতিটি এন্ট্রি এখন একটি আলাদা ইনডেক্সড অ্যারে।
*/
console.log(entries);
