/* 
   জাভাস্ক্রিপ্টে একটি অবজেক্টের ভেতরে অন্য একটি অবজেক্ট থাকাকে 'Nesting' বলা হয়। 
   এখানে 'user' একটি অবজেক্ট যার ভেতরে 'address' এবং তার ভেতরে 'location' নামে আরও অবজেক্ট আছে।
*/
const user = {
  /* 'name' হলো একটি সাধারণ প্রপার্টি */
  name: "Harsh",

  /* 'address' হলো একটি নেস্টেড অবজেক্ট (Nested Object) */
  address: {
    city: "Bhopal",
    pin: 462001,

    /* 'location' হলো address অবজেক্টের ভেতরে আরও একটি নেস্টেড অবজেক্ট */
    location: {
      lat: 23.2,
      lng: 77.4,
    },
  },
};

/* 
   ডিপ অ্যাক্সেস (Deep Access): 
   যখন আমাদের অনেক গভীরে থাকা কোনো ডাটা প্রয়োজন হয়, তখন আমরা ডট নোটেশন (.) ব্যবহার করে 
   ধাপে ধাপে ভেতরে প্রবেশ করি। 
*/

/* 
   নিচের লাইনে যা ঘটছে:
   ১. user অবজেক্টে প্রবেশ করা হয়েছে।
   ২. user এর ভেতর থেকে address অবজেক্টে যাওয়া হয়েছে।
   ৩. address এর ভেতর থেকে location অবজেক্টে যাওয়া হয়েছে।
   ৪. সবশেষে location এর ভেতর থেকে 'lng' কি-এর ভ্যালু বের করা হয়েছে।
*/
console.log(user.address.location.lng);

/* 
   সতর্কতা: 
   ডিপ অ্যাক্সেস করার সময় যদি মাঝখানের কোনো একটি কি (যেমন address বা location) 
   অবজেক্টে না থাকে বা undefined হয়, তবে জাভাস্ক্রিপ্ট এরর (Error) দিবে। 
   তাই বড় প্রজেক্টে কাজ করার সময় এগুলো সাবধানে হ্যান্ডেল করতে হয়।
*/

