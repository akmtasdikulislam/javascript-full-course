/* 
   অপশনাল চেইনিং (Optional Chaining - ?.):
   জাভাস্ক্রিপ্টে যখন আমরা কোনো নেস্টেড অবজেক্টের গভীরে থাকা ডাটা অ্যাক্সেস করতে চাই, 
   তখন যদি মাঝখানের কোনো প্রপার্টি না থাকে (undefined বা null হয়), তবে কোড এরর দেয়। 
   এই এরর হ্যান্ডেল করার সহজ উপায় হলো অপশনাল চেইনিং।
*/

/* এখানে একটি সাধারণ অবজেক্ট 'obj' তৈরি করা হয়েছে যার ভেতরে 'address' অবজেক্ট আছে। */
let obj = {
  name: "harsh",
  age: 26,
  email: "test@test.com",
  address: {
    city: "Bhopal", // এখানে 'address' এর ভেতরে 'city' বিদ্যমান।
  },
};

/* 
   এখানে 'obj?.address?.city' ব্যবহার করা হয়েছে। 
   এর মানে হলো: জাভাস্ক্রিপ্ট প্রথমে চেক করবে 'obj' আছে কি না, তারপর চেক করবে 'address' আছে কি না। 
   যদি সব থাকে, তবেই সে 'city' এর মান (Bhopal) প্রিন্ট করবে। 
   যদি কোনো একটি অংশ না থাকতো, তবে এরর না দিয়ে সে 'undefined' রিটার্ন করতো।
*/
console.log(obj?.address?.city);

/* 
   এখানে দ্বিতীয় একটি অবজেক্ট 'obj2' তৈরি করা হয়েছে। 
   লক্ষ্য করুন, এখানে কি (key) এর নাম 'addresses' (বহুবচন), কিন্তু আগেরটিতে ছিল 'address'।
*/
let obj2 = {
  name: "harsh",
  age: 26,
  email: "test@test.com",
  addresses: {
    city: "Bhopal",
  },
};

/* 
   এখানে 'obj2' অবজেক্টে 'address' কি-টি নেই (আছে 'addresses')। 
   যদি আমরা সাধারণ ডট নোটেশন (obj2.address.city) ব্যবহার করতাম, 
   তবে জাভাস্ক্রিপ্ট "TypeError" দিতো এবং পুরো প্রোগ্রামটি ক্র্যাশ করতো।
   
   কিন্তু '?. ' ব্যবহার করার ফলে:
   ১. জাভাস্ক্রিপ্ট দেখছে 'obj2.address' হলো undefined।
   ২. সে আর সামনের দিকে (city প্রপার্টিতে) যাওয়ার চেষ্টা করবে না।
   ৩. এরর না দিয়ে সরাসরি 'undefined' আউটপুট দিবে।
   
   এটি কোডকে নিরাপদ রাখে যাতে অ্যাপ মাঝপথে বন্ধ না হয়ে যায়।
*/
console.log(obj2?.address?.city);

/* 
   সারসংক্ষেপ: 
   ?. চিহ্নটি একটি গার্ড বা পাহারাদারের মতো কাজ করে। 
   এটি চেক করে যে বাম পাশের অংশটি null বা undefined কি না। 
   যদি হয়, তবে সে ডান পাশের অংশটি আর রান করে না। 
   বড় প্রজেক্টে যেখানে API থেকে ডাটা আসে এবং কোনো প্রপার্টি মিসিং থাকতে পারে, 
   সেখানে এটি অত্যন্ত প্রয়োজনীয়।
*/
