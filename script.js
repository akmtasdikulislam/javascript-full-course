/* 
   এখানে 'a' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে গ্লোবাল স্কোপে (Global Scope)।
   'let' ব্যবহার করার ফলে এটি এই ব্লকের বাইরে সব জায়গায় অ্যাক্সেসযোগ্য থাকবে।
*/
let a = 10;

{
  /* 
     এটি একটি ব্লক স্কোপ (Block Scope)। 
     'let' কিওয়ার্ডের বৈশিষ্ট্য হলো এটি ব্লক-লেভেল স্কোপ তৈরি করে। 
     তাই এখানে নতুন করে 'a' ডিক্লেয়ার করলে তা বাইরের 'a' এর মান পরিবর্তন করবে না, 
     বরং এই ব্লকের ভেতরে একটি আলাদা লোকাল ভেরিয়েবল হিসেবে কাজ করবে।
  */
  let a = 20;

  /* 
     এখানে কনসোলে ২০ (20) প্রিন্ট হবে। 
     কারণ জাভাস্ক্রিপ্ট প্রথমে তার বর্তমান স্কোপে ভেরিয়েবলটি খোঁজে (Shadowing)।
  */
  console.log(a);
}

/* 
   এখানে কনসোলে ১০ (10) প্রিন্ট হবে। 
   কারণ ব্লকের ভেতরে থাকা 'a = 20' শুধুমাত্র ওই ব্লকের জন্যই সীমাবদ্ধ ছিল। 
   ব্লকের বাইরে আসায় আমরা আবার গ্লোবাল স্কোপের 'a' এর মানটি দেখতে পাচ্ছি।
*/
console.log(a);
