/* 
   এখানে একটি অবজেক্ট (Object) তৈরি করা হয়েছে 'const' কিওয়ার্ড ব্যবহার করে। 
   'const' দিয়ে ডিক্লেয়ার করার মানে হলো 'person' ভেরিয়েবলটি অন্য কোনো নতুন ভ্যালু বা অবজেক্টে পুনরায় অ্যাসাইন করা যাবে না।
*/
const person = {
  name: "Harsh",
};

/* 
   যদিও অবজেক্টটি 'const' দিয়ে তৈরি, তবুও আমরা এর ভিতরের প্রোপার্টি (যেমন: name) পরিবর্তন করতে পারি। 
   একে বলা হয় 'Mutation'। এখানে "Harsh" পরিবর্তন করে "Sharma" করা হচ্ছে। 
   এটি জাভাস্ক্রিপ্টে বৈধ কারণ আমরা ভেরিয়েবলটি পরিবর্তন করছি না, শুধু অবজেক্টের ভিতরের ডাটা পরিবর্তন করছি।
*/
person.name = "Sharma";

/* 
   নিচের এই লাইনটি একটি এরর (Error) তৈরি করবে। 
   কারণ 'person' ভেরিয়েবলটি 'const' দিয়ে ডিফাইন করা হয়েছে, তাই আমরা একে সম্পূর্ণ নতুন একটি খালি অবজেক্ট '{}' 
   হিসেবে পুনরায় অ্যাসাইন (Re-assign) করতে পারবো না। 
   জাভাস্ক্রিপ্ট এখানে 'TypeError: Assignment to constant variable' দেখাবে।
*/
person = {};
