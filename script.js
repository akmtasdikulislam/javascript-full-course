/* 
   Type Coercion: এটি এমন একটি প্রক্রিয়া যেখানে জাভাস্ক্রিপ্ট নিজে থেকেই একটি ডাটা টাইপকে অন্য টাইপে রূপান্তর করে। 
*/

/* 
   নিচের লাইনে সংখ্যা ১ এবং স্ট্রিং '১' যোগ করা হচ্ছে। 
   যেহেতু এখানে প্লাস (+) অপারেটর আছে এবং একটি অপারেন্ড স্ট্রিং, 
   তাই জাভাস্ক্রিপ্ট সংখ্যা ১-কে স্ট্রিং-এ রূপান্তর করবে এবং কনক্যাটিনেশন (জোড়া লাগানো) করবে। 
   ফলাফল হবে '11'।
*/
console.log(1+'1');    // '11'

/* 
   প্লাস (+) অপারেটর দুটি কাজ করতে পারে: গাণিতিক যোগফল অথবা স্ট্রিং জোড়া লাগানো। 
   যদি কোনো একটি অংশ স্ট্রিং হয়, তবে এটি সবসময় জোড়া লাগানোর কাজকে প্রাধান্য দেয়।
*/

/* 
   নিচের লাইনে স্ট্রিং '৫' থেকে সংখ্যা ১ বিয়োগ করা হচ্ছে। 
   যেহেতু বিয়োগ (-) অপারেটর শুধুমাত্র গাণিতিক বিয়োগ করতে পারে এবং স্ট্রিং-এর সাথে এর কোনো কাজ নেই, 
   তাই জাভাস্ক্রিপ্ট স্ট্রিং '৫'-কে সংখ্যা ৫-এ রূপান্তর করে নেয়। 
   ফলাফল হবে ৪।
*/
console.log('5' - 1);    // 4

/* 
   সারসংক্ষেপ: 
   ১. প্লাস (+) অপারেটর থাকলে এবং স্ট্রিং থাকলে সেটি কনক্যাটিনেশন করে।
   ২. মাইনাস (-), গুণ (*), বা ভাগ (/) অপারেটর থাকলে জাভাস্ক্রিপ্ট স্ট্রিং-কে সংখ্যায় রূপান্তর করার চেষ্টা করে, 
      কারণ এই অপারেটরগুলো স্ট্রিং-এর ওপর কাজ করে না।
*/