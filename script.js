// ### **References**

// When these values are copied, we don’t get the actual copy of them, we only get reference to them.

/* 
   এখানে 'a' নামে একটি অ্যারে তৈরি করা হয়েছে। 
   মেমোরিতে এই অ্যারেটি একটি নির্দিষ্ট জায়গায় জমা থাকে। 
*/
let a = [1, 2, 3];

/* 
   এখানে 'b = a' করার মানে হলো, আমরা 'a' এর ভেতরের ভ্যালুগুলো কপি করছি না। 
   বরং 'a' যে মেমোরি অ্যাড্রেস বা রেফারেন্স নির্দেশ করছে, 'b' কেও সেই একই ঠিকানায় পাঠিয়ে দিচ্ছি। 
   এখন 'a' এবং 'b' উভয়ই একই অ্যারেকে নির্দেশ করছে। 
*/
let b = a;

/* 
   'b.pop()' মেথডটি অ্যারের শেষ উপাদানটি মুছে ফেলে। 
   যেহেতু 'a' এবং 'b' একই রেফারেন্স শেয়ার করছে, তাই 'b' এর মাধ্যমে পরিবর্তন করলে 
   সেটি মূল অ্যারেতেই পরিবর্তন ঘটায়। 
*/
b.pop();

/* 
   এখানে 'a' প্রিন্ট করলে দেখা যাবে আউটপুট [1, 2]। 
   কারণ 'b' এর মাধ্যমে করা পরিবর্তনটি 'a' এর ওপরও প্রভাব ফেলেছে। 
*/
console.log(a);

/* 
   এখানে 'b' প্রিন্ট করলেও আউটপুট [1, 2] আসবে। 
   কারণ তারা দুজনেই মেমোরির একই ডেটা বা রেফারেন্স পয়েন্ট করে আছে। 
*/
console.log(b);
