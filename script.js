/* 
   JavaScript-এ Primitives হলো এমন ডাটা টাইপ যা ইমিউটেবল (পরিবর্তনযোগ্য নয়) 
   এবং সরাসরি মেমোরিতে ভ্যালু হিসেবে সেভ হয়।
*/

/* String: টেক্সট ডাটা রাখার জন্য ব্যবহৃত হয়। ডাবল কোট, সিঙ্গেল কোট বা ব্যাকটিক দিয়ে এটি লেখা যায়। */
let str1 = "harsh";
let str2 = "harsh";
let str3 = `harsh`; /* ব্যাকটিক (Template Literal) ডাইনামিক ভ্যালু বা মাল্টি-লাইন স্ট্রিংয়ের জন্য ভালো। */

/* Numbers: পূর্ণসংখ্যা (Integer) এবং দশমিক সংখ্যা (Float) উভয়ই 'number' টাইপের অন্তর্ভুক্ত। */
let num1 = 12; /* এটি একটি পূর্ণসংখ্যা। */
let num2 = 13.4564; /* এটি একটি ফ্লোটিং পয়েন্ট বা দশমিক সংখ্যা। */

/* Booleans: এটি শুধুমাত্র দুটি ভ্যালু নিতে পারে: true (সত্য) অথবা false (মিথ্যা)। লজিক্যাল ডিসিশনের জন্য এটি ব্যবহৃত হয়। */
let a = true; 
let b = false; 

/* NULL: এটি ইচ্ছাকৃতভাবে কোনো ভেরিয়েবলকে 'খালি' বা 'শূন্য' বোঝাতে ব্যবহৃত হয়। এটি একটি অবজেক্ট টাইপ হিসেবে দেখায় (JS bug)। */
let c = null; 

/* 
   undefined: যখন একটি ভেরিয়েবল ডিক্লেয়ার করা হয় কিন্তু কোনো ভ্যালু অ্যাসাইন করা হয় না, 
   তখন জাভাস্ক্রিপ্ট নিজে থেকেই তাকে undefined সেট করে দেয়। 
*/
let d; 
console.log(d); /* আউটপুট আসবে undefined কারণ এতে কোনো মান নেই। */

/* 
   Symbol: এটি একটি ইউনিক (অনন্য) এবং ইমিউটেবল ডাটা টাইপ। 
   একই ডেসক্রিপশন থাকলেও দুটি সিম্বল কখনো সমান হয় না। 
*/
let u1 = Symbol("uid"); 
let u2 = Symbol("uid"); 

/* এখানে u1 এবং u2 এর ভ্যালু "uid" হলেও তারা মেমোরিতে আলাদা আইডেন্টিটি বহন করে। */
console.log(u1 === u2); // false (কারণ সিম্বল সবসময় ইউনিক হয়)

/* একটি অবজেক্ট তৈরি করা হচ্ছে যেখানে বিভিন্ন প্রপার্টি আছে। */
let obj = {
  uid: 1,
  name: "Harsh",
  age: 22,
  email: "test@test.com",
};

/* 
   সিম্বলকে অবজেক্টের কী (Key) হিসেবে ব্যবহার করা হচ্ছে। 
   এর সুবিধা হলো এটি অন্য কোনো প্রপার্টির সাথে কলিশন (ধাক্কা) খাবে না এবং এটি সহজে ইটারেট করা যায় না। 
*/
let u3 = Symbol("uid"); 
obj[u3] = "001"; 

console.log(obj); /* অবজেক্টটি প্রিন্ট করলে দেখা যাবে 'uid' এবং Symbol(uid) আলাদাভাবে আছে। */

/* 
   BigInt: জাভাস্ক্রিপ্টে সাধারণ Number টাইপ 2^53 - 1 এর চেয়ে বড় সংখ্যা সঠিকভাবে হ্যান্ডেল করতে পারে না। 
   খুব বড় সংখ্যা নিয়ে কাজ করার জন্য BigInt ব্যবহার করা হয়। 
*/

/* এটি জাভাস্ক্রিপ্টের সেফ ইন্টিজার লিমিট (MAX_SAFE_INTEGER)। */
let normalInt = 9007199254740991; 

/* 
   সাধারণ নাম্বারে এই লিমিটের পর যোগ করলে সঠিক রেজাল্ট পাওয়া যায় না (Precision loss)। 
   নিচের আউটপুটগুলো লক্ষ্য করলে দেখা যাবে রেজাল্ট ভুল আসছে। 
*/
console.log(normalInt+3); 
console.log(normalInt+10); 

/* 
   BigInt তৈরি করতে সংখ্যার শেষে 'n' যোগ করতে হয়। 
   এটি অনেক বড় সংখ্যাকেও নির্ভুলভাবে ক্যালকুলেট করতে পারে। 
*/
let bigNum = 9007199254740991n; 
console.log(bigNum); 

/* BigInt এর সাথে শুধুমাত্র অন্য একটি BigInt ই যোগ/বিয়োগ করা সম্ভব। */
console.log(bigNum+5n); 
console.log(bigNum+9n); 
