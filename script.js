/* 
   typeof অপারেটর ব্যবহার করে আমরা যেকোনো ভ্যারিয়েবল বা ভ্যালুর ডাটা টাইপ চেক করতে পারি। 
   এটি আমাদের নিশ্চিত হতে সাহায্য করে যে আমরা কোন ধরণের ডাটা নিয়ে কাজ করছি।
*/
console.log(typeof 12); 
/* 
   এখানে ১২ একটি পূর্ণসংখ্যা, তাই আউটপুট আসবে 'number'। 
   জাভাস্ক্রিপ্টে সব ধরণের সংখ্যাই (integer বা float) 'number' টাইপের হয়।
*/

console.log(typeof "harsh"); 
/* 
   কোটেশনের ভেতরে থাকা যেকোনো টেক্সটকে জাভাস্ক্রিপ্ট 'string' হিসেবে গণ্য করে।
*/

/* 
   নিচে জাভাস্ক্রিপ্টের কিছু অদ্ভুত আচরণ (Quirks) দেওয়া হলো যা ইন্টারভিউতে প্রায়ই জিজ্ঞেস করা হয়:
*/

console.log(typeof null); 
/* 
   আউটপুট: 'object'। এটি জাভাস্ক্রিপ্টের একটি ঐতিহাসিক ভুল (bug)। 
   null আসলে একটি প্রিমিটিভ ভ্যালু হওয়া উচিত ছিল, কিন্তু এটি অবজেক্ট হিসেবে রিটার্ন করে।
*/

console.log(typeof NaN); 
/* 
   আউটপুট: 'number'। NaN এর পূর্ণরূপ হলো 'Not a Number'। 
   মজার বিষয় হলো, গাণিতিক হিসাবের ভুলের কারণে এটি তৈরি হয় বলে জাভাস্ক্রিপ্ট একে 'number' টাইপেই রাখে।
*/

console.log(NaN === NaN); 
/* 
   আউটপুট: false। এটি জাভাস্ক্রিপ্টের একমাত্র ভ্যালু যা নিজের সমান নয়। 
   দুটি অনির্দিষ্ট গাণিতিক ভুল কখনো এক হতে পারে না, তাই এটি false দেয়।
*/

console.log(0.1 + 0.4); 
/* 
   আউটপুট: 0.5। এখানে সাধারণ গণিতের মতোই কাজ করছে।
*/

console.log(0.1 + 0.2); 
/* 
   আউটপুট: 0.30000000000000004। 
   কম্পিউটার বাইনারি সিস্টেমে ফ্লোটিং পয়েন্ট সংখ্যা হিসাব করে, 
   যার ফলে ০.১ এবং ০.২ যোগ করলে সামান্য প্রিসিশন এরর (precision error) দেখা দেয়।
*/

console.log([] + []); 
/* 
   আউটপুট: "" (খালি স্ট্রিং)। 
   যখন আমরা দুটি অ্যারে যোগ করার চেষ্টা করি, জাভাস্ক্রিপ্ট সেগুলোকে ইন্টারনালি স্ট্রিংয়ে রূপান্তর করে ফেলে। 
   যেহেতু অ্যারেগুলো খালি, তাই যোগফল একটি খালি স্ট্রিং হয়।
*/

console.log(1 + "1"); 
/* 
   আউটপুট: "11"। 
   যোগ চিহ্নের (+) একপাশে স্ট্রিং থাকলে জাভাস্ক্রিপ্ট অন্য পাশের সংখ্যাটিকেও স্ট্রিং বানিয়ে ফেলে 
   এবং দুটিকে জোড়া লাগিয়ে দেয় (Concatenation)।
*/

console.log(1 == "1"); 
/* 
   আউটপুট: true। 
   ডাবল ইকুয়াল (==) শুধুমাত্র ভ্যালু চেক করে। এটি চেক করার আগে টাইপ কনভার্সন (Coercion) করে নেয়, 
   তাই স্ট্রিং "1" এবং সংখ্যা 1 সমান দেখায়।
*/

console.log(1 === "1"); 
/* 
   আউটপুট: false। 
   ট্রিপল ইকুয়াল (===) ভ্যালু এবং ডাটা টাইপ—উভয়ই চেক করে (Strict Equality)। 
   যেহেতু একটি সংখ্যা এবং অন্যটি স্ট্রিং, তাই এটি false রিটার্ন করে।
*/
