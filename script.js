/* 
   এখানে 'var' কিওয়ার্ড ব্যবহার করে 'x' ভেরিয়েবলটি ডিক্লেয়ার করা হয়েছে।
   জাভাস্ক্রিপ্টে 'var' ফাংশন-স্কোপড (function-scoped), এটি ব্লক-স্কোপড (block-scoped) নয়।
   অর্থাৎ, কার্লি ব্রেস বা ব্লকের {} ভেতরে 'var' দিয়ে কিছু লিখলে তা বাইরের ভেরিয়েবলকেই পরিবর্তন করে।
*/
var x = 1;

{
  /* 
     যেহেতু 'var' ব্লক স্কোপ মানে না, তাই এই ব্লকের ভেতরের 'x' আসলে 
     বাইরের 'x' কেই নির্দেশ করছে। একে 'Variable Shadowing' বা রি-ডিক্লেয়ারেশন বলা হয়।
     এখানে x এর মান ১ থেকে পরিবর্তন হয়ে ২ হয়ে যাচ্ছে।
  */
  var x = 2;
}

/* 
   আউটপুট ২ আসবে, কারণ ব্লকের ভেতরের পরিবর্তনটি গ্লোবাল বা বাইরের স্কোপে প্রভাব ফেলেছে।
   আধুনিক জাভাস্ক্রিপ্টে এই সমস্যা এড়াতে 'let' বা 'const' ব্যবহার করা হয়।
*/
console.log(x);
