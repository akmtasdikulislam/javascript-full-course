// ### **Primitives**

// The values which are when copied, we get the actual copy of those datas

// Examples: String, Number, Boolean, NULL, undefined, symbol, bigInt

/* 
   এখানে 'a' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং তাতে ১২ (number) অ্যাসাইন করা হয়েছে।
   যেহেতু ১২ একটি প্রিমিটিভ ডাটা টাইপ, এটি মেমোরিতে সরাসরি ভ্যালু হিসেবে জমা থাকে।
*/
let a = 12; 

/* 
   এখানে 'b' ভেরিয়েবলের মধ্যে 'a' এর মান কপি করা হচ্ছে। 
   প্রিমিটিভ টাইপ হওয়ার কারণে, 'b' সরাসরি 'a' এর ভ্যালুটির (১২) একটি আলাদা কপি পায়। 
   এদের মধ্যে কোনো রেফারেন্স বা সংযোগ থাকে না।
*/
let b = a; 

/* 
   এখানে 'a' এর মানের সাথে ২ যোগ করা হচ্ছে (১২ + ২ = ১৪)। 
   এটি শুধুমাত্র 'a' ভেরিয়েবলকেই পরিবর্তন করবে। 
   যেহেতু 'b' এর কাছে আগে থেকেই আলাদা একটি কপি ছিল, তাই 'a' পরিবর্তন হলেও 'b' এর মানের কোনো পরিবর্তন হবে না।
*/
a = a + 2; 

/* 
   কনসোলে 'a' এর মান প্রিন্ট করা হচ্ছে। 
   আউটপুট আসবে ১৪, কারণ আমরা 'a' এর মান আপডেট করেছি।
*/
console.log(a); 

/* 
   কনসোলে 'b' এর মান প্রিন্ট করা হচ্ছে। 
   আউটপুট আসবে ১২। এটি প্রমাণ করে যে প্রিমিটিভ টাইপ কপি করলে তারা একে অপরের থেকে স্বাধীন থাকে।
*/
console.log(b); 