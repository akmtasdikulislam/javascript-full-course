/* 
   Dynamic Typing (ডাইনামিক টাইপিং):
   জাভাস্ক্রিপ্টে ভেরিয়েবলের কোনো নির্দিষ্ট ডাটা টাইপ আগে থেকে ঠিক করে দিতে হয় না। 
   একই ভেরিয়েবলে ভিন্ন ভিন্ন সময়ে ভিন্ন ভিন্ন ধরনের ডাটা রাখা যায়।
*/

/*
   অন্যান্য স্ট্যাটিক টাইপড ল্যাঙ্গুয়েজে (যেমন: C++ বা Java) একবার ভেরিয়েবল ডিক্লেয়ার করলে 
   তার টাইপ পরিবর্তন করা যায় না। কিন্তু জাভাস্ক্রিপ্ট ফ্লেক্সিবল।

 int a = 12;
 a = true;    // ERROR
*/

/* 
   জাভাস্ক্রিপ্টে আমরা যেকোনো সময় একটি ভেরিয়েবলে যেকোনো ধরনের ভ্যালু অ্যাসাইন করতে পারি।
*/

let a = 13;
/* 
   এখানে 'a' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে ১৩ (Number) রাখা হয়েছে।
   যেহেতু এটি প্রথম অ্যাসাইনমেন্ট, জাভাস্ক্রিপ্ট ইঞ্জিন বুঝে নিয়েছে বর্তমানে 'a' এর টাইপ হলো Number।
*/

a = true;
/* 
   এখানে একই 'a' ভেরিয়েবলে আমরা একটি Boolean ভ্যালু (true) রাখছি। 
   জাভাস্ক্রিপ্ট কোনো এরর দিবে না, বরং এটি ডাইনামিক্যালি ভেরিয়েবলের টাইপ পরিবর্তন করে ফেলবে। 
   এখন 'a' এর টাইপ হয়ে যাবে Boolean।
*/

/* 
   সারসংক্ষেপ: 
   জাভাস্ক্রিপ্টে ভেরিয়েবল হলো একটি কন্টেইনারের মতো যার গায়ে কোনো স্থায়ী লেবেল নেই। 
   আপনি এতে যখন যা রাখবেন, ভেরিয়েবলটি সেই টাইপ ধারণ করবে। 
   একেই বলা হয় Dynamic Typing।
*/
