/* 
   এখানে একটি if ব্লক ব্যবহার করা হয়েছে। 
   শর্ত হিসেবে true দেয়া হয়েছে, তাই এই ব্লকের ভেতরের কোড অবশ্যই রান করবে।
*/
if (true) {
  /* 
     'var' দিয়ে ডিক্লেয়ার করা ভেরিয়েবল ফাংশন-স্কোপড বা গ্লোবাল-স্কোপড হয়। 
     ব্লকের ভেতরে ডিক্লেয়ার করলেও এটি ব্লকের বাইরে থেকে অ্যাক্সেস করা যায়।
  */
  var a = 1;

  /* 
     'let' দিয়ে ডিক্লেয়ার করা ভেরিয়েবল ব্লক-স্কোপড হয়। 
     অর্থাৎ এটি শুধুমাত্র এই কার্লি ব্রেসের {} ভেতরেই সীমাবদ্ধ থাকবে।
  */
  let b = 2;
}

/* 
   'a' প্রিন্ট হবে কারণ 'var' ব্লক স্কোপ মানে না, 
   তাই এটি ব্লকের বাইরেও দৃশ্যমান। আউটপুট আসবে: 1
*/
console.log(a);

/* 
   এখানে একটি ReferenceError আসবে। 
   কারণ 'b' কে 'let' দিয়ে ব্লকের ভেতরে ডিফাইন করা হয়েছে, 
   তাই ব্লকের বাইরে থেকে একে খুঁজে পাওয়া সম্ভব নয়।
*/
console.log(b);
