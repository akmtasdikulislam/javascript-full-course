/* 
   নিচে 'continue' কি-ওয়ার্ডের একটি উদাহরণ দেওয়া হয়েছে। 
   'continue' ব্যবহার করা হয় লুপের কোনো একটি নির্দিষ্ট ধাপকে (iteration) বাদ দিয়ে 
   পরের ধাপে চলে যাওয়ার জন্য।
*/

/* 
   ১. for (let i = 0; i < 101; i++)
   এখানে একটি for লুপ শুরু করা হয়েছে যা ০ থেকে ১০০ পর্যন্ত চলার কথা।
   - i = 0: লুপটি ০ থেকে শুরু হবে।
   - i < 101: শর্ত দেওয়া হয়েছে যেন এটি ১০০ পর্যন্ত চলে।
   - i++: প্রতিবার লুপ শেষে i এর মান ১ করে বাড়বে।
*/
for (let i = 0; i < 101; i++) {
  /* 
     ২. if (i === 32)
     লুপের ভেতরে একটি শর্ত দেওয়া হয়েছে। প্রতিবার চেক করা হচ্ছে i এর মান ৩২ কি না।
     যখনই i এর মান ৩২ হবে, তখনই এই if ব্লকের ভেতরের কোড কাজ করবে।
  */
  if (i === 32) {
    /* 
       ৩. continue;
       এটিই হলো মূল কি-ওয়ার্ড। যখন i এর মান ৩২ হয়, তখন 'continue' কমান্ডটি 
       লুপের বর্তমান ধাপটি এখানেই থামিয়ে দেয় এবং নিচের কোডগুলো (console.log) 
       না চালিয়ে সরাসরি লুপের পরের ধাপে (i = 33) চলে যায়।
    */
    continue;
  }
  /* 
     ৪. console.log(i);
     এটি i এর মান প্রিন্ট করে। যেহেতু ৩২ এর সময় 'continue' ব্যবহার করা হয়েছে, 
     তাই ৩২ সংখ্যাটি কনসোলে প্রিন্ট হবে না। ৩২ বাদে ০ থেকে ১০০ পর্যন্ত 
     বাকি সব সংখ্যা প্রিন্ট হবে।
  */
  console.log(i);
}

/* 
   পুরো প্রক্রিয়াটি যেভাবে কাজ করছে:
   - লুপটি ০ থেকে ৩১ পর্যন্ত স্বাভাবিকভাবে প্রিন্ট করে।
   - যখন i এর মান ৩২ হয়, তখন if শর্তটি সত্য হয়।
   - 'continue' পাওয়ার কারণে ৩২ প্রিন্ট করার লাইনটি স্কিপ (skip) বা বাদ পড়ে যায়।
   - লুপটি ভেঙে যায় না (break এর মতো), বরং ৩৩ থেকে আবার প্রিন্ট করা শুরু করে।
   - সহজ কথায়: 'break' লুপ থামিয়ে দেয়, আর 'continue' শুধু বর্তমান ধাপটি বাদ দিয়ে পরের ধাপে যায়।
*/
