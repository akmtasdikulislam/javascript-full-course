/* 
   নিচে একটি 'do-while loop' এর উদাহরণ দেওয়া হয়েছে। 
   do-while লুপের বিশেষত্ব হলো, এটি শর্ত (condition) চেক করার আগেই অন্তত একবার কোডটি রান করে।
*/

/* 
   ১. let i = 10; (Initialization)
   এখানে আমরা লুপের শুরুর মান ১০ নির্ধারণ করছি। 
   'i' ভেরিয়েবলটি লুপের কাউন্টার হিসেবে কাজ করবে।
*/
let i = 10;

/* 
   ২. do { ... } while (condition);
   do ব্লকের ভেতরে থাকা কোডগুলো প্রথমে একবার কাজ করবেই, 
   তারপর while এর শর্তটি চেক করা হবে।
*/
do {
  /* 
     ৩. console.log(i);
     বর্তমান 'i' এর মান কনসোলে প্রিন্ট করা হচ্ছে। 
     যেহেতু এটি do ব্লকের ভেতরে, তাই শর্ত মিথ্যা হলেও প্রথমবার এটি প্রিন্ট হবে।
  */
  console.log(i);

  /* 
     ৪. i++; (Increment)
     প্রতিবার লুপের কাজ শেষে i এর মান ১ করে বাড়ানো হচ্ছে। 
     এটি না করলে i এর মান সবসময় ১০ থেকে যেত এবং লুপটি কখনো থামত না।
  */
  i++;

  /* 
   ৫. while (i < 100); (Condition)
   এখানে শর্ত চেক করা হচ্ছে। যতক্ষণ i এর মান ১০০ এর কম থাকবে, 
   ততক্ষণ লুপটি পুনরায় 'do' ব্লকের শুরুতে ফিরে যাবে এবং কোডটি চালাবে।
*/
} while (i < 100);

/* 
   পুরো প্রক্রিয়াটি যেভাবে কাজ করছে:
   - প্রথমে i = 10 সেট হয়।
   - 'do' ব্লকের কারণে কোনো শর্ত ছাড়াই ১০ প্রিন্ট হয় এবং i বেড়ে ১১ হয়।
   - এরপর চেক করে ১১ কি ১০০ থেকে ছোট? হ্যাঁ, তাই আবার লুপটি চলে।
   - এভাবে চলতে চলতে যখন i এর মান ১০০ হয়, তখন (100 < 100) শর্তটি মিথ্যা হয় এবং লুপটি বন্ধ হয়ে যায়।
   - মনে রাখবেন: while লুপে শর্ত আগে চেক হয়, কিন্তু do-while লুপে কাজ আগে হয় এবং শর্ত পরে চেক হয়।
*/
