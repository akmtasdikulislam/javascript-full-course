/* 
   নিচে 'break' কি-ওয়ার্ডের একটি উদাহরণ দেওয়া হয়েছে। 
   'break' ব্যবহার করা হয় কোনো লুপকে তার নির্ধারিত শর্ত শেষ হওয়ার আগেই থামিয়ে দেওয়ার জন্য।
*/

/* 
   ১. for (let i = 0; i < 101; i++)
   এখানে একটি for লুপ শুরু করা হয়েছে যা ০ থেকে ১০০ পর্যন্ত (১০১ এর কম) চলার কথা।
   - i = 0: লুপটি ০ থেকে শুরু হবে।
   - i < 101: শর্ত দেওয়া হয়েছে যেন এটি ১০০ পর্যন্ত চলে।
   - i++: প্রতিবার i এর মান ১ করে বাড়বে।
*/
for (let i = 0; i < 101; i++) {
  /* 
     ২. if (i === 32)
     লুপের ভেতরে একটি শর্ত দেওয়া হয়েছে। প্রতিবার চেক করা হচ্ছে i এর মান ৩২ কি না।
     যখনই i এর মান ৩২ হবে, তখনই এই if ব্লকের ভেতরের কোড কাজ করবে।
  */
  if (i === 32) {
    /* 
       ৩. break;
       এটিই হলো মূল কি-ওয়ার্ড। যখন i এর মান ৩২ হয়, তখন 'break' লুপটিকে 
       পুরোপুরি বন্ধ করে দেয়। এরপর লুপটি আর চলবে না, এমনকি ১০০ পর্যন্তও যাবে না।
    */
    break;
  }
  /* 
     ৪. console.log(i);
     এটি i এর মান প্রিন্ট করে। যেহেতু ৩২ এ পৌঁছালে লুপটি ভেঙে (break) যায়, 
     তাই কনসোলে আমরা ০ থেকে ৩১ পর্যন্ত সংখ্যাগুলো দেখতে পাব।
  */
  console.log(i);
}

/* 
   পুরো প্রক্রিয়াটি যেভাবে কাজ করছে:
   - লুপটি ০ থেকে প্রিন্ট করা শুরু করে।
   - যখন i এর মান ৩২ হয়, তখন if শর্তটি সত্য হয়।
   - 'break' কমান্ড পাওয়ার সাথে সাথে লুপটি সাথে সাথে থেমে যায়।
   - ফলে ৩২ বা তার পরের কোনো সংখ্যা আর প্রিন্ট হয় না।
*/
