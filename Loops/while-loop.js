/* 
   নিচে একটি 'while loop' এর উদাহরণ দেওয়া হয়েছে। 
   while লুপ ততক্ষণ কাজ করে যতক্ষণ একটি নির্দিষ্ট শর্ত (condition) সত্য থাকে।
*/

/* 
   ১. let i = 1; (Initialization)
   এখানে আমরা লুপের শুরুর মান নির্ধারণ করছি। 
   আমরা 'i' নামে একটি ভেরিয়েবল নিয়েছি যার মান ১।
*/
let i = 1;

/* 
   ২. while (i < 101) (Condition)
   এটি লুপের শর্ত। যতক্ষণ i এর মান ১০১ এর কম থাকবে, 
   ততক্ষণ লুপের ভেতরের কোডগুলো বারবার চলতে থাকবে।
*/
while (i < 101) {
  /* 
     ৩. console.log(i);
     এই লাইনটি বর্তমান 'i' এর মান কনসোলে প্রিন্ট করছে। 
     প্রথমবার এটি ১ প্রিন্ট করবে, তারপর ২, এভাবে ক্রমান্বয়ে চলবে।
  */
  console.log(i);

  /* 
     ৪. i++; (Increment)
     প্রতিবার লুপের কাজ শেষ হওয়ার পর i এর মান ১ করে বাড়ানো হচ্ছে। 
     এটি না করলে i এর মান সবসময় ১ থেকে যেত এবং লুপটি কখনো থামত না (Infinite Loop)।
  */
  i++;
}

/* 
   পুরো প্রক্রিয়াটি যেভাবে কাজ করছে:
   - প্রথমে চেক করে ১ কি ১০১ থেকে ছোট? হ্যাঁ, তাই ১ প্রিন্ট হয় এবং i বেড়ে ২ হয়।
   - এভাবে চলতে চলতে যখন i এর মান ১০১ হয়, তখন (101 < 101) শর্তটি মিথ্যা হয় এবং লুপটি বন্ধ হয়ে যায়।
*/
