/* 
   নিচে একটি 'for loop' এর উদাহরণ দেওয়া হয়েছে। 
   লুপ ব্যবহার করা হয় যখন আমরা একই কাজ বারবার করতে চাই।
*/

/* 
   for লুপের তিনটি প্রধান অংশ থাকে:
   ১. let i = 1; (Initialization) - এখানে আমরা একটি ভেরিয়েবল 'i' শুরু করছি ১ দিয়ে। এটি লুপের শুরুর মান।
   ২. i < 101; (Condition) - লুপটি কতক্ষণ চলবে তার শর্ত। যতক্ষণ i এর মান ১০১ এর কম থাকবে, ততক্ষণ লুপটি চলবে।
   ৩. i++ (Increment) - প্রতিবার লুপ শেষ হওয়ার পর i এর মান ১ করে বাড়বে।
*/
for (let i = 1; i < 101; i++) {
  /* 
     console.log(i); 
     এই লাইনটি বর্তমান 'i' এর মান কনসোলে প্রিন্ট করছে। 
     প্রথমবার এটি ১ প্রিন্ট করবে, তারপর ২, এভাবে ১০০ পর্যন্ত চলবে।
  */
  console.log(i);
}

/* 
   পুরো প্রক্রিয়াটি যেভাবে কাজ করছে:
   - প্রথমে i = 1 সেট হয়।
   - চেক করে ১ কি ১০১ থেকে ছোট? হ্যাঁ, তাই ১ প্রিন্ট হয়।
   - i++ এর মাধ্যমে i এখন ২ হয়।
   - আবার চেক করে ২ কি ১০১ থেকে ছোট? হ্যাঁ, তাই ২ প্রিন্ট হয়।
   - এভাবে যখন i এর মান ১০১ হয়ে যায়, তখন শর্তটি (101 < 101) মিথ্যা বা False হয় এবং লুপটি বন্ধ হয়ে যায়।
*/
