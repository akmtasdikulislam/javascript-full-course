/*
Types of Operators
    - Arithmetic (গাণিতিক হিসাব-নিকাশের জন্য ব্যবহৃত হয়)
    - Comparison (দুটি ভ্যালুর মধ্যে তুলনা করার জন্য ব্যবহৃত হয়)
    - Logical (যৌক্তিক সিদ্ধান্ত বা একাধিক শর্ত চেক করার জন্য ব্যবহৃত হয়)
    - Assignment (ভ্যারিয়েবলে মান নির্ধারণ বা সেট করার জন্য ব্যবহৃত হয়)
    - Unary (একটি মাত্র অপারেন্ড বা ভ্যালুর ওপর কাজ করে)
    - Ternary (শর্তের ওপর ভিত্তি করে এক লাইনে সিদ্ধান্ত নেওয়ার জন্য ব্যবহৃত হয়)

Examples

Arithmetic Operators: 
+ (যোগ)
- (বিয়োগ)
* (গুণ)
/ (ভাগ)
% (ভাগশেষ বা Modulus)
** (Power বা ঘাত)। 
এগুলো মূলত গাণিতিক ক্যালকুলেশনের জন্য ব্যবহৃত হয়। 


Assignment Operators: 
= (মান সেট করা)
== (মান সমান কি না চেক করা - Loose Equality)
=== (মান এবং ডাটা টাইপ উভয়ই সমান কি না চেক করা - Strict Equality)। 

Comparison & Logical Operators: 
< (ছোট)
> (বড়)
<= (ছোট বা সমান)
>= (বড় বা সমান) 
! (Not - মান উল্টে দেওয়া)
!= (অসমান কি না)
!== (টাইপসহ অসমান কি না)
!! (Boolean-এ রূপান্তর করা)


Logical Operators: 
&& (Logical AND - সব শর্ত সত্য হতে হবে) 
|| (Logical OR - যেকোনো একটি শর্ত সত্য হলেই হবে)। 

Ternary Operator: 
? : (এটি if-else এর সংক্ষিপ্ত রূপ। শর্ত সত্য হলে '?' এর পরের অংশ কাজ করে, 
মিথ্যা হলে ':' এর পরের অংশ কাজ করে)। 
*/
