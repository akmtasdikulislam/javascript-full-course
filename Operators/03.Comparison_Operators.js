/* 
Loose Equality (==): এটি শুধুমাত্র ভ্যালু বা মান সমান কি না তা চেক করে। 
যদি ডাটা টাইপ আলাদা হয়, তবে এটি ইন্টারনাল টাইপ কনভার্সন (Type Coercion) করে মান মিলিয়ে দেখে। 
*/
console.log(12 == 12);

/* 
এখানে একটি নাম্বার ১২ এবং একটি স্ট্রিং "12" এর তুলনা করা হচ্ছে। 
যেহেতু এটি '==' (Loose Equality), তাই JavaScript স্ট্রিংটিকে নাম্বারে রূপান্তর করে ফেলে। 
উভয় মান সমান হওয়ায় এটি true রিটার্ন করবে। 
*/
console.log(12 == "12");

/* 
Strict Equality (===): এটি মান এবং ডাটা টাইপ (যেমন: Number, String) উভয়ই চেক করে। 
এখানে ১২ এবং ১২ উভয়ই নাম্বার এবং মান সমান, তাই true আসবে। 
*/
console.log(12 === 12);

/* 
এখানে মান ১২ হলেও একটি হলো Number এবং অন্যটি String। 
Strict Equality (===) কোনো টাইপ কনভার্সন করে না, তাই টাইপ আলাদা হওয়ায় এটি false রিটার্ন করবে। 
*/
console.log(12 === "12");

/* 
Inequality (!=): এটি চেক করে যে দুটি মান অসমান কি না। 
যেহেতু ১২ এবং ১৩ সমান নয়, তাই শর্তটি সত্য এবং আউটপুট true আসবে। 
*/
console.log(12 != 13);

/* 
Strict Inequality (!==): এটি চেক করে যে মান অথবা ডাটা টাইপ—যেকোনো একটি অসমান কি না। 
এখানে ১২ (Number) এবং "12" (String) এর ডাটা টাইপ আলাদা, তাই এটি true রিটার্ন করবে। 
*/
console.log(12 !== "12");

/* 
Greater Than or Equal To (>=): এটি চেক করে বাম পাশের মান ডান পাশের চেয়ে বড় অথবা সমান কি না। 
যেহেতু ১২ এবং ১২ সমান, তাই এটি true হবে। 
*/
console.log(12 >= 12);

/* 
এখানে ১৪, ১২-এর চেয়ে বড়, তাই শর্তটি সত্য (true)। 
*/
console.log(14 >= 12);

/* 
Less Than or Equal To (<=): এটি চেক করে বাম পাশের মান ডান পাশের চেয়ে ছোট অথবা সমান কি না। 
যেহেতু ১২ এবং ১২ সমান, তাই এটি true হবে। 
*/
console.log(12 <= 12);

/* 
এখানে ১২, ১৪-এর চেয়ে ছোট, তাই শর্তটি সত্য (true)। 
*/
console.log(12 <= 14);

/* 
Less Than (<): এটি চেক করে বাম পাশের মান ডান পাশের চেয়ে ছোট কি না। 
১২ যেহেতু ১৪ থেকে ছোট, তাই এটি true রিটার্ন করবে। 
*/
console.log(12 < 14);

/* 
Greater Than (>): এটি চেক করে বাম পাশের মান ডান পাশের চেয়ে বড় কি না। 
১৪ যেহেতু ১২ থেকে বড়, তাই এটি true রিটার্ন করবে। 
*/
console.log(14 > 12);
