/* 
এখানে 'a' নামে একটি ভ্যারিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে প্রাথমিক মান হিসেবে ১২ (12) অ্যাসাইন করা হয়েছে। 
*/
let a = 12;

/* 
Addition Assignment (+=): 
এটি 'a = a + 3' এর সংক্ষিপ্ত রূপ। 
অর্থাৎ, 'a' এর বর্তমান মান ১২-এর সাথে ৩ যোগ করে নতুন মান ১৫ আবার 'a' তেই সেভ করা হচ্ছে। 
*/
a += 3;
console.log(a); // আউটপুট: 15

/* 
Subtraction Assignment (-=): 
এটি 'a = a - 3' এর সংক্ষিপ্ত রূপ। 
বর্তমানে 'a' এর মান ১৫, সেখান থেকে ৩ বিয়োগ করলে ১২ হয় এবং সেটি 'a' তে স্টোর হয়। 
*/
a -= 3;
console.log(a); // আউটপুট: 12

/* 
Division Assignment (/=): 
এটি 'a = a / 3' এর সংক্ষিপ্ত রূপ। 
বর্তমানে 'a' এর মান ১২, একে ৩ দিয়ে ভাগ করলে ভাগফল ৪ হয় এবং সেটি 'a' তে স্টোর হয়। 
*/
a /= 3;
console.log(a); // আউটপুট: 4

/* 
Multiplication Assignment (*=): 
এটি 'a = a * 3' এর সংক্ষিপ্ত রূপ। 
বর্তমানে 'a' এর মান ৪, একে ৩ দিয়ে গুণ করলে গুণফল ১২ হয় এবং সেটি 'a' তে স্টোর হয়। 
*/
a *= 3;
console.log(a); // আউটপুট: 12

/* 
Remainder/Modulus Assignment (%=): 
এটি 'a = a % 5' এর সংক্ষিপ্ত রূপ। 
বর্তমানে 'a' এর মান ১২, একে ৫ দিয়ে ভাগ করলে ভাগশেষ (Remainder) থাকে ২ (৫ দুগুনে ১০, অবশিষ্ট ২)। 
এই ২ মানটি 'a' তে স্টোর হচ্ছে। 
*/
a %= 5;
console.log(a); // আউটপুট: 2

/* 
Exponentiation Assignment (**=): 
এটি 'a = a ** 3' এর সংক্ষিপ্ত রূপ। 
বর্তমানে 'a' এর মান ২, তাই ২-এর পাওয়ার ৩ (২ কিউব) করলে ফলাফল ৮ হয়। 
*/
a **= 3;
console.log(a); // আউটপুট: 8
