/* 
   এখানে 'temp' (তাপমাত্রা) নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে ৩৫ (35) অ্যাসাইন করা হয়েছে। 
   এটি আমাদের কন্ডিশন চেক করার জন্য ইনপুট ভ্যালু হিসেবে কাজ করবে। 
*/
let temp = 35;

/* 
   এখানে একটি 'if' স্টেটমেন্ট ব্যবহার করা হয়েছে যেখানে '!' (Logical NOT) অপারেটর ব্যবহার করা হয়েছে। 
   ভিতরের অংশ (temp < 35): চেক করছে তাপমাত্রা ৩৫ এর কম কি না। যেহেতু temp এর মান ৩৫, তাই ৩৫ < ৩৫ কন্ডিশনটি মিথ্যা (False)। 
   বাইরের অংশ (!): লজিক্যাল NOT অপারেটর কোনো রেজাল্টকে উল্টে দেয়। 
   যেহেতু ভিতরের কন্ডিশনটি মিথ্যা (False) ছিল, তাই '!' ব্যবহারের ফলে সেটি সত্য (True) হয়ে যাবে। 
*/
if (!(temp < 35)) {
  /* 
     যেহেতু '!(false)' এর ফলাফল 'true' বা সত্য হয়েছে, তাই এই ব্লকের কোডটি কার্যকর হবে। 
     কনসোলে "Hot" প্রিন্ট হবে। 
     সহজ কথায়: যদি তাপমাত্রা ৩৫ এর কম না হয়, তবেই এটি প্রিন্ট হবে। 
  */
  console.log("Hot");
} else {
  /* 
     যদি 'if' এর কন্ডিশনটি শেষ পর্যন্ত মিথ্যা (False) হতো, তবে কোডটি এই 'else' ব্লকে আসতো। 
     অর্থাৎ তাপমাত্রা যদি ৩৫ এর কম হতো, তবে "Pleasant" প্রিন্ট হতো। 
  */
  console.log("Pleasant");
}

/* 
   সারসংক্ষেপ: 
   ১. temp = 35
   ২. (temp < 35) -> (35 < 35) যা False.
   ৩. !(False) -> True.
   ৪. ফলাফল: "Hot"
*/
