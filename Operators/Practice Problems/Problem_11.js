/* 
   এখানে 'x' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে প্রাথমিক মান হিসেবে ৩ (3) অ্যাসাইন করা হয়েছে। 
   এটি একটি সাধারণ সংখ্যা (Number) টাইপ ভেরিয়েবল।
*/
let x = 3;

/* 
   এখানে 'y' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে '++x' এর মান অ্যাসাইন করা হয়েছে। 
   এখানে '++x' হলো একটি 'Pre-increment' অপারেটর। 
   
   প্রি-ইনক্রিমেন্টের কাজ করার পদ্ধতি:
   ১. প্রথমে 'x' এর বর্তমান মানের সাথে ১ যোগ হবে। অর্থাৎ ৩ থেকে বেড়ে ৪ হবে। 
   ২. এরপর এই বর্ধিত মানটি (৪) 'y' ভেরিয়েবলে অ্যাসাইন হবে। 
   
   সহজ কথায়: আগে মান বাড়বে, তারপর ব্যবহার হবে। তাই 'x' এবং 'y' উভয়ের মানই ৪ হবে।
*/
let y = ++x;

/* 
   সবশেষে 'console.log()' এর মাধ্যমে 'x' এবং 'y' এর বর্তমান মান আউটপুট হিসেবে দেখানো হচ্ছে। 
   যেহেতু প্রি-ইনক্রিমেন্টের কারণে অ্যাসাইনমেন্টের আগেই মান বেড়ে গিয়েছিল, 
   তাই আউটপুট আসবে: 4 4
*/
console.log(x, y);

/* 
   সারসংক্ষেপ: 
   ১. শুরুতে x = 3.
   ২. y = ++x লাইনে:
      - প্রথমে x এর মান ১ বেড়ে 4 হলো।
      - এরপর সেই 4 মানটি y এর মধ্যে বসে গেল।
   ৩. ফলাফল: x = 4, y = 4.

   নোট: 
   যদি এখানে 'x++' (Post-increment) ব্যবহার করা হতো, তবে 'x' এর মান ৪ হলেও 'y' এর মান ৩ হতো। 
   কারণ পোস্ট-ইনক্রিমেন্টে আগে মান অ্যাসাইন হয়, তারপর বাড়ে। 
   কিন্তু এখানে '++x' (Pre-increment) ব্যবহার করায় আগে মান বেড়েছে, তারপর অ্যাসাইন হয়েছে।
*/
