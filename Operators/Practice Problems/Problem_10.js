/* 
   এখানে 'x' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে প্রাথমিক মান হিসেবে ৩ (3) অ্যাসাইন করা হয়েছে। 
   এটি একটি সাধারণ সংখ্যা (Number) টাইপ ভেরিয়েবল।
*/
let x = 3;

/* 
   এখানে 'y' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে 'x++' এর মান অ্যাসাইন করা হয়েছে। 
   এখানে 'x++' হলো একটি 'Post-increment' অপারেটর। 
   
   পোস্ট-ইনক্রিমেন্টের কাজ করার পদ্ধতি:
   ১. প্রথমে 'x' এর বর্তমান মান (যা ৩) 'y' ভেরিয়েবলে অ্যাসাইন হবে। 
   ২. এরপর 'x' এর মান ১ বৃদ্ধি পেয়ে ৪ হবে। 
   
   সহজ কথায়: আগে মান ব্যবহার হবে, তারপর বাড়বে। তাই 'y' পাবে ৩, কিন্তু 'x' নিজে হয়ে যাবে ৪।
*/
let y = x++;

/* 
   সবশেষে 'console.log()' এর মাধ্যমে 'x' এবং 'y' এর বর্তমান মান আউটপুট হিসেবে দেখানো হচ্ছে। 
   যেহেতু ইনক্রিমেন্ট হওয়ার পর x = 4 এবং অ্যাসাইনমেন্টের সময় y = 3 ছিল, 
   তাই আউটপুট আসবে: 4 3
*/
console.log(x, y);

/* 
   সারসংক্ষেপ: 
   ১. শুরুতে x = 3.
   ২. y = x++ লাইনে:
      - y এর মধ্যে x এর বর্তমান মান (3) বসে গেল।
      - এরপর x এর মান ১ বেড়ে 4 হলো।
   ৩. ফলাফল: x = 4, y = 3.

   নোট: 
   যদি এখানে '++x' (Pre-increment) ব্যবহার করা হতো, তবে 'x' এবং 'y' উভয়ের মানই ৪ হতো। 
   কারণ প্রি-ইনক্রিমেন্টে আগে মান বাড়ে, তারপর অন্য ভেরিয়েবলে অ্যাসাইন হয়। 
   কিন্তু পোস্ট-ইনক্রিমেন্টে (x++) আগে মান অ্যাসাইন হয়, তারপর বাড়ে।
*/
