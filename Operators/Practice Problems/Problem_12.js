/* 
   এখানে 'm' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে প্রাথমিক মান হিসেবে ১০ (10) অ্যাসাইন করা হয়েছে। 
   এটি একটি সাধারণ সংখ্যা (Number) টাইপ ভেরিয়েবল।
*/
let m = 10;

/* 
   এখানে 'm--' ব্যবহার করা হয়েছে, যা একটি 'Post-decrement' অপারেটর। 
   
   পোস্ট-ডিক্রিমেন্টের কাজ করার পদ্ধতি:
   ১. প্রথমে ভেরিয়েবলের বর্তমান মানটি ব্যবহার হয় (এক্ষেত্রে কনসোলে প্রিন্ট হয়)। 
   ২. এরপর ভেরিয়েবলের মান ১ কমে যায়। 
   
   যেহেতু এটি পোস্ট-ডিক্রিমেন্ট, তাই Line 1 এ 'm' এর বর্তমান মান ১০-ই প্রিন্ট হবে। 
   কিন্তু এই লাইনটি শেষ হওয়ার সাথে সাথে 'm' এর মান কমে ৯ হয়ে যাবে।
*/
console.log(m--);

/* 
   আগের লাইনে পোস্ট-ডিক্রিমেন্ট (m--) হওয়ার কারণে 'm' এর মান এখন ৯। 
   তাই এই লাইনে 'console.log(m)' এর আউটপুট আসবে ৯।
*/
console.log(m);

/* 
   সারসংক্ষেপ: 
   ১. শুরুতে m = 10.
   ২. console.log(m--) লাইনে:
      - আগে m এর বর্তমান মান (10) প্রিন্ট হলো।
      - তারপর m এর মান ১ কমে 9 হলো।
   ৩. console.log(m) লাইনে:
      - m এর বর্তমান মান 9 প্রিন্ট হলো।
   ৪. ফলাফল: 10 এবং 9.

   নোট: 
   যদি এখানে '--m' (Pre-decrement) ব্যবহার করা হতো, তবে প্রথম লাইনেই আউটপুট ৯ আসতো। 
   কারণ প্রি-ডিক্রিমেন্টে আগে মান কমে, তারপর ব্যবহার হয়।
*/
