/* 
   এখানে 'score' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে ৭৮ (78) অ্যাসাইন করা হয়েছে। 
   এটি শিক্ষার্থীর প্রাপ্ত নম্বর হিসেবে কাজ করবে যা গ্রেড নির্ধারণের জন্য ব্যবহৃত হবে। 
*/
let score = 78;

/* 
   এখানে 'Ternary Operator' (? :) ব্যবহার করে গ্রেড নির্ধারণ করা হয়েছে। 
   টারনারি অপারেটর হলো 'if-else' এর একটি সংক্ষিপ্ত রূপ। এখানে চেইন আকারে একাধিক কন্ডিশন চেক করা হচ্ছে:
   
   ১. score >= 90 ? "A" : -> যদি স্কোর ৯০ বা তার বেশি হয়, তবে "A" হবে। (এখানে ৭৮ >= ৯০ মিথ্যা)
   ২. score >= 75 ? "B" : -> যদি প্রথমটি মিথ্যা হয়, তবে চেক করবে স্কোর ৭৫ বা তার বেশি কি না। 
      (এখানে ৭৮ >= ৭৫ সত্য, তাই ফলাফল হবে "B")
   ৩. score >= 60 ? "C" : -> যদি উপরের সব মিথ্যা হতো, তবে এটি চেক করতো।
   ৪. "Fail" -> যদি কোনো কন্ডিশনই সত্য না হয়, তবে ডিফল্ট হিসেবে "Fail" হবে।

   যেহেতু ৭৮ সংখ্যাটি ৭৫ এর চেয়ে বড় কিন্তু ৯০ এর চেয়ে ছোট, তাই এটি "B" গ্রেড রিটার্ন করবে।
*/
let grade = score >= 90 ? "A" : score >= 75 ? "B" : score >= 60 ? "C" : "Fail";

/* 
   সবশেষে 'grade' ভেরিয়েবলে যে মানটি জমা হয়েছে (এক্ষেত্রে "B"), তা কনসোলে প্রিন্ট করা হচ্ছে। 
*/
console.log(grade);

