/* 
   এখানে 'count' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে প্রাথমিক মান হিসেবে ৫ (5) অ্যাসাইন করা হয়েছে। 
   এটি একটি সাধারণ সংখ্যা (Number) টাইপ ভেরিয়েবল যা কন্ডিশন চেক করার জন্য ব্যবহৃত হবে।
*/
let count = 5;

/* 
   এখানে একটি 'if' স্টেটমেন্টের ভেতরে 'count--' (Post-decrement) এবং '===' (Strict Equality) অপারেটর ব্যবহার করা হয়েছে। 
   
   অপারেশনটি যেভাবে কাজ করছে:
   ১. count-- (Post-decrement): পোস্ট-ডিক্রিমেন্টের নিয়ম হলো এটি প্রথমে ভেরিয়েবলের বর্তমান মানটি অপারেশনে ব্যবহার করে, 
      তারপর মান ১ কমিয়ে দেয়। 
   ২. তুলনা (Comparison): এখানে 'count' এর বর্তমান মান ৫ ব্যবহার করে ৫ === ৫ চেক করা হচ্ছে। 
      যেহেতু ৫ এবং ৫ সমান, তাই এই কন্ডিশনটি 'true' বা সত্য হবে।
   ৩. মান পরিবর্তন: এই লাইনটি বা কন্ডিশনটি চেক হওয়ার সাথে সাথেই 'count' এর মান ১ কমে ৪ হয়ে যাবে।
*/
if (count-- === 5) {
  /* 
     যেহেতু পোস্ট-ডিক্রিমেন্টের কারণে শুরুতে ৫ মানটিই তুলনার জন্য ব্যবহৃত হয়েছে এবং ৫ === ৫ সত্য হয়েছে, 
     তাই কোডটি এই ব্লকে প্রবেশ করবে এবং "Matched" প্রিন্ট করবে।
  */
  console.log("Matched");
} else {
  /* 
     যদি কন্ডিশনটি মিথ্যা হতো (যেমন: যদি এখানে --count ব্যবহার করা হতো), তবে এই 'else' ব্লকটি কাজ করতো।
  */
  console.log("Not Matched");
}

/* 
   সারসংক্ষেপ: 
   ১. শুরুতে count = 5.
   ২. if (count-- === 5) লাইনে:
      - প্রথমে count এর বর্তমান মান ৫ নিয়ে ৫ === ৫ চেক করা হলো (ফলাফল: True)।
      - চেক করার ঠিক পরেই count এর মান ১ কমে ৪ হলো।
   ৩. ফলাফল: "Matched" প্রিন্ট হবে।

   গুরুত্বপূর্ণ নোট: 
   যদি এখানে '--count' (Pre-decrement) ব্যবহার করা হতো:
   - প্রথমে count এর মান ৫ থেকে কমে ৪ হতো।
   - তারপর ৪ === ৫ চেক করা হতো, যা মিথ্যা (False) হতো।
   - সেক্ষেত্রে আউটপুট আসতো "Not Matched"।
   
   কিন্তু 'count--' (Post-decrement) ব্যবহারের ফলে আগে তুলনা হয়েছে, পরে মান কমেছে।
*/
