/* 
   এখানে 'b' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে প্রাথমিক মান হিসেবে ৭ (7) অ্যাসাইন করা হয়েছে। 
   এটি একটি সাধারণ সংখ্যা (Number) টাইপ ভেরিয়েবল।
*/
let b = 7;

/* 
   এখানে '++' (Increment Operator) ব্যবহার করা হয়েছে। 
   এটি একটি 'Pre-increment' অপারেটর যা ভেরিয়েবলের বর্তমান মানের সাথে ১ যোগ করে। 
   অর্থাৎ, b = b + 1; এখানে ৭ এর সাথে ১ যোগ হয়ে 'b' এর নতুন মান হবে ৮।
*/
++b;

/* 
   সবশেষে 'console.log()' এর মাধ্যমে 'b' ভেরিয়েবলের বর্তমান মানটি আউটপুট হিসেবে দেখানো হচ্ছে। 
   যেহেতু ইনক্রিমেন্ট অপারেটরের মাধ্যমে মান ১ বৃদ্ধি পেয়েছে, তাই এখানে ৮ (8) প্রিন্ট হবে।
*/
console.log(b);

/* 
   সারসংক্ষেপ: 
   ১. শুরুতে b = 7.
   ২. ++b করার ফলে মান ১ বেড়ে হয়েছে ৮.
   ৩. ফলাফল: 8

   নোট: 
   ++b (Pre-increment) এবং b++ (Post-increment) এর মধ্যে পার্থক্য আছে। 
   Pre-increment এর ক্ষেত্রে মান আগে বাড়ে তারপর ব্যবহার হয়, 
   তবে এখানে যেহেতু সরাসরি মান বাড়ানো হচ্ছে, তাই ফলাফল ৮-ই হবে।
*/
