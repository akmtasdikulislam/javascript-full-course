/* 
   এখানে 'points' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে ১২০ (120) অ্যাসাইন করা হয়েছে। 
   এটি মূলত একজন ব্যবহারকারীর অর্জিত পয়েন্ট যা তার মেম্বারশিপ স্ট্যাটাস নির্ধারণ করতে ব্যবহৃত হবে। 
*/
let points = 120;

/* 
   এখানে 'Ternary Operator' (? :) ব্যবহার করে 'status' নির্ধারণ করা হয়েছে। 
   টারনারি অপারেটর হলো 'if-else' এর একটি সংক্ষিপ্ত রূপ। এখানে চেইন আকারে একাধিক কন্ডিশন চেক করা হচ্ছে:
   
   ১. points > 100 ? "Gold" : -> প্রথমে চেক করছে পয়েন্ট ১০০ এর বেশি কি না। 
      (এখানে ১২০ > ১০০ সত্য, তাই ফলাফল সরাসরি "Gold" হবে এবং পরের কন্ডিশনগুলো আর চেক করবে না)
   
   ২. points > 50 ? "Silver" : -> যদি প্রথম কন্ডিশন মিথ্যা হতো, তবে চেক করতো পয়েন্ট ৫০ এর বেশি কি না। 
      যদি সত্য হতো তবে "Silver" হতো।
   
   ৩. "Bronze" -> যদি উপরের কোনো কন্ডিশনই সত্য না হতো (অর্থাৎ পয়েন্ট ৫০ বা তার কম হতো), 
      তবে ডিফল্ট হিসেবে "Bronze" হতো।

   যেহেতু ১২০ সংখ্যাটি ১০০ এর চেয়ে বড়, তাই এটি "Gold" স্ট্যাটাস রিটার্ন করবে।
*/
let status = points > 100 ? "Gold" : points > 50 ? "Silver" : "Bronze";

/* 
   সবশেষে 'status' ভেরিয়েবলে যে মানটি জমা হয়েছে (এক্ষেত্রে "Gold"), 
   তা 'console.log()' এর মাধ্যমে আউটপুট হিসেবে দেখানো হচ্ছে। 
*/
console.log(status);

/* 
   সারসংক্ষেপ: 
   ১. points = 120
   ২. প্রথম কন্ডিশন (120 > 100) সত্য (True) হয়েছে।
   ৩. ফলাফল: "Gold"

   নোট: টারনারি অপারেটর চেইনিংয়ের ক্ষেত্রে প্রথম যে কন্ডিশনটি সত্য হয়, 
   সেটির মানই ভেরিয়েবলে অ্যাসাইন হয়ে যায়।
*/
