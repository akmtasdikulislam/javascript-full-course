/* 
   এখানে 'a' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে প্রাথমিক মান হিসেবে ৫ (5) অ্যাসাইন করা হয়েছে। 
   এটি একটি সাধারণ সংখ্যা (Number) টাইপ ভেরিয়েবল।
*/
let a = 5;

/* 
   এখানে '++' (Increment Operator) ব্যবহার করা হয়েছে। 
   এটি একটি 'Post-increment' অপারেটর যা ভেরিয়েবলের বর্তমান মানের সাথে ১ যোগ করে। 
   অর্থাৎ, a = a + 1; এখানে ৫ এর সাথে ১ যোগ হয়ে 'a' এর নতুন মান হবে ৬।
*/
a++;

/* 
   সবশেষে 'console.log()' এর মাধ্যমে 'a' ভেরিয়েবলের বর্তমান মানটি আউটপুট হিসেবে দেখানো হচ্ছে। 
   যেহেতু ইনক্রিমেন্ট অপারেটরের মাধ্যমে মান ১ বৃদ্ধি পেয়েছে, তাই এখানে ৬ (6) প্রিন্ট হবে।
*/
console.log(a);

/* 
   সারসংক্ষেপ: 
   ১. শুরুতে a = 5.
   ২. a++ করার ফলে মান ১ বেড়ে হয়েছে ৬.
   ৩. ফলাফল: 6

   নোট: 
   a++ (Post-increment) এবং ++a (Pre-increment) এর মধ্যে পার্থক্য আছে, 
   তবে এখানে যেহেতু সরাসরি মান বাড়ানো হচ্ছে, তাই ফলাফল একই হবে।
*/
