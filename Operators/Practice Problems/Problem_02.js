/* 
   এখানে 'isAdmin' নামে একটি বুলিয়ান (Boolean) ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে 'true' অ্যাসাইন করা হয়েছে। 
   এটি নির্দেশ করছে যে ব্যবহারকারীর অ্যাডমিন প্রিভিলেজ বা ক্ষমতা আছে। 
*/
let isAdmin = true;

/* 
   এখানে 'isLoggedIn' নামে আরেকটি বুলিয়ান ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে 'false' অ্যাসাইন করা হয়েছে। 
   এর মানে হলো ব্যবহারকারী বর্তমানে সিস্টেমে লগ-ইন করা অবস্থায় নেই। 
*/
let isLoggedIn = false;

/* 
   এখানে একটি 'if' স্টেটমেন্ট ব্যবহার করা হয়েছে যেখানে '||' (Logical OR) অপারেটর ব্যবহার করা হয়েছে। 
   '||' অপারেটরের বৈশিষ্ট্য হলো, এর দুই পাশের কন্ডিশনের মধ্যে যেকোনো একটি সত্য (true) হলেই পুরো কন্ডিশনটি সত্য বলে গণ্য হয়। 
   এখানে 'isAdmin' সত্য (true), তাই 'isLoggedIn' মিথ্যা (false) হওয়া সত্ত্বেও 'if' ব্লকের কোডটি কার্যকর হবে। 
*/
if (isAdmin || isLoggedIn) {
  /* 
     যেহেতু 'isAdmin' সত্য, তাই লজিক্যাল OR এর শর্ত অনুযায়ী এক্সেস পাওয়া যাবে। 
     কনসোলে "Access granted" প্রিন্ট হবে। 
  */
  console.log("Access granted");
} else {
  /* 
     যদি 'isAdmin' এবং 'isLoggedIn' উভয়ই মিথ্যা (false) হতো, 
     তবেই কেবল কোডটি এই 'else' ব্লকে আসতো এবং "Access denied" প্রিন্ট করতো। 
  */
  console.log("Access denied");
}
