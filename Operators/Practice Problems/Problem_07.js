/* 
   এখানে 'loggedIn' নামে একটি বুলিয়ান (Boolean) ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে 'true' অ্যাসাইন করা হয়েছে। 
   এটি নির্দেশ করছে যে ব্যবহারকারী বর্তমানে সিস্টেমে লগ-ইন করা অবস্থায় আছেন। 
*/
let loggedIn = true;

/* 
   এখানে 'hasToken' নামে আরেকটি বুলিয়ান ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে 'false' অ্যাসাইন করা হয়েছে। 
   এটি নির্দেশ করছে যে ব্যবহারকারীর কাছে প্রয়োজনীয় সিকিউরিটি টোকেনটি নেই। 
*/
let hasToken = false;

/* 
   এখানে 'Ternary Operator' (? :) এবং 'Logical AND' (&&) অপারেটর ব্যবহার করে 'access' নির্ধারণ করা হয়েছে। 
   
   ১. (loggedIn && hasToken): এখানে লজিক্যাল AND চেক করছে দুটি শর্তই সত্য কি না। 
      যেহেতু 'loggedIn' সত্য (true) কিন্তু 'hasToken' মিথ্যা (false), তাই পুরো কন্ডিশনটি মিথ্যা (false) হবে। 
      লজিক্যাল AND এর ক্ষেত্রে একটি মিথ্যা হলেই ফলাফল মিথ্যা হয়।
   
   ২. ? "Allow" : "Deny" -> টারনারি অপারেটর অনুযায়ী যদি কন্ডিশন সত্য হতো তবে "Allow" রিটার্ন করতো, 
      কিন্তু যেহেতু কন্ডিশন মিথ্যা হয়েছে, তাই এটি "Deny" রিটার্ন করবে।
*/
let access = loggedIn && hasToken ? "Allow" : "Deny";

/* 
   সবশেষে 'access' ভেরিয়েবলে যে মানটি জমা হয়েছে (এক্ষেত্রে "Deny"), 
   তা 'console.log()' এর মাধ্যমে আউটপুট হিসেবে দেখানো হচ্ছে। 
*/
console.log(access);

/* 
   সারসংক্ষেপ: 
   ১. loggedIn = true, hasToken = false.
   ২. (true && false) এর ফলাফল হলো false.
   ৩. টারনারি অপারেটর false হওয়ার কারণে "Deny" বেছে নিয়েছে।
   ৪. ফলাফল: "Deny"
*/
