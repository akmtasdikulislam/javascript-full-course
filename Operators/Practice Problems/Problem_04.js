/* 
   এখানে 'a' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে ০ (0) অ্যাসাইন করা হয়েছে। 
   জাভাস্ক্রিপ্টে ০ (zero) একটি 'Falsy' ভ্যালু হিসেবে গণ্য হয়। 
*/
let a = 0;

/* 
   এখানে একটি 'if' স্টেটমেন্ট ব্যবহার করা হয়েছে যা সরাসরি ভেরিয়েবল 'a' এর মান পরীক্ষা করছে। 
   জাভাস্ক্রিপ্টে কিছু নির্দিষ্ট মানকে 'Falsy' ধরা হয় (যেমন: 0, "", null, undefined, NaN, এবং false)। 
   যেহেতু এখানে a = 0, তাই কন্ডিশনটি 'false' বা মিথ্যা হিসেবে বিবেচিত হবে। 
*/
if (a) {
  /* 
     যদি 'a' এর মান কোনো 'Truthy' ভ্যালু হতো (যেমন: কোনো সংখ্যা যা ০ নয়, অথবা কোনো স্ট্রিং), 
     তবে এই ব্লকের কোডটি কার্যকর হতো এবং "Truthy" প্রিন্ট হতো। 
  */
  console.log("Truthy");
} else {
  /* 
     যেহেতু 'a' এর মান ০ এবং এটি একটি 'Falsy' ভ্যালু, তাই 'if' কন্ডিশনটি মিথ্যা হয়েছে। 
     ফলে কোডটি এই 'else' ব্লকে চলে আসবে এবং কনসোলে "Falsy" প্রিন্ট করবে। 
  */
  console.log("Falsy");
}

