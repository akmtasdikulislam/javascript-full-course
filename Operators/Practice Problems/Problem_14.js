/* 
   এখানে 'likes' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে প্রাথমিক মান হিসেবে ১০০ (100) অ্যাসাইন করা হয়েছে। 
   এটি একটি গ্লোবাল ভেরিয়েবল যা পোস্টের মোট লাইক সংখ্যা ট্র্যাক করবে।
*/
let likes = 100;

/* 
   এখানে 'likePost' নামে একটি ফাংশন তৈরি করা হয়েছে। 
   এই ফাংশনটির কাজ হলো যখনই এটি কল করা হবে, তখন 'likes' ভেরিয়েবলের মান ১ বাড়িয়ে দেওয়া।
   
   ফাংশনের ভিতরে '++likes' (Pre-increment) ব্যবহার করা হয়েছে:
   ১. এটি প্রথমে 'likes' এর বর্তমান মানের সাথে ১ যোগ করবে (১০০ + ১ = ১০১)।
   ২. এরপর এই বর্ধিত মানটি (১০১) রিটার্ন করবে।
*/
function likePost() {
  return ++likes;
}

/* 
   এখানে 'likePost()' ফাংশনটিকে কল করা হচ্ছে এবং এর রিটার্ন করা মানটি 'console.log()' এর মাধ্যমে প্রিন্ট করা হচ্ছে। 
   যেহেতু ফাংশনের ভেতরে প্রি-ইনক্রিমেন্ট (++likes) ব্যবহার করা হয়েছে, 
   তাই এটি প্রথমে মান বাড়িয়ে ১০১ করবে এবং সেই ১০১-ই আউটপুট হিসেবে দেখাবে।
*/
console.log(likePost());

/* 
   যেহেতু 'likes' একটি গ্লোবাল ভেরিয়েবল এবং আগের লাইনে ফাংশনটি কল করার ফলে এর মান স্থায়ীভাবে বেড়ে গেছে, 
   তাই এখন 'likes' প্রিন্ট করলে আমরা আপডেট হওয়া মান অর্থাৎ ১০১ দেখতে পাবো।
*/
console.log(likes);

/* 
   সারসংক্ষেপ: 
   ১. শুরুতে likes = 100.
   ২. likePost() কল করার সময়:
      - ++likes এর কারণে likes এর মান ১০০ থেকে বেড়ে ১০১ হলো।
      - ফাংশনটি ১০১ রিটার্ন করলো এবং তা প্রিন্ট হলো।
   ৩. দ্বিতীয় console.log(likes) এ:
      - likes এর বর্তমান মান ১০১ প্রিন্ট হলো।
   ৪. ফলাফল: 101 এবং 101.

   গুরুত্বপূর্ণ নোট: 
   যদি এখানে 'likes++' (Post-increment) ব্যবহার করা হতো:
   - প্রথম console.log এ ১০০ প্রিন্ট হতো (কারণ পোস্ট-ইনক্রিমেন্ট আগে মান রিটার্ন করে, পরে বাড়ায়)।
   - কিন্তু দ্বিতীয় console.log এ ১০১ প্রিন্ট হতো (কারণ ততক্ষণে মান বেড়ে গেছে)।
   যেহেতু এখানে '++likes' ব্যবহার করা হয়েছে, তাই উভয় ক্ষেত্রেই আমরা ১০১ পাচ্ছি।
*/
