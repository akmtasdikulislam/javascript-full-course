/* 
   এখানে 'n' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে প্রাথমিক মান হিসেবে ৫ (5) অ্যাসাইন করা হয়েছে। 
   এটি একটি সাধারণ সংখ্যা (Number) টাইপ ভেরিয়েবল যা পরবর্তী গাণিতিক অপারেশনে ব্যবহৃত হবে।
*/
let n = 5;

/* 
   এখানে 'result' ভেরিয়েবলে একটি জটিল ইনক্রিমেন্ট অপারেশন করা হয়েছে। 
   অপারেশনটি বোঝার জন্য এটিকে দুটি ভাগে ভাগ করা যাক:
   
   ১. n++ (Post-increment): 
      - এখানে 'n' এর বর্তমান মান (৫) প্রথমে অপারেশনে ব্যবহৃত হবে। 
      - এই অংশটি শেষ হওয়ার পর 'n' এর মান ১ বেড়ে ৬ হবে।
   
   ২. ++n (Pre-increment): 
      - আগের ধাপের পর 'n' এর মান এখন ৬। 
      - প্রি-ইনক্রিমেন্ট হওয়ার কারণে প্রথমে 'n' এর মান ১ বাড়বে (৬ থেকে ৭ হবে)। 
      - এরপর এই বর্ধিত মানটি (৭) অপারেশনে ব্যবহৃত হবে।
   
   হিসাবটি দাঁড়ালো: ৫ (n++ থেকে) + ৭ (++n থেকে) = ১২।
*/
let result = n++ + ++n;

/* 
   সবশেষে 'console.log()' এর মাধ্যমে 'result' ভেরিয়েবলের মান আউটপুট হিসেবে দেখানো হচ্ছে। 
   যেহেতু ৫ এবং ৭ যোগ করা হয়েছে, তাই আউটপুট আসবে ১২।
*/
console.log(result);

/* 
   সারসংক্ষেপ: 
   ১. শুরুতে n = 5.
   ২. n++ অংশে: মান ৫ ব্যবহার হলো, কিন্তু n বেড়ে ৬ হলো।
   ৩. ++n অংশে: n এর মান ৬ থেকে বেড়ে ৭ হলো এবং সেই ৭ ব্যবহার হলো।
   ৪. মোট যোগফল: 5 + 7 = 12.
   ৫. ফলাফল: 12

   গুরুত্বপূর্ণ নোট: 
   এই ধরণের কোড ইন্টারভিউতে প্রায়ই দেখা যায়। মনে রাখতে হবে:
   - Post-increment (n++) আগে মান দেয়, পরে বাড়ায়।
   - Pre-increment (++n) আগে বাড়ায়, পরে মান দেয়।
   এখানে প্রথম অংশের পর n এর মান পরিবর্তিত হয়ে গিয়েছিল, যা দ্বিতীয় অংশে প্রভাব ফেলেছে।
*/
