/* 
   এখানে 'x' নামে একটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে ১০ (10) অ্যাসাইন করা হয়েছে। 
   এটি আমাদের কন্ডিশন চেক করার জন্য প্রথম ভ্যালু হিসেবে কাজ করবে। 
*/
let x = 10;

/* 
   এখানে 'y' নামে আরেকটি ভেরিয়েবল ডিক্লেয়ার করা হয়েছে এবং এতে ২০ (20) অ্যাসাইন করা হয়েছে। 
   এটি আমাদের কন্ডিশন চেক করার জন্য দ্বিতীয় ভ্যালু। 
*/
let y = 20;

/* 
   এখানে একটি 'if' স্টেটমেন্ট ব্যবহার করা হয়েছে যেখানে '&&' (Logical AND) অপারেটর আছে। 
   প্রথম অংশ (x > 5): চেক করছে x এর মান ৫ এর চেয়ে বড় কি না (১০ > ৫, যা সত্য)। 
   দ্বিতীয় অংশ (y < 25): চেক করছে y এর মান ২৫ এর চেয়ে ছোট কি না (২০ < ২৫, যা সত্য)। 
   যেহেতু '&&' অপারেটর ব্যবহার করা হয়েছে, তাই দুটি কন্ডিশনই সত্য হলে তবেই এই ব্লকের কোড চলবে। 
*/
if (x > 5 && y < 25) {
  /* 
     যেহেতু উপরের কন্ডিশনটি সত্য (True), তাই কনসোলে "A" প্রিন্ট হবে। 
     এর মানে হলো x এবং y উভয়ই আমাদের দেওয়া শর্ত পূরণ করেছে। 
  */
  console.log("A");
} else {
  /* 
     যদি উপরের কন্ডিশনগুলোর মধ্যে যেকোনো একটিও মিথ্যা (False) হতো, 
     তবে কোডটি এই 'else' ব্লকে চলে আসতো এবং "B" প্রিন্ট করতো। 
  */
  console.log("B");
}
